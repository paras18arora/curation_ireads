      <h3 id="introduction">Introduction</h3>

<p>Implementing a firewall is an important step in securing your server.  A large part of that is deciding on the individual rules and policies that will enforce traffic restrictions to your network.  Firewalls like <code>iptables</code> also allow you to have a say about the structural framework in which your rules are applied.</p>

<p>In this guide, we will construct a firewall that can be the basis for more complex rule sets.  This firewall will focus primarily on providing reasonable defaults and establishing a framework that encourages easy extensibility.  We will be demonstrating this on an Ubuntu 14.04 server.</p>

<h2 id="prerequisites">Prerequisites</h2>

<p>Before you begin, you should have a basic idea of the firewall policies you wish to implement.  You can follow <a href="https://indiareads/community/tutorials/how-to-choose-an-effective-firewall-policy-to-secure-your-servers">this guide</a> to get a better idea of some of the things you should be thinking about.</p>

<p>In order to follow along, you will need to have access to an Ubuntu 14.04 server.  We will be using a non-root user configured with <code>sudo</code> privileges throughout this guide.  You can learn how to configure this type of user in our <a href="https://indiareads/community/tutorials/initial-server-setup-with-ubuntu-14-04">Ubuntu 14.04 initial server setup guide</a>.</p>

<p>When you are finished, continue below.</p>

<h2 id="installing-the-persistent-firewall-service">Installing the Persistent Firewall Service</h2>

<p>To get started, you will need to install the <code>iptables-persistent</code> package if you have not done so already.  This will allow us to save our rule sets and have them automatically applied at boot:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo apt-get update
</li><li class="line" prefix="$">sudo apt-get install iptables-persistent
</li></ul></code></pre>
<p>During the installation, you'll be asked whether you want to save your current rules.  Say "yes" here.  We will be editing the generated rules files momentarily.</p>

<h2 id="a-note-about-ipv6-in-this-guide">A Note About IPv6 in this Guide</h2>

<p>Before we get started, we should talk briefly about IPv4 vs IPv6.  The <code>iptables</code> command only handles IPv4 traffic.  For IPv6 traffic, a separate companion tool called <code>ip6tables</code> is used.  The rules are stored in separate tables and chains.  For <code>iptables-persistent</code>, the IPv4 rules are written to and read from <code>/etc/iptables/rules.v4</code> and the IPv6 rules are kept in <code>/etc/iptables/rules.v6</code>.</p>

<p>This guide assumes that you are <strong>not</strong> actively using IPv6 on your server.  If your services do not leverage IPv6, it is safer to block access entirely, as we will be doing in this article.</p>

<h2 id="implementing-the-basic-firewall-policy-the-quick-way">Implementing the Basic Firewall Policy (The Quick Way)</h2>

<p>For the sake of getting up and running as quickly as possible, we'll show you how to edit the rules file directly to copy and paste the finished firewall policy.  Afterwards, we will explain the general strategy and show you how these rules could be implemented using the <code>iptables</code> command instead of modifying the file.</p>

<p>To implement our firewall policy and framework, we will be editing the <code>/etc/iptables/rules.v4</code> and <code>/etc/iptables/rules.v6</code> files.  Open the <code>rules.v4</code> file in your text editor with <code>sudo</code> privileges:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo nano /etc/iptables/rules.v4
</li></ul></code></pre>
<p>Inside, you will see a file that looks something like this:</p>
<div class="code-label " title="/etc/iptables/rules.v4">/etc/iptables/rules.v4</div><pre class="code-pre "><code langs=""># Generated by iptables-save v1.4.21 on Tue Jul 28 13:29:56 2015
*filter
:INPUT ACCEPT [0:0]
:FORWARD ACCEPT [0:0]
:OUTPUT ACCEPT [0:0]
COMMIT
# Completed on Tue Jul 28 13:29:56 2015
</code></pre>
<p>Replace the contents with:</p>
<div class="code-label " title="/etc/iptables/rules.v4">/etc/iptables/rules.v4</div><pre class="code-pre "><code langs="">*filter
# Allow all outgoing, but drop incoming and forwarding packets by default
:INPUT DROP [0:0]
:FORWARD DROP [0:0]
:OUTPUT ACCEPT [0:0]

# Custom per-protocol chains
:UDP - [0:0]
:TCP - [0:0]
:ICMP - [0:0]

# Acceptable UDP traffic

# Acceptable TCP traffic
-A TCP -p tcp --dport 22 -j ACCEPT

# Acceptable ICMP traffic

# Boilerplate acceptance policy
-A INPUT -m conntrack --ctstate ESTABLISHED,RELATED -j ACCEPT
-A INPUT -i lo -j ACCEPT

# Drop invalid packets
-A INPUT -m conntrack --ctstate INVALID -j DROP

# Pass traffic to protocol-specific chains
## Only allow new connections (established and related should already be handled)
## For TCP, additionally only allow new SYN packets since that is the only valid
## method for establishing a new TCP connection
-A INPUT -p udp -m conntrack --ctstate NEW -j UDP
-A INPUT -p tcp --syn -m conntrack --ctstate NEW -j TCP
-A INPUT -p icmp -m conntrack --ctstate NEW -j ICMP

# Reject anything that's fallen through to this point
## Try to be protocol-specific w/ rejection message
-A INPUT -p udp -j REJECT --reject-with icmp-port-unreachable
-A INPUT -p tcp -j REJECT --reject-with tcp-reset
-A INPUT -j REJECT --reject-with icmp-proto-unreachable

# Commit the changes
COMMIT

*raw
:PREROUTING ACCEPT [0:0]
:OUTPUT ACCEPT [0:0]
COMMIT

*nat
:PREROUTING ACCEPT [0:0]
:INPUT ACCEPT [0:0]
:OUTPUT ACCEPT [0:0]
:POSTROUTING ACCEPT [0:0]
COMMIT

*security
:INPUT ACCEPT [0:0]
:FORWARD ACCEPT [0:0]
:OUTPUT ACCEPT [0:0]
COMMIT

*mangle
:PREROUTING ACCEPT [0:0]
:INPUT ACCEPT [0:0]
:FORWARD ACCEPT [0:0]
:OUTPUT ACCEPT [0:0]
:POSTROUTING ACCEPT [0:0]
COMMIT
</code></pre>
<p>Save and close the file.</p>

<p>You can test the file for syntax errors by typing this command.  Fix any syntax errors that this reveals before continuing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables-restore -t /etc/iptables/rules.v4
</li></ul></code></pre>
<p>Next, open the <code>/etc/iptables/rules.v6</code> file to modify the IPv6 rules:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo nano /etc/iptables/rules.v6
</li></ul></code></pre>
<p>We can block all IPv6 traffic by replacing the contents of the file with the below configuration:</p>
<div class="code-label " title="/etc/iptables/rules.v6">/etc/iptables/rules.v6</div><pre class="code-pre "><code langs="">*filter
:INPUT DROP [0:0]
:FORWARD DROP [0:0]
:OUTPUT DROP [0:0]
COMMIT

*raw
:PREROUTING DROP [0:0]
:OUTPUT DROP [0:0]
COMMIT

*nat
:PREROUTING DROP [0:0]
:INPUT DROP [0:0]
:OUTPUT DROP [0:0]
:POSTROUTING DROP [0:0]
COMMIT

*security
:INPUT DROP [0:0]
:FORWARD DROP [0:0]
:OUTPUT DROP [0:0]
COMMIT

*mangle
:PREROUTING DROP [0:0]
:INPUT DROP [0:0]
:FORWARD DROP [0:0]
:OUTPUT DROP [0:0]
:POSTROUTING DROP [0:0]
COMMIT
</code></pre>
<p>Save and close the file.</p>

<p>To test this file for syntax errors, we can use the <code>ip6tables-restore</code> command with the <code>-t</code> option:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo ip6tables-restore -t /etc/iptables/rules.v6
</li></ul></code></pre>
<p>When both rules files report no syntax errors, you can apply the rules within by typing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo service iptables-persistent reload
</li></ul></code></pre>
<p>This will immediately implement the policy outlined in your files.  You can verify this by listing the <code>iptables</code> rules currently in use:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -S
</li><li class="line" prefix="$">sudo ip6tables -S
</li></ul></code></pre>
<p>These firewall rules will be re-applied at each boot.  Test to make sure that you can still log in and that all other access is blocked off.</p>

<h2 id="an-explanation-of-our-general-firewall-strategy">An Explanation of Our General Firewall Strategy</h2>

<p>In the basic firewall we've constructed with the above rules, we've created an extensible framework that can be easily adjusted to add or remove rules.  For IPv4 traffic, we're mainly concerned with the <code>INPUT</code> chain within the <code>filter</code> table.  This chain will process all packets destined for our server.  We've also allowed all outgoing traffic and denied all packet forwarding, which would only be appropriate if this server were acting as a router for other hosts.  We accept packets in all of the other tables since we are only looking to filter packets in this guide.</p>

<p>In general, our rules set up a firewall that will deny incoming traffic by default.  We then go about creating exceptions for the services and traffic types we wish to exclude from this policy.</p>

<p>In the main <code>INPUT</code> chain, we've added some generic rules for traffic that we are confident will always be handled the same way.  For instance, we always want to deny packets that are deemed "invalid" and we will always want to allow traffic on the local loopback interface and data associated with an established connection.</p>

<p>Afterwards, we match traffic based on the protocol it is using and shuffle it to a protocol-specific chain.  These protocol-specific chains are meant to hold rules that match and allow traffic for specific services.  In this example, the only service we allow is SSH in our <code>TCP</code> chain.  If we were offering another service, like an HTTP(S) server, we could add exceptions that here as well.  These chains will be the focus of most of your customization.</p>

<p>Any traffic that does not match the generic rules or the service rules in the protocol-specific are handled by the last few rules in the <code>INPUT</code> chain.  We have set the default policy to <code>DROP</code> for our firewall, which will deny packets that fall through our rules.  However, the rules at the end of the <code>INPUT</code> chain reject packets and send a message to the client that mimics how the server would respond if there were no service running on that port.</p>

<p>For IPv6 traffic, we simply drop all traffic.  Our server is not using this protocol, so it is safest to not engage with the traffic at all.</p>

<h2 id="optional-update-nameservers">(Optional) Update Nameservers</h2>

<p>Blocking all IPv6 traffic can interfere with how your server resolves things on the Internet. For example, this can affect how you use APT.</p>

<p>If you get errors like this when you try to run <code>apt-get update</code>:</p>
<div class="code-label " title="Error">Error</div><pre class="code-pre "><code langs="">Err http://security.ubuntu.com trusty-security InRelease

