<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo nano /etc/apt/apt.conf.d/99force-ipv4
</li></ul></code></pre>
<p>Add this single line to the file:</p>
<div class="code-label " title="/etc/apt/apt.conf.d/99force-ipv4">/etc/apt/apt.conf.d/99force-ipv4</div><pre class="code-pre "><code langs="">Acquire::ForceIPv4 "true";
</code></pre>
<p>Save and close the file. Now you should be able to use APT.</p>

<h2 id="implementing-our-firewalls-using-the-iptables-command">Implementing our Firewalls Using the IPTables Command</h2>

<p>Now that you understand the general idea behind the policy we built, we will walk through how you could go about creating those rules using <code>iptables</code> commands.  We will end up with the same rules that we specified above but we will create our policies by adding rules iteratively.  Because <code>iptables</code> applies each of the rules immediately, rule ordering is very important (we leave the rules that deny packets until the end).</p>

<h3 id="reset-your-firewall">Reset your Firewall</h3>

<p>We will start by resetting our firewall rules so that we can see how policies can be built from the command line.  You can flush all of your rules by typing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo service iptables-persistent flush
</li></ul></code></pre>
<p>You can verify that your rules are reset by typing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -S
</li></ul></code></pre>
<p>You should see that the rules in the <code>filter</code> table are gone and that the default policy is set to <code>ACCEPT</code> on all chains:</p>
<pre class="code-pre "><code langs=""><div class="secondary-code-label " title="output">output</div>-P INPUT ACCEPT
-P FORWARD ACCEPT
-P OUTPUT ACCEPT
</code></pre>
<h3 id="create-protocol-specific-chains">Create Protocol-Specific Chains</h3>

<p>We will start by creating all of our protocol-specific chains.  These will be used to hold the rules that create exceptions to our deny policy for services we want to expose.  We will create one for <code>UDP</code> traffic, one for <code>TCP</code>, and one for <code>ICMP</code>:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -N UDP
</li><li class="line" prefix="$">sudo iptables -N TCP
</li><li class="line" prefix="$">sudo iptables -N ICMP
</li></ul></code></pre>
<p>We can go right ahead and add the exception for SSH traffic.  SSH uses TCP, so we will add a rule to accept TCP traffic destined for port 22 to the TCP chain:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A TCP -p tcp --dport 22 -j ACCEPT
</li></ul></code></pre>
<p>If we wanted to add additional TCP services, we could do that now by repeating the command with the port number replaced.</p>

<h3 id="create-general-purpose-accept-and-deny-rules">Create General Purpose Accept and Deny Rules</h3>

<p>In the <code>INPUT</code> chain, where all incoming traffic begins filtering, we need to add our general purpose rules.  These are some common sense rules that set the baseline for our firewall by accepting traffic that's low risk (local traffic and traffic that's associated with connections we've already checked) and dropping traffic that is clearly not useful (invalid packets).</p>

<p>First, we will create an exception to accept all traffic that is part of an established connection or is related to an established connection:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -m conntrack --ctstate ESTABLISHED,RELATED -j ACCEPT
</li></ul></code></pre>
<p>This rule uses the <code>conntrack</code> extension, which provides internal tracking so that <code>iptables</code> has the context it needs to evaluate packets as part of larger connections instead of as a stream of discrete, unrelated packets.  TCP is a connection-based protocol, so an established connection is fairly well-defined.  For UDP and other connectionless protocols, established connections refer to traffic that has seen a response (the source of the original packet will the destination of the response packet, and vice versa).  A related connection refers to a new connection that has been initiated in association with an existing connection.  The classic example here is an FTP data transfer connection, which would be related to the FTP control connection that has already been established.</p>

<p>We want to also allow all traffic originating on the local loopback interface.  This is traffic generated by the server and destined for the server.  It is used by services on the host to communicate with one another:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -i lo -j ACCEPT
</li></ul></code></pre>
<p>Finally, we want to deny all invalid packets.  Packets can be invalid for a number of reasons.  They may refer to connections that do not exist, they may be destined for interfaces, addresses, or ports that do not exist, or they may simply be malformed.  In any case, we will drop all invalid packets since there is no proper way to handle them and because they could represent malicious activity:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -m conntrack --ctstate INVALID -j DROP
</li></ul></code></pre>
<h3 id="creating-the-jump-rules-to-the-protocol-specific-chains">Creating the Jump Rules to the Protocol-Specific Chains</h3>

<p>So far, we have created some general rules in the <code>INPUT</code> chain and some rules for specific acceptable services within our protocol-specific chains.  However, right now, traffic comes into the <code>INPUT</code> chain and has no way of reaching our protocol-specific chains.</p>

<p>We need to direct traffic in the <code>INPUT</code> chain into the appropriate protocol-specific chains.  We can match on protocol type to send it to the right chain.  We will also ensure that the packet represents a new connection (any established or related connections should already be handled earlier).  For TCP packets, we will add the additional requirement that the packet is a SYN packet, which is the only valid type to start a TCP connection:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -p udp -m conntrack --ctstate NEW -j UDP
</li><li class="line" prefix="$">sudo iptables -A INPUT -p tcp --syn -m conntrack --ctstate NEW -j TCP
</li><li class="line" prefix="$">sudo iptables -A INPUT -p icmp -m conntrack --ctstate NEW -j ICMP
</li></ul></code></pre>
<h3 id="reject-all-remaining-traffic">Reject All Remaining Traffic</h3>

<p>If a packet that was passed to a protocol-specific chain did not match any of the rules within, control will be passed back to the <code>INPUT</code> chain.  Anything that reaches this point should not be allowed by our firewall.</p>

<p>We will deny the traffic using the <code>REJECT</code> target, which sends a response message to the client.  This allows us to specify the outbound messaging so that we can mimic the response that would be given if the client tried to send packets to a regular closed port.  The response is dependent on the protocol used by the client.</p>

<p>Attempting to reach a closed UDP port will result in an ICMP "port unreachable" message.  We can imitate this by typing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -p udp -j REJECT --reject-with icmp-port-unreachable
</li></ul></code></pre>
<p>Attempting to establish a TCP connection on a closed port results in a TCP RST response:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -p tcp -j REJECT --reject-with tcp-reset
</li></ul></code></pre>
<p>For all other packets, we can send an ICMP "protocol unreachable" message to indicate that the server doesn't respond to packets of that type:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -A INPUT -j REJECT --reject-with icmp-proto-unreachable
</li></ul></code></pre>
<h3 id="adjusting-default-policies">Adjusting Default Policies</h3>

<p>The last three rules we added should handle all remaining traffic in the <code>INPUT</code> chain.  However, we should set the default policy to <code>DROP</code> as a precaution.  We should also set this policy in the <code>FORWARD</code> chain if this server isn't configured as a router to other machines:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo iptables -P INPUT DROP
</li><li class="line" prefix="$">sudo iptables -P FORWARD DROP
</li></ul></code></pre>
<p></p><div class="code-label notes-and-warnings warning" title="Warning">Warning</div><span class="warning">
With your policy set to <code>DROP</code>, if you clear your <code>iptables</code> with <code>sudo iptables -F</code>, your current SSH connection will be dropped!  Flushing with <code>sudo iptables-persistent flush</code> is a better way to clear rules since it will reset the default policy as well.<br /></span>

<p>To match our IPv6 policy of dropping all traffic, we can use the following <code>ip6tables</code> commands:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo ip6tables -P INPUT DROP
</li><li class="line" prefix="$">sudo ip6tables -P FORWARD DROP
</li><li class="line" prefix="$">sudo ip6tables -P OUTPUT DROP
</li></ul></code></pre>
<p>This should replicate our rules set fairly closely.</p>

<h3 id="saving-iptables-rules">Saving IPTables Rules</h3>

<p>At this point, you should test your firewall rules and make sure they cover the block the traffic you want to keep out while not hindering your normal access.  Once you are satisfied that your rules are behaving correctly, you can save them so that they will be automatically be applied to your system at boot.</p>

<p>Save your current rules (both IPv4 and IPv6) by typing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo service iptables-persistent save
</li></ul></code></pre>
<p>This will overwrite your <code>/etc/iptables/rules.v4</code> and <code>/etc/iptables/rules.v6</code> files with the policies you crafted on the command line.</p>

