  Could not resolve 'security.ubuntu.com'

. . .
</code></pre>
<p>You should follow this section to get APT working again.</p>

<p>First, set your nameservers to outside nameservers. This example uses Google's nameservers. Open <code>/etc/network/interfaces</code> for editing:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo nano /etc/network/interfaces
</li></ul></code></pre>
<p>Update the <code>dns-nameservers</code> line as shown:</p>
