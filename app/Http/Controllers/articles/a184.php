iface eth0 inet6 static
        address 2604:A880:0800:0010:0000:0000:00B2:0001
        netmask 64
        gateway 2604:A880:0800:0010:0000:0000:0000:0001
        autoconf 0
        dns-nameservers <span class="highlight">8.8.8.8 8.8.4.4</span>
</code></pre>
<p>Refresh your network settings:</p>
<pre class="code-pre command"><code langs=""><ul class="prefixed"><li class="line" prefix="$">sudo ifdown eth0 && sudo ifup eth0
</li></ul></code></pre>
<p>The expected output is:</p>
<div class="code-label " title="Output">Output</div><pre class="code-pre "><code langs="">RTNETLINK answers: No such process
