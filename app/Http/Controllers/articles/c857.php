						<h1 class="entry-title">Arrays in C Language | Set 2 (Properties)</h1>
				
			
			<p>We have introduced arrays in set 1 (<a>Introduction to arrays in C</a>).<span id="more-16689"></span></p>
<p>In this post array properties in C are discussed.</p>
<p>1) In C, it is possible to have array of all types except void and functions.  See <a>this </a>for details.</p>
<p>2) In C, array and pointer are different.  They seem similar because array name gives address of first element and array elements are accessed using pointer arithmetic.  See <a>array vs pointer in C</a> for details. </p>
<p>3) Arrays are always passed as pointer to functions.  See <a>this </a>for details.</p>
<p>4) A character array initialized with double quoted string has last element as ‘\0′.  See <a>this </a>for details.</p>
<p>5) Like other variables, arrays can be allocated memory in any of the three segments, data, heap, and stack (See <a>this </a>for details).   Dynamically allocated arrays are allocated memory on heap, static or global arrays are allocated memory on data segment and local arrays are allocated memory on stack segment. </p>
