	<p>Apartment-sharing startup Airbnb is in talks for a new round of funding that would value the company at $30 billion, a source close to the company said on Tuesday.</p><p>Airbnb intends to use the financing to support new investments and growth opportunities, the source added.</p><p>The New York Times first reported the news on Tuesday.</p><p>Airbnb, which expects to achieve profitability in 2016, raised over $100 million in a round of funding late last year that valued the company at $25.5 billion.</p><p>The San Francisco-based startup, which operates in nearly 200 countries, also secured a $1 billion debt facility in June from some big U.S. banks to finance its expansion plans.</p><p>The rise of Airbnb and other such startups have increased the pressure on the hotel bookings business of established travel companies such as Priceline Group Inc. and Expedia Inc.</p><p>(Reporting by Anya George Tharakan in Bengaluru; Editing by Savio D'Souza)</p>

	
				
