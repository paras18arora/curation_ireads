	<p>If you fancy a quick snack every now and then or refer a good old newspaper in the morning, you may have turned to <a>Gateway Newstands</a> for these needs.</p><p>Michael Aychental and David Goldman founded the company in 1983 and began franchising in the same year. Still family owned and operated, the Canadian-based franchise operates in kiosks, stores and office buildings offering snacks, drinks, candy, cards, tobacco, lottery tickets, magazines, books and other items.</p><p>Today, Gateways has more than 300 stores in North America, with locations including Toronto, New York City and Chicago.</p><p>It is also ranked as No. 118 on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
