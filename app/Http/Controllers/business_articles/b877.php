	<blockquote><p>How do I negotiate salaries without my employees feeling stiffed?</p></blockquote><p>Salary negotiations can be anxiety-filled on both sides -- but they shouldn’t be, says Thomas Neubauer, a managing partner with <em>The Gap Partnership</em>, a firm that provides negotiation training and consulting services. It’s all a matter of communication.</p><h2></h2><p>A productive negotiation is based on mutual trust and respect, Neubauer says, so don’t think this is a conversation you have to win. “You want to work with them, ideally, for many years,” he says, “so you don’t want to upset them by being too competitive.”</p><h2></h2><p>Understand what’s most important to the employee -- because it may not be the base salary. More vacation days? A more flexible schedule? “There are different things that could create satisfaction,” Neubauer says. “That’s what you want to achieve.”</p><h2></h2><p>If you’re giving something, you ought to get something -- like your new employee agreeing to take on additional responsibilities. When you’re both happy, put everything into a written agreement. </p>

	
				
				
