	<h2 class="slides">6. Pooper scooper services</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629133527-6.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Courtesy of doggie do not!

					</div>
	
	<p></p>
<p></p>
<p>Sure, it doesn’t sound like the most glamorous job, but if you don’t want to do it, neither does anyone else. What does that mean? Business for <em>you</em>.</p>
<p>“We had a customer tell us that we saved their marriage because they used to fight over the dirty job of scooping,” says Ashlee Nestor, owner of <a>doggie doo not!</a> “We currently make about 1,400 stops a week scooping poop.”</p>
<p>The company’s website explains that <a>doggie doo not!</a> will come and scoop up feces in kennels, dog parks, office grounds, apartment complexes and yards -- or as they put it “anywhere a dog goes.”</p>
<p>The cost of services is based on the number of dogs, size of area to be cleaned and desired frequency, according to Nestor. "We invoice monthly with most customers choosing to be automatically charged on their credit card each month.”</p>
<p>The majority of your efforts will be spent on reaching customers, says Nestor, who says she started marketing the company by mailing flyers to dog owners in her town. Now that the business has grown, she uses yellow trucks with the brand's logo along with “word of mouth from happy customers.”</p>
		
	<h2 class="slides">7. Pet-matching expert</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629170523-GettyImages-166273107.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Tom Merton | Getty Images

					</div>
	
	<p></p>
<p></p>
<p>If you’re technology-minded but don’t want to sit behind a desk all day, consider creating your own pet-matching <a>app or website</a>.</p>
<p>What is a pet-matching app? This could be an app that helps people connect to shelters, find animal boarding, find animal products or other pet owners for pet playdates -- a one-stop-hub for pet owners or pet-minded people.</p>
<p><a>Bark & Co.</a>, the company that created <a>BarkBox</a>, a subscription service that sends dog owners treats and toys every month, has embraced that idea through its <a>BarkBudy app</a>. The app works a lot like Tinder and allows you to browse adoptable dogs in your area and swipe right or left, matching you with the dog breeds you search most; it also lets you “favorite” dog preferences.</p>
<p>The app pulls from databases of rescues around the United States and Canada and has over a quarter of a million adoptable dogs in its database.</p>
<p>“BarkBuddy comes from our desire to build products that foster the health and happiness of dogs everywhere. We believe that everyone should have a dog in their life, and every dog should have a human,” says Stacie Grissom, editor in chief at Bark & Co.</p>
<p>Although getting an app on the iTunes app store only <a>costs</a> $99 a year for a <a>developer’s license</a> and $5 for the Xcode development software, it can still be difficult to get Apple to approve the app. However, Android’s Google market is <a>free</a> -- and the guidelines are much less strict.</p>

