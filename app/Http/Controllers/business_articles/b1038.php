	<h2 class="slides">2. Pet sitter</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629170215-GettyImages-522796763.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Hero Images | Getty Images

					</div>
	
	<p></p>
<p></p>
<p></p>
<p>Starting a pet-sitting service requires little in startup costs, but you do need credentials, such as past or present pet ownership, as well as other pet-related experience, including working at a pet retail shop, an animal hospital or another animal-related work.</p>
<p>You’ll also need to be able to build the “trust” credential, sometimes referred to as "honesty insurance.” Owners often regard their pets as children, so bond with your client’s precious pet by offering a brief but important “meet-and-greet” with both the client and pet to get to know each other and go over your policies and ask questions about the pet’s needs.</p>
<p>Consider what services you’ll offer by researching the services that other pet sitters offer, such as overnight stays and/or private dog walks versus non-private services (such as walking multiple dogs at once); plus, come up with your own set of policies to protect your business, such as cancellation and payment policy and what sort of management system you’ll use to record your clients’ information and keep track of your pet-sitting schedule.</p>
<p>Some startup costs? Consider pet-sitter’s insurance (for liability coverage), which costs approximately $190-$290 annually through <a>Pet Sitters Associates</a> and <a>animal first aid class</a>, which runs $100. </p>
