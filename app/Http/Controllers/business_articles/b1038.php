	<p></p>
<p></p>
<p>Kitty couture. Designer hoodies for dogs. The entrepreneurial possibilities for pet clothing are vast.</p>
<p>“Coats and pajamas are a mainstay, as well as Halloween costumes and fun holiday wear,” says Madelena Perrelli, owner of <a>Diamond Collar</a>, a pet attire store in Brooklyn.</p>
<p>According to Perrelli, this business is beneficial for small animals in particular. “They need comfort from the cold in winter, and in the summer as well due to air conditioning,” she says, so there is a large market geared towards smaller animals.</p>
<p>And for ideas on how to cleverly market your pet threads, check out <a>Lucy & Co.</a>, an online store that specializes in signature dog clothing classics, such as puffer vests and hooded sweatshirts, which uses <a>Instagram</a>, <a>Facebook</a> and <a>Pinterest</a> to show off its adorable wares.</p>
		
	<h2 class="slides">5. Gourmet pet treats</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629170356-GettyImages-477445797.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			SageElyse | Getty Images

					</div>
	
	<p></p>
<p></p>
<p>Sick of going to the store for dog biscuits every week? Think cats deserve some unique treats too? Start a pet-bakery business! <a>Gourmet pet snacks are not only a unique business idea</a>, but they could also be your foot-in-the-door for other animal-related businesses.</p>
<p>There are many options with animal treats ranging from a focus on healthy, all-natural selection, to unique and crazy flavors. Just make sure to do your due diligence for what dogs can and can’t eat -- for example, did you know that dogs can get sick from eating onions, garlic or avocados?</p>
<p>“It took me 3 years before I opened to come up with recipes that are safe and that dogs would love,” shares Penny Milligan, owner of pet bakery <a>The Hungry Hound</a>.</p>
<p>Karry Barolo, owner of <a>D.O.G. Bakery</a>, a dog bakery that is carried in nearly 100 stores nationwide, creates healthy, natural dog treats. However, when she started her business, people dismissed it as silly.</p>
<p>“Once I was able to educate them on the importance of great nutrition for dogs, they started to see the value of what we do,” Barolo explains.</p>
<p>Neither Milligan or Barolo had baking experience before starting their businesses, however Milligan had worked in retail for most of her adult life and Barolo worked in restaurant management.</p>
<p>“As a small business owner you have to be able to do everything. So I would strongly recommend exposing yourself to all aspects of business," says Barolo.</p>
