	<p></p>
<p></p>
<p>Do you have experience with a digital SLR camera? Love hanging out with animals? A pet photographer might be the business for you.</p>
<p>“The most important thing in establishing such a business is to create a memorable, identifiable style of pet photography that appeals to dog lovers,” says Mark McQueen, owner of <a>phoDOGraphy</a>, a New York City-based business that specializes in dog photography with a city feel. He adds that being a pet photographer requires a lot of patience.</p>
<p>Some ideas for how you can cut down on costs? Take photos of your subjects outside of a studio, which can be a fun way to catch them in a unique environment with natural light but also presents some challenges in a less-controlled environment. Or use the owner's home as a setting. Another way to save is to rent camera equipment until you have the funds to purchase.</p>

