	<h2 class="slides">3. Gift basket service for pets</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629170309-GettyImages-182243892.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			skhoward | Getty Images

					</div>
	
	<p></p>
<p></p>
<p>Finding a niche to corner is the best way to start out in the <a>gift basket business</a>. And if you love animals, why not create a gift basket for pets?</p>
<p>Will you focus on homemade tasty treats for brachycephalic dogs (that have flat faces, such as French bulldogs and pugs) that have sensitive stomachs? Or a starter puppy kit, full of safe toys and snacks -- and a booklet containing 10 important training tips?</p>
<p>Will your basket be filled with goodies for dog grooming?</p>
<p>One company that has capitalized on this idea is Bark & Co. through its <a>BarkBox</a> product. The BarkBox is a monthly subscription service that sends dog treats and toys to pet owners every month.</p>
<p>“One of the reasons so many startups in this industry fail is that they act like humans and not as dogs,” Henrik Werdelin, co-founder at Bark & Co. said. “We have from day one built BarkBox with dogs values like contagious optimism. We are always trying to please and not be too serious.”</p>
<p>Try cutting down on costs by marketing through social media. BarkBox uses its customers on social media to market their brand. “Try asking on Twitter 'should I get a BarkBox?' and you will find that our amazing customers do the branding for us,” Werdelin says.</p>

