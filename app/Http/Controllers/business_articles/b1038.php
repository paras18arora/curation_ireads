	<p><em>I wish I could just play with animals all day.</em></p>
<p>If you’re an animal lover you know you’ve said that once or twice before. What if that dream could become your next business?</p>
<p>“With so many people having dogs instead of children, products and services that help people care for their four-legged family members are in a growth pattern that I don’t see slowing down for the near future,” says Matthew Osborn, doo-doo guru and owner of <a>Pooper Scooper Services</a>.</p>
<p>Since <a>1994</a>, the pet industry has nearly quadrupled in sales. In 2015, Americans spent over <a>$60 billion</a> on their pets, according to American Pet Products, a nonprofit trade association serving the pet industry. It’s estimated that 2016 will see an increase of $3 billion in sales.</p>
<p>If you’re an animal lover looking to go into your own business, these are 9 low-cost business ideas that can start you on your way. The<em> paw-ssibilities</em> are endless!</p>
