	<p>The success of McDonald’s all-day breakfast menu has proven America’s insatiable love for morning fare. And now Taco Bell is jumping on the bandwagon.</p><p>The franchise is introducing a 10-item $1 breakfast menu. The offering will be available Thursday nationwide and served from 7 a.m. to 11 a.m., though some stores may offer the menu as early as 6 a.m.</p><p>The fast-food chain created three new items for the menu: a skillet of seasoned potatoes, scrambled eggs, cheese and salsa; a soft taco with scrambled eggs, cheddar cheese and a choice of bacon or sausage; and a quesadilla with cheese, sausage and scrambled eggs.</p><p>The Yum! Brands subsidiary is also offering a variety of burritos and tacos as well as mini cinnamon buns for a dollar. The menu represents an expansion of the fast-food chain’s focus on low-prices. Taco Bell revealed a “Dollar Cravings” menu nationally in 2014, which consists of lunch and dinner items.</p><p>“Our survey results were completely expected: Americans would rather pay less and have more choice,” said Taco Bell CMO Marisa Thalberg in a statement.</p><p>Taco Bell’s breakfast offerings represent just about 6 percent of total sales now, in contrast to McDonald’s, where breakfast represents about 25 percent of sales, according to the <em><a>Wall Street Journal</a></em>.</p><p>Taco Bell’s new value menu is an attempt to differentiate itself in a fast food industry that is starting to pull away from the traditional dollar menu for bundled items. Wendy’s replaced its dollar menu for one with <a>prices in the range of $1 to $5</a> in 2015, <em>Time</em> reported.</p><p>While McDonald’s introduced the all-day breakfast menu last year, it also gradually phased out its own dollar menu, replacing it for the McPick 2, <a>which allows customers to choose two items for $2</a>.</p><p>McDonald’s is now <a>expanding its breakfast offering</a> after the promotion drove up the Golden Arches’ stock prices and gave the <a>company its best quarter in nearly four years</a>.</p>

	
				
