	<p>It's one thing to get a package delivered to your door by drone, but how would you like to <em>be</em> the delivery?</p><p>The<a> Ehang 184</a> is a single-passenger pilotless drone made by EHang, Inc., a 2-year-old electronics startup based in Guangzhou, China. The vehicle made its debut in January at CES 2016 in Las Vegas, and was given the OK by the Nevada Governor's Office of Economic Development (GOED) to start testing in the state this year.</p><p></p><p>The company doesn't have FAA approval just yet, but EHang's partnership with the governor's office and the Nevada Institute for Autonomous Systems (NIAS) has made it possible for the vehicle to be flown at the Nevada UAS (unmanned aerial systems) test site.</p><p>“EHang’s selection of Nevada to test its people-carrying drone marks a thrilling addition to the innovative companies testing throughout our state to advance the commercial drone industry. I personally look forward to the day when drone taxis are part of Nevada’s transportation system," GOED’s Aerospace and Defense Industry Specialist Tom Wilczek said in a<a> statement</a> about the agreement.</p><p></p><p>The Ehang 184 looks a little like the nose of a helicopter, but instead of wings on the top, it has four legs with two propellers each. The vehicle runs on a battery and weighs 440 pounds, and when it gets into the air at about 11,500 feet, it can fly for roughly 23 minutes at a little over 60 mph.</p><p>So who knows? If all the regulatory and engineering stars align, maybe you could actually hail a drone taxi one day.</p>

	
				
				
