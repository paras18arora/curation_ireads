	<p>In a 1993 interview for <a><em>The Wall Street Journal,</em></a><em> </em>Jobs said, "Being the richest man in the cemetery doesn't matter to me. ... Going to bed at night saying we've done something wonderful -- that's what matters to me."</p>
<p>Jobs lived a modest life, considering his multibillion-dollar worth. According to <em>S</em><em>teve Jobs: The Man in the Machine</em>, when Jobs became a successful CEO he didn’t go for the Silicon Valley mega-mansion and collection of cars. Instead, he leased a silver Mercedes Benz and lived in an understated and lightly furnished 5,700-square-foot home in Palo Alto, Calif. with his wife and three children, according to <a><em>ABC News</em></a>.  </p>
<p>His one real splurge was his private Gulfstream V airplane (which cost $90 million) that he asked Apple to give him as an executive bonus in 2001.</p>
				
				
