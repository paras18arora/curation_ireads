	<p>There were several influential people who were mentors in Jobs' life -- one of them was the Zen master Otogawa -- and it only seemed natural for Jobs to eventually become a mentor himself.</p>
<p>One of his more famous mentees was Facebook CEO and co-founder Mark Zuckerberg, who during a town-hall style meeting last year with India Prime Minister Narendra Modi, recounted a story about Jobs’ role in guiding him during an uncertain time at Facebook.</p>
<p>"Early on in our history when things weren't really going well -- we had hit a tough patch and a lot of people wanted to buy Facebook -- I went and I met with Steve Jobs, and he said that to reconnect with what I believed was the mission of the company, I should go visit this temple in India that he had gone to early in the evolution of Apple, when he was thinking about what he wanted his vision of the future to be,” <a><em>Business Insider</em></a> reports Zuckerberg saying.</p>
<p>Jobs had a special connection to a particular temple in India that he visited after he dropped out from college and recommended that Zuckerberg visit -- which he did. The Facebook founder spent a month traveling the country and said that “seeing the way that people connected made him feel that the world would be much better if everyone had a stronger ability to connect,” which reinforced his vision. Subsequently,</p>
<p>Zuckerberg rejected the offers to purchase his company and survived the tough times to grow Facebook into one of the largest tech companies in the world. </p>
