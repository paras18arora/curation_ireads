	<p>Jobs had big ideas -- and some of them belly-flopped. One of his most epic failures was the <a>Lisa</a>, a personal computer for Apple -- referred to as an “early Mac” -- released in 1983 that used graphic icons instead of text and came with a price tag of $10,000.</p>
<p>Jobs had lead the team on the Lisa -- until he was forced off of it and joined the Macintosh team. Being his typical combative self, Jobs poached engineering talent from the other team and contributed to the Lisa’s demise by giving multiple interviews to publications talking up the Macintosh and touting it as a less expensive version of the Lisa.</p>
<p>The Lisa only sold 10,000 units and was discontinued a year later. After disappointing sales with the Macintosh, Jobs was pushed out of Apple in 1983.</p>
