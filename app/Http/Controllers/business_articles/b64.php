	<p>The young Jobs had <a>seriously considered becoming a monk</a>, according to Alex Gibney’s 2015 documentary<em> </em><a><em>Steve Jobs: The Man in the Machine</em></a>. During his lifetime, Jobs was often compared to a monk with his single-minded dedication to Apple and his restrictive vegetarian diet -- sometimes fasting for periods -- and austere choice of wardrobe.</p>
<p>In the film, a very tortured teenager, Jobs had befriended a Zen monk from Japan by the name of Kobun Otogawa and showed up at his doorstep late one night to tell Otogawa that he had become “enlightened,” and he didn’t know what to do. The monk’s response was that he needed proof of this enlightenment. Jobs returned to Otogawa a week later with “a little metal sheet” -- a computer chip he said that both he and Steve Wozniak had built.</p>
<p>Jobs studied with the monk throughout the 1970s, repeatedly asking Otogawa to make him a monk. Otogawa refused, saying that Jobs’ work was elsewhere. However, their relationship eventually led Otogawa to becoming Apple’s corporate spiritual advisor, and he presided over Jobs’ marriage ceremony in the Ahwahnee Hotel in Yosemite National Park. In 2002, Otogawa drowned while trying to save his five-year-old daughter. Jobs was <a>devastated</a>.</p>
		
	<h2 class="slides">2. Jobs failed -- big time.</h2>
