	<h2 class="slides">3. Jobs lead a tech company -- but he wasn’t a technical person.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160330193657-3.jpeg" />
			</div>
			<div class="credit">
