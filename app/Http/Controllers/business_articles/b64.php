							</a>
					</div>
	
	<p>For a man who founded a computer company, Jobs lacked the sophisticated engineering know-how that someone such as Bill Gates or Mark Zuckerberg possessed.</p>
<p>According to Apple co-founder Wozniak in an interview with <a><em>I Programmer</em></a>:</p>
<p>“Steve Jobs played no role at all in any of my designs of the Apple I and Apple II computer and printer interfaces and floppy disks and stuff that I made to enhance the computers. He did not know technology. He never designed anything as a hardware engineer, and he didn’t know software. He wanted to be important, and the important people are always the business people. So that’s what he wanted to do.”</p>
<p>So, what did he bring to the table? Jobs had a wonderful eye for design and had the gift of vision mixed in with a searingly intense personality. He knew what he wanted to accomplish and adopting (some say stealing) great ideas along the way.</p>
<p>However, Jobs knew he could not accomplish anything alone and had a great knack at recruiting top-notch people and getting them to do anything to get the job done -- even if it was a detriment to their personal life, according to former Apple employees’ testimony from <a><em>Steve Jobs: The Man in the Machine</em></a>.</p>
<p>Whether Jobs had programming skills or not, Wozniak admitted in the same interview in <a><em>I Programmer</em></a> that Apple would not have done as well without Jobs.</p>

