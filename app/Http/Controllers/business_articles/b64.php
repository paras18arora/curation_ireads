	<p><a>Steve Jobs</a> wasn’t regarded a philanthropist. He ended the Apple’s corporate philanthropy program shortly after he returned to the floundering company in 1997. The late-Apple CEO, who grew up in Silicon Valley, was worth $8.3 billion in Apple stock at the time of his death in 2011, making him the 34th richest man in the United States at this time of this death. However, as of that year, there is no public tax record of Jobs donating directly to charity, according to <a><em>The New York Times</em></a>.</p>
<p>His reason for cutting the corporate philanthropy programs when he returned to helm Apple in 1997 was at least partly due to the fact that the company was in <a>dire financial trouble</a> reporting hundreds of millions in losses and $1 billion in unsold products.</p>
<p>But just because Jobs wasn’t on any public giving lists, doesn’t mean he wasn’t generously giving, said Adriene Davis, manager of public affairs at Indiana University’s Center on Philanthropy, who tracks such gifts, to <a><em>The</em> <em>Washington Post</em></a>.</p>
<p>Leander Kahney, the author of the unauthorized biography <a><em>Inside Steve’s Brain</em></a>, also supported that theory, writing, "For a person as private as Jobs, who shuns any publicity about his family life, [giving enormous sums of money to charity anonymously] seems credible.”</p>
<p>We can speculate what he might have done, but here’s what’s on record. In 2013, Jobs’ successor at  Apple, <a>Tim Cook</a>, publicly stated that his late boss gave $50 million to Stanford hospitals, which went towards building a children's medical center and a new main building.</p>
<p>In addition, U2 lead singer Bono, who’d friended Jobs during a collaboration between Bono’s AIDS/HIV nonprofit <a>Product Red</a> and Apple, wrote an <a>op-ed in <em>The New York Times</em></a> in 2011 praising Jobs' contributions to the fight against AIDS in Africa. In 2014, <a><em>AdWeek</em> reported</a> that Bono went on record to state Apple had quietly helped raise $75 million for a nonprofit that Jobs had a primary role in supporting.</p>
<p>Even Jobs himself admitted that what he gives will not necessarily be on public display. In a 1985 <a><em>Playboy</em> interview</a> he was asked what he’d like to do with his wealth, the then 30-year-old Jobs said, "That’s a part of my life that I like to keep private. When I have some time, I’m going to start a public foundation. I do some things privately now."</p>
<p>It’s entirely plausible that Jobs had simply continued his policy of doing certain things privately throughout his life. He died at the age of 56 in 2011, and his widow, Laurene Powell Jobs, who sits on many boards for education and health-related initiatives, has embodied privacy as well in her philanthropic work, according to <em><a>The New York Times.</a></em></p>
<p>In 2013, she founded the <a>Emerson Collective LLC</a>, a charitable organization which is structured like a small business and allows the organization to make anonymous donations to various causes, such as <a>College Track</a>, the college-preparatory organization for the underserved that she co-founded in 1997, and the <a>NewSchools Ventures Fund</a>, an education nonprofit the Jobs’ family has donated millions to, <a>according to its CEO Ted Mitchell</a>.</p>
<p>It’s impossible to say whether Jobs would have grown into someone who focused as fiercely on his philanthropic projects as he did on his business. However, one can confidently surmise that if he had, he wouldn’t have done it half way. Love him, judge him or hate him, Jobs was a true radical and a visionary.</p>
<p>Check out five more reasons that made the Silicon Valley entrepreneur truly one of a kind.</p>
