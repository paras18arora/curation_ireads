<p>Carolina Rustica is no stranger to virtual commerce. The Concord, N.C.-based retailer specializing in handcrafted iron and wood furniture went online in 2000. Between 85 percent and 90 percent of current sales originate via the web, says founder and president Richard Sexton, with the average order exceeding $1,000.</p>
<p>Now Carolina Rustica is moving its showroom to the smartphone -- and though dining room tables and four-poster beds may seem at odds with the digital impulse purchases typically synonymous with mobile commerce, Sexton says that keeping up with the competition makes mobile a necessary evolution.</p>
<p>"We're going up against the big outfits, like [home décor powerhouse] CSN Stores--they're not an 800-pound gorilla but a 2,000-pound gorilla, so it's important to keep pace with what they're doing," he says. "You can't exclude mobile commerce. People are doing everything on their smartphones, including shopping."</p>
