<p>Amazon.com can vouch for mobile commerce's potential: In July, the company said its customers spent more than $1 billion using their phones during the previous year. But Amazon is Amazon. Hopping on the m-commerce bandwagon is exponentially more challenging for smaller retailers.</p>
<p>"It's very difficult to launch a mobile presence," says mShopper founder and CEO David Gould, citing the lack of uniform mobile web standards and the broad array of devices. "You've got to rethink your entire page layout for mobile because of the tiny screens. The context is also a challenge--you're trying to reach a user who's on the go and whose needs are different."</p>
<p>Carolina Rustica launched its fledgling mobile website in partnership with Boulder, Colo.-based mShopper, which in September introduced Mobile Commerce Platform. With it, merchants of all sizes can build customized smartphone storefronts, or "mStores." Included are design tools, built-in merchandising mechanisms and SEO resources, as well as an analytics dashboard to measure traffic and sales. Every order generated by a client's mStore is input directly into the retailer's existing shopping cart, ensuring that standard order and customer service processes still apply.</p>
<p>Retailers simply upload their product data feed and design their mStore, guided by mShopper's videos. Once it's live, merchants also can leverage mShopper's GetFirstDibs mobile marketing program, which transmits text-based discounts and promotions to customers on the retailer's mobile mailing list.</p>
<p>"We're here to help generate buzz and awareness and to drive traffic to your homepage," Gould says. "We only make money when merchants do."</p>
<p><a>mShopper</a> charges retailers a monthly licensing fee ranging from $99 to $999, depending on website traffic. The firm collects a commission of at least 5 percent of the sale, or the store's published standard affiliate rates.</p>
<p>Along with Carolina Rustica, more than 10 other retailers signed on with mShopper during Mobile Commerce Platform's first few weeks online. And mShopper expects to have 50 to 100 paying customers by the end of the year.</p>
<p>"The obvious question is why you should create an mStore when customers can browse your website on a smartphone," Sexton says. "The answer is that people need to start seeing your products right away. You can't worry about download times or screen sizes. You can't keep your website like it is and hope it all resolves on a phone."</p>

	
				
				
