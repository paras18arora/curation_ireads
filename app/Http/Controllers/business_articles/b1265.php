	<p>Sometimes all it takes is a good idea and a bit of faith.</p><p>Take, for example, the founder of Edible Arrangements.  At age 17, Tariq Farid’s parents loaned him $5,000 to start a floral business in Connecticut. Within two years, he was running four flower shops.</p><p>Seven years later, Farid got another idea: arrangements you can eat. After opening the first Edible Arrangements store in 1999 he continued to create, sell and deliver bouquets of fresh fruit that looked like flower arrangements. The endeavor later turned into a franchise in 2001.</p>The business’s prior successes, in addition to its expansion into other products, including chocolate dipped fruit, fruit salads and fruit sundaes,led to Edible Arrangements’ ranking as<a> no. 35 in Entrepreneur’s Franchise 500 list.</a>

	
				
				
