	<p>There's no doubt that 2009 saw many small businesses entrench themselves in survival mode. But as business owners start to emerge from their chrysalides, many of them have some financial breathing room and are flitting ahead withnew investments.</p>
<p>Tops on many priority lists? Tech--in particular, capital investments in new computer hardware to replace equipment that has been in service far longer than initially intended. A survey of 1,250 small- to medium-size businesses conducted by IT company Spiceworks this spring found a big lean toward new tech investment.</p>
<ul>
<li>&#7;Small- to medium-size businesses will increase their IT spending by about 9 percent this year, compared with a 1 percent decrease in 2009.</li>
<li>&#7;As much as 47 percent of IT budgets will be dedicated to new hardware.</li>
<li>The number of desktops and servers more than 4 years old increased by 14 percent in the last six months of 2009.</li>
<li>&#7;The number of laptops older than 4 years increased by 22 percent.</li>
<li>&#7;Of the businesses surveyed, 80 percent said they will buy some type of new hardware in 2010.</li>
</ul>

	
				
				
