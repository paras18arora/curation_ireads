	<p>Amid chatter about funding winter, venture capital fund SAIF Partners has been in the limelight for placing its bet on startups belonging to various categories. From Cleartax, FarEye, YourDost and now mobile crowdsourcing platform – Playment.</p><p>Bengaluru-based Playment helps large companies to crowdsource, completion of process oriented tasks that are typically done by in-house or outsourced teams (BPOs). The startup will use the funds raised to lay the foundation of an engineering and product team, along with scaling up deployments in all clients.</p><p>Formed in August 2015, Playment was launched in November 2015 by IIT-Kharagpur alumni Siddharth Mall & Akshay Lal, along with IIT-Guwahati alumnus Ajinkya Malasane.</p><p>Playment is a two sided marketplace that helps large companies bring cost-effectiveness and speed to their operations, and on the other side helps smartphone users supplement their income on the go.</p><p> “We strongly believe in the power of the sharing economy. The world has seen how the likes of Airbnb & Uber have created a new source of income and we want to be right up there with them,” Siddharth Mall, CEO, Playment said.</p><p>“Playment’s platform we believe has massive market opportunity . Moreover, we feel it’s the best team for the job as the co-founders have strong technical backgrounds and first hand knowledge of the space via their Flipkart experience,” Mayank Khanduja, Principal, SAIF Partners said.</p><p> </p>

	
				
				
