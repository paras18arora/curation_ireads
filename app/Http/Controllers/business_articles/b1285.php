	<p>In the near future when you answer the door to get your delivery Domino’s pizza, your run-of-the-mill delivery person could be replaced by a robot.</p><p>A robot that delivers pizza? Oh yes, Domino’s went there. In Australia, at least.</p><p></p><p>Named DRU, or Domino’s Robotic Unit, droid has been in the making for months as the company worked with Australian startup, Marathon Robotics, to turn a military robot into a pizza delivering machine, according to <a>The Guardian. </a></p><p>The contraption has its own sensory system, complete with lasers, to help it maneuver without assistance during its pizza-delivery journey.  Designed to use foot- or bike-paths and trails, the four-wheeled robot can move up to 20 km/h.</p><p></p><p>To retrieve the pizza itself and prevent pie theft, customers have to use their phone to enter a security code that opens a locked compartment within the robot.</p><p>However, since Domino’s Australia just unveiled the device in <a>Brisbane</a> Thursday, it’ll probably still be a while before you see DRU making its rounds stateside.</p> <div> </div>

	
				
				
