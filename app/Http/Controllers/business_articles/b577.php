	<p>CRM solution provider Helpshift closed a $23 Million Series B with participation from new investors, Microsoft Ventures, Salesforce Ventures and all previously existing investors, including Intel Capital, Nexus Venture Partners, True Ventures and Visionnaire Ventures.</p><p>The fresh round of funds brings the company’s total capital raised to $36.2 million, Helpshift said in a statement.</p><p>Consumers are tired of waiting for support agents to get back to them, and companies are tired of staffing expensive support teams to answer common, or even predictable, problems in the first place. People want immediate help, wherever they are, especially when using mobile applications,” said Abinash Tripathy, CEO and co-founder of Helpshift.</p><p>Helpshift is a standard fast paced SaaS Tech company that provides customer support platform for retaining and engaging mobile users. Industries that have most quickly adopted Helpshift include brands focused on ecommerce, gaming, lifestyle and productivity.</p><p>Helpshift, which mainly targets <a>international markets</a>, has 7 percent of its clientele in India. About 45 percent of its business comes from the U.S. Its clientele includes Zynga, VirginMedia, Microsoft, WesternUnion, Flipboard, Shyp, Luxe, WordPress and thousands of other industry leading brands, startups, and developers.</p><p> </p>

	
				
				
