	<p><em>This piece was originally published on May 26, 2016. </em></p>
<p>In so many ways, <a>Bill Gates</a> did his most significant work outside of <a>Microsoft</a>, the company he co-founded with childhood friend Paul Allen in 1975.</p>
<p>In his 20s, Gates saw the future in copyrighting software before the majority of the tech industry. When <a>IBM</a> came to him to ask if he had an operating system for its first line of personal computers, he said yes. Then, he purchased an operating system from small software outfit in Seattle, modifying the software program and called it MS-DOS. He licensed it to IBM for $50,000, retaining the copyright.</p>
<p>As PCs became ever-popular, MS-DOS became the reigning operating system. What followed were more benchmarks in innovation. Gates went on to announce the revolutionary Windows in 1985, which became the most widely used operating system; by 1987, the year after Microsoft went public, the 31-year-old Gates became the world’s youngest billionaire at that time.</p>
<p>Two years later, he founded <a>Corbis</a>, the largest visual archive of art and photography, and in 1995, when the Internet was still budding, Microsoft released the browser <a>Internet Explorer</a>. The same year, Gates became the world’s richest man.</p>
<p>However, it wasn’t until after his mother Mary Maxwell Gates passed away in 1994 that Gates developed a visible stake outside of Microsoft, which was largely due to his mother. Mary was a formidable woman in her own right who served on several boards, including First Interstate Bank in Seattle (which was founded by her grandfather), as well as for the United Way national board where she served as its first female chair.</p>
<p>Close as they were, Mary and Gates butted heads often. At the age of 11, Gates seemed to gain precocious intellect overnight, his father recalled to <a><em>The</em> <em>Wall Street Journal</em></a>. The young Gates pushed back against his mother’s rules and expectations, and their fights got explosive. Ultimately, she took him to see a therapist, whom <a>Gates informed</a>, “I'm at war with my parents over who is in control."</p>
<p>The therapist, in turn, counseled Mary and her husband to ease up. Ease up they did, enrolling him in a private school at the age of 13 where he’d have more academic freedom to pursue his interests -- and where he discovered his love for computers.</p>
<p>However, his mother never stopped offering counsel and guidance. Years later, when Gates took Microsoft public and became a billionaire, a <a>Microsoft employee recounts</a> how the two quarreled after Mary pressured her son to use his wealth for philanthropy.</p>
<p>Gates <a>responded</a> by yelling, “I’m trying to run my company!"</p>
<p>However, the tech billionaire was convinced to create a fundraising arm at Microsoft and donate to his mother’s preferred charity: The United Way. Eventually, he joined its board. But, it was a letter that his mother gave his bride-to-be at the time in 1994 that lead to his work in philanthropy on a purposeful scale.</p>
<p>Her letter read: "From those to whom much is given, much is expected.”</p>
<p>Six months later, she died. Gates never forgot her words. He tasked his father, Bill Gates Sr., with $100 million to start the William H. Gates Foundation in 1994 to give grants to worthy causes. It eventually became part of the Bill & Melinda Gates Foundation (founded in 2000).</p>
<p>In 2011, Gates and his wife, along with his good friend Warren Buffett, devised <a>The Giving Pledge</a>, a campaign to convince the world’s wealthiest to give away the majority of their fortunes during their lifetimes.</p>
<p>From advocating for and funding Common Core initiatives in education to providing vaccines to combating infectious disease in the world’s poorest communities -- and more recently, focusing $80 million on gender gap research -- the Seattle titan <a>worth $87.4 billion</a> is one of the true radicals and visionaries of our time.</p>
<p>He sets a high bar for the rest of his ilk -- both and his wife have given away more than $29 billion so far.</p>
<p>Here are five more lessons we can learn from his remarkable life.</p>

