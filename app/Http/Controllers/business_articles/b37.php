	<p>He lost his mother in 1994, the same year he married Melinda French, a former Microsoft executive with a strong mind of her own.</p>
<p>Melinda’s influence is seen in their shared work at The Bill & Melinda Gates Foundation, where she’s focused on women and children in underdeveloped countries. As a Roman Catholic, she’s been a recurring focus of controversy over her choice to fund birth control for women in developing countries in Africa.</p>
<p>During a <a>TED Talks interview</a>, she said that her decision to support birth control in spite of religious pushback was bolstered by her conversations with women in the rural countryside of Africa, where birth control meant the ability to space out the births of children, so the kids have a chance to be fed and educated.</p><a></a><br />
 
