	<p>While he was a student at Harvard, he read about a new computer, the MITS Altair 8800, in <em>Popular Mechanics </em>and called the company to tell them he wrote a programming language for it -- which he hadn’t.</p>
<p>However, that didn’t stop him. He took a leave of absence from Harvard and recruited his childhood computer buddy, Allen, to work on the programming language, which became BASIC.</p>
<p>The two ended up selling it to MITS for $3,000, retaining the copyright license.</p>

