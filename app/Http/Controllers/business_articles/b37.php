	<p>Gates has a reputation of being a tough boss, but he was tough on himself, too. During the first five years of Microsoft, he was not only in charge of running the business, but he also oversaw product development, taking it upon himself to rewrite code if need be.</p>
<p>As a younger man, he also frequently pulled all-nighters. According to <a><em>Business Insider</em></a>, one time, an employee came into work and found a man sleeping on the floor. She was going to call the police, but then realized the man was Bill Gates.</p>

