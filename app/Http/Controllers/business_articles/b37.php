	<p>Curiosity and learning are part of Gates’ DNA. The busy billionaire frequently recommends books on his blog <a>Gates Notes</a>, and there is an entire section dedicated to book reviews, from beach reading to nonfiction reads. (<em>Entrepreneur</em> just covered <a>his most recent book</a> recommendations.) </p>
<p>As a young boy, “he read the World Book Encyclopedia series start to finish,” reported <a><em>The</em> <em>Wall Street Journal</em></a>. “His parents encouraged his appetite for reading by paying for any book he wanted.”</p>
<p>He also <a>attributed his ability to dream big</a> to his early opportunities in reading:  </p>
<p>“I really had a lot of dreams when I was a kid, and I think a great deal of that grew out of the fact that I had a chance to read a lot.”</p>
<p>He’s an author, too, and has written two <em>New York Times </em>best-sellers, <a><em>The Road Ahead</em></a> and<em> </em><a><em>Business @ the Speed of Thought</em></a><em>.</em></p>
<p> </p>
		
	<h2 class="slides">2. Marry a strong partner who will challenge you.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160519192213-2-bill-melinda-gates.jpeg" />
