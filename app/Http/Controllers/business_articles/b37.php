	<p>Gates is one of the founding members of The Giving Pledge, which he came up with in 2011. So far he’s donated over <a>$29 billion of his fortune</a> to the Bill & Melinda Gates Foundation.</p>
<p>As for his three kids, they won’t be paupers and stand to inherit a “minuscule portion of his wealth.</p>
<p>However, Gates shared with the <a><em>Daily Mail</em></a> that his three children are going to be given “an unbelievable education and that will all be paid for,” but like the rest of us, they’ll have to pick a job and go to work.</p>
<p></p>

