	<p>Known for its catchy jingle, “Nobody cares for eyes more than Pearle,” optical retailer Pearle Vision has had its “eye” on the franchise game for quite some time.</p><p>Around for more than 50 years, Dr. Stanley Pearle got the idea for a one-stop eye-shop offering complete eye care in Savannah, Georgia, where the first store opened.</p><p>The specialist was one of the first to combine eye exams with large collections of eyewear for customers to choose from. The concept lent itself to a franchise model in 1981, as it worked to include both doctors and opticians.</p>Now owned by Luxottica, the company landed as <a>No. 115 on Entrepreneur’s Franchise 500 list. </a>

	
				
				
