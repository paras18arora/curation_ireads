<p>For every bottle sold, VeeV donates $1 to retailer Sambazon's Sustainable Açai Project, in San Clemente, Calif., which promotes sustainable harvesting practices of the Açai berry and ensures that Brazilian farmers are paid a living wage for their region, doubling the standard of the equivalent of 4 American dollars per day to $8. In addition, VeeV employs approximately 100 people in Brazil to make bracelets out of the berry seeds, which are used as VeeV promotional items and sold locally.</p>
<p>The bars and restaurants pouring VeeV wanted to be involved in the charitable component, too, Reum says. Thus was born the Treetini program. Bars and restaurants simply create their own versions of cocktails using VeeV, called Treetinis, and report to the company periodically how many were ordered. VeeV makes a donation to plant one tree in flood-ravaged Tamil Nadu, India, per Treetini served.</p>
<p>When VeeV--derived from the French word vivre, which means "to live"--launched, the Reums sold it out of the back of their car. Soon, it caught on in a few markets in Los Angeles and New York. Within two years, it was in 20 markets and, during the past year, distribution has gone nationwide and annual sales now surpass $2 million.</p>
<p>Reum, who calls himself a "New Age tree-hugger," says the brand's commitment to giving back at every level is a way for people to feel good about their cocktail of choice. "Sustainability is kind of like working out," he says. "It doesn't matter whether you do it for heath, vanity or some combination of other reasons. It all goes toward the greater good."</p>

	
				
				
