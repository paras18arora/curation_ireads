<p><br />
Courtney Reum ordered yet another vodka and soda and realized he was utterly bored with his drinking experience. The New York City investment banker had just helped complete the merger of two alcohol companies--Allied Domecq and Pernod Ricard--in 2005 and concluded during his due diligence research that the spirits space severely lacked innovation.</p>
<p>"The Bacardis of the world launch a new flavor of Bacardi," he says, "but they don't tend to do things that are really outside the box--both as a product in the bottle and then as a company."</p>
<p>During a surfing trip to Brazil in 2003, Reum learned about the burgeoning popularity of the Açai berry, which, the company says, has 57 percent more antioxidants than its fellow "super foods," pomegranates and blueberries. So Reum and his brother, Carter, came up with creating "a better way to drink" which ultimately became the tag line for <a>VeeV</a>, the Açai-flavored spirit they launched in 2007. Though quick to say the company makes no claims that VeeV is a health beverage, Reum adds, "We like to think of it as the red wine equivalent of hard alcohol."</p>

<p>The Reums believe in sustainability and charity, so for their U.S. operations, they chose a distillery in eastern Idaho that uses wind energy, decreasing the electricity needed to distill the spirit.</p>
