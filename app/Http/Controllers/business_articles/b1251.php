	<p>Aftering offering music lessons in his home for a few years,Paul Green decided to take his passion for rock ‘n’ roll one step further. In 2000, he opened Paul Green School of Rock, an after-school program for children to learn about music.</p><p>After he began franchising the concept in 2005, it was soon bought out by Sterling Partners who changed the named to just the School of Rock. Now with more than 160 schools throughout the world, students can learn how to play guitar, bass, keyboards, drums or even sing thanks to private music lessons, group band rehearsals and even live performances.</p><p>With so many opportunities to rock out, it’s no wonder School of Rock landed as No. 208 on <em>Entrepreneur’s</em> <a>Franchise 500 list. </a></p><div> </div>

	
				
				
