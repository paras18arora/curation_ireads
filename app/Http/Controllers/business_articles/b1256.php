	<p>It’s really no surprise Golden Corral had its start in the south.</p><p>Golden Corral’s first restaurant opened in 1973 in Fayetteville, N.C., as a family steakhouse.  In the mid-’80s, guests’ requests for additional entrees and an expanded salad bar prompted the company to reinvent itself as the buffet-style restaurant we know today.</p><p>Though it’s not exactly known for five-star meals, it does feature hundreds of hot and cold stations and a large variety of edible options from muffins to sirloin and pizza.</p>While the sirloin didn’t come until 2001, the decision to do so could be why Golden Corral scored a spot on <a>Entrepreneur’s  Franchise 500 list</a> in the no. 104 spot.

	
				
				
