	<p>Madam Goh Gwek Eng, a 92-year-old woman from Singapore, is a perfect example that a career can start at any age.</p><p>As one of <a>McDonald’s</a> oldest employees, she has been working with the chain for 18 years. Before that, she raised five children. She now has 10 grandchildren and 20 great-grandchildren. When her kids began to have lives of their own and the house got quiet, boredom quickly set in, reports <a>The <em>Straits Times</em>.</a></p><p></p><p>So she set out to find something else to do and found her way to McDonald’s. The transition from her own kitchen to McDonald’s was an easy one, according to the paper. But she does face a few challenges, such as working with the fryer.</p><p>Her will to work and keep moving, even at an advanced age, may be the perfect example of a <a>study we reported on last week</a> that suggests holding off on retirement could lead to a longer life.</p><p></p><p>But in the case of Madam Goh Gwek Eng, she just might not ever retire.</p><p>"I plan to keep working for as long as I am healthy," she says in the article.</p><div> </div>

	
				
				
