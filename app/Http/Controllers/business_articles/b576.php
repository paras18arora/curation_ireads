	<p>Sophia Howard, a third grader from Michigan, was born without a hand. Though her parents kept trying to get her to play sports such as soccer, the little girl was determined to play softball. She’s played the sport for three years, but thanks to <a>AxisLab 3D and its printers,</a> it’ll be the first season she’s played with both hands.</p>
<p>Aaron Brown, the owner of AxisLab, is a volunteer for e-NABLE and has created 3-D printed hands for people all over the world, including little Sophia.</p>
<p>The hand only cost Brown about $40 to make, but we bet it's priceless for Sophia.</p>
				
				
