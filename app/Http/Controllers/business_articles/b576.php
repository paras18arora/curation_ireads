	<p>Like any rambunctious 7-year-old, Carson Watson loves adventures and to play the piano. However, his ability to do so was limited because of an eye condition -- b<a>ilateral optic nerve hypoplasia</a> -- that limited his eyesight. He could only see things at close range in the size of pinhole with the corner of his right eye.</p>
