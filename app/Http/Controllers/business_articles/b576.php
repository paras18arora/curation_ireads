	<p><iframe allowfullscreen="" frameborder="0" height="315" src="https://www.youtube.com/embed/GhC_By9GMv0" width="560"></iframe></p>
<p>Sometimes, all you need is to see the reaction on someone’s face to know what they’re thinking.</p>
<p>Take, for instance, the look on 3-year-old Grayson’s face the first time he hears his dad say he loves him.</p>
<p>It’s OK, grab a tissue or two if you need it. It was an emotional day for his dad, Len Clamp, too.</p>
<p>“I've never seen a look like that today," Clamp said in a <a>UNC Health Care press release</a>. "I mean, he looked deep into my eyes. He was hearing my voice for the first time. It was phenomenal."</p>
<p>Grayson Clamp was born without cochlear nerves, rendering him deaf. He was the first to receive an auditory brainstem implant, the first-of-its-kind surgery in the U.S., as part of an FDA clinical trial. The device was originally used for people with auditory nerve tumors, but thanks to the results with Grayson, is now considered to help restore children’s hearing, as well.</p>
		
	<h2 class="slides">4. I think and then I walk.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160607194733-4.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Brain Computer Interface Lab | UCI

					</div>
	
	<p>Adam Fitz, a 29-year-old from California, never thought he would walk again after a motorcycle accident left him paralyzed.</p>
<p>But UC Irvine’s research lab had different plans, using his thoughts to create a computer algorithm that would help him walk again, bypassing his damaged spinal cord to help fire the muscles in his legs. As a result, Fitz was able to walk with his own legs for a brief period of time.</p>
<p>It was <a>the first time</a> a paraplegic was able to walk through brain control.</p>
		
