	<p>I chatted recently with a woman who earns her living as a freelance copy editor and sometime photographer. Her income fluctuates, making it tough to budget. She's able to pay the bills, but some months are tougher than others.</p>
<p>I'm in the same boat. I've been self-employed for three years, and my income has never been the same from one month to the next. Although budgeting for a variable income can be frustrating, it is possible. I've found a system that takes the terror out of an uncertain future.</p>
<p>The key is to give yourself a regular paycheck. First, project your personal income for the upcoming year. Traditionally, a small-business owner with variable income might build a budget based on average income earned during the previous 12 months.</p>
<p>But as the Great Recession has demonstrated, revenues can decline, which wreaks havoc with the "average-income" method. A more guarded approach projects future income based on the least amount earned during the previous year.</p>
<p>For example, maybe your average income in 2010 was $6,000 per month, but you earned only $4,800 in June. To be safe, tighten your belt and allow yourself only $4,800 per month in 2011. (If your income varies wildly -- $120 one month and $12,000 the next, say -- you'll need to use the average-income method.)</p>
<p>Once you've established a monthly salary, it's time to draw a paycheck -- and to save the rest of your cash for the future. To make this work, use two different bank accounts:</p>
<ul>
<li>A designated savings account owned by your business. This holding account should stand apart from your other business accounts. (Consider a high-interest savings account like those listed at Money-Rates.com.)</li>
<li>Your personal savings account.</li>
</ul>
<p>After paying business expenses each month, write yourself a paycheck based on the salary you calculated. Move any remaining cash into your business savings account, where it will accumulate during the year.</p>
<p>At the end of each year, do three things. Reset your salary based on the previous year's income. If needed, use the money pooled in your business savings account to pay taxes. Finally, if there's money left after paying taxes, pull it to your personal account as a year-end bonus.</p>
<p>To make variable income more manageable:</p>
<ul>
<li>Don't be tempted to draw from your holding account as cash accumulates.</li>
<li>Build a firewall of personal savings. When you have $5,000 or $10,000 or $20,000 in the bank, a few slow months won't bring you to panic.</li>
<li>Prioritize spending. Financial guru Dave Ramsey recommends listing all your expenses in order of importance. ("Importance, not urgency," he says.) When you get paid, start at the top of the list and work down.</li>
</ul>
<p>This system may seem complex, but it's simple in practice. Best of all, it's effective. Drawing a salary based on your minimum monthly income creates a safety buffer -- and that brings peace of mind, which allows you to focus on more important things, like your business. -- J.D. Roth</p>

	
				
				
