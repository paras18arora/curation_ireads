	<p>If you’re looking for a frozen yogurt place with a creative spin, <a>Menchie’s</a> is the place for you.</p>
<p>The name comes from a nickname co-founder Adam Caldwell gave his wife Danna, also a founder, when they were dating. However, Menchie, the character, was born when the couple opened their first store in Los Angeles in 2007.</p>
<p>As the story goes, the company’s logo and a cartoon character -- Menchie -- started mixing yogurt as just a baby and continued to cultivate his passion for mixing yogurts and toppings with his friends. Together, the franchise's cartoon characters make the United Flavors of Yumm and travel the world together in promoting the brand.  </p>In reality, after it started franchising in 2008, Menchie’s expanded its brand to include shops all over the world. With self-serve froyo, the store has 100 rotating flavors including low-carb, no-sugar added, dairy-free, nonfat, gluten-free and kosher options.
		
