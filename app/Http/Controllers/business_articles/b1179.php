	<p>During those sweltering summer months there are few things that can really help to keep the heat at a manageable level, one being frozen yogurt.</p>
<p>The revitalizing trend and affinity for frozen yogurt took center stage in the mid 2000s. Considered a healthier alternative to ice cream or other sweets, the market for frozen yogurt is on track to becoming a $9.3 billion industry by 2017.</p>
<p>For <a>National Frozen Yogurt Month</a>, here are a few frozen-yogurt franchises that can help to do it:</p>

		
