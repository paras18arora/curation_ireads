	<p>Simple and to the point is sometimes all it takes to get that taste of satisfaction.</p>
<p>The modest and humble approach is what helped two brothers Michael and Aaron Serruya -- start <a>Yogen Fruz</a> in Toronto in 1986. The first store was small but with great product and design. The idea was to offer consumers a healthy, experience rich with taste.</p>
<p>Since it started franchising in 1987, it has more than 1,400 locations in 47 countries.</p>

