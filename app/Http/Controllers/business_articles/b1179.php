	<p>Cool down on a hot summer day with this franchise.</p>
<p>Founded in 2013 outside of Tampa, Fla., <a>Happy Cow Frozen Yogurt </a>got its start with “the principle that everyone deserves to be happy,” according to <a>its website.</a></p>
<p>The froyo store offers a self-serving rotation of frozen yogurt, gelato, ice cream, sorbet and Italian ice. It also has 16 flavors -- including tastes like the original chocolate and vanilla --  and over 60 toppings and sauces to choose from.</p>
<p>It began franchising at the end of of 2015.</p>

