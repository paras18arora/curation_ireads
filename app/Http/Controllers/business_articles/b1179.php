	<p>Looking for tart frozen yogurt? Well, <a>Pinkberr</a>y’s got your back.</p>
<p>Founded in 2005, the Los Angeles-based company started franchising in 2006 and has since grown to 21 countries in 10 years. Its locations span from L.A. to New York City to Tokyo and even Istanbul.</p>
<p>The franchise, ranked <a>No. 228 </a>on Entrepreneur’s Franchise 500 list, prides itself on its offering of distinctive products, outstanding service and inspirational design.</p>

