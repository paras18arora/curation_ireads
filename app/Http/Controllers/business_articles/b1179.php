	<p>Want to fill your life cup with yogurt bursting with flavor and a positive can-do attitude?</p>
<p>So did entrepreneur Phillip Chang. He started <a>Yogurtland</a>, offering a variety of frozen yogurt, in 2006 and started franchising just a year later. The franchise comes complete with its own “flavorologists” who research, taste and adjust, to come up with the best flavors -- like salted caramel pecan and banana foster.</p>
<p>As a result, the franchise has more than 260 locations in the U.S., 16 flavors served daily and 33 different toppings to choose from. It’s also listed as No. 173 on Entrepreneur’s Franchise 500 list.</p>

