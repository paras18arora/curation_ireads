	<p>Is orange your favorite color? If so, you’re in luck because it’s <a>Orange Leaf</a>’s favorite color, too!</p>
<p>According to its website, the company sees the color orange as original, bold, different, bright, happy and a little bit weird.</p>
<p>“Everyone has a bit of orange in them,” the<a> site reads</a>. Which is why Orange Leaf’s goal is to inspire people to let their own orange out with its frozen yogurt, rotating flavors and topping bar.</p>
<p>The Oklahoma City-based company was founded in 2008 and has more than 300 locations in the United States. It’s also ranked as<a> No. 125 </a>on Entrepreneur’s Franchise 500 list.</p>
		
