	<p><a>Red Mango</a> got its start in the U.S. in 2007 near the University of California of Los Angeles.</p>
<p>But before making its way to the states, founder Dan Kim had set up shop in South Korea, opening 130 between the years of 2003 and 2007. This sort of growth caught the attention of Brix Holdings, a company that also has Souper Salad, RedBrick Pizza and Greenz in its portfolio. Brix Holdings acquired the rights to the Red Mango franchise in 2008 for the Western Hemisphere.   </p>
<p>Now with nearly 200 locations in the U.S., the franchise’s menu features fruit smoothies, cold-squeezed juices, salads, wraps, all-natural frozen yogurts -- available in original tart or a rotating variety of other flavors -- and a variety of toppings such as fruit or nuts.</p>Today, Red Mango is ranked as <a>No. 494 </a>on Entrepreneur’s Franchise 500 list.
		
