	<p>Hop from one delicious yogurt flavor to the next with <a>sweetFrog</a> and its mascots Scoop and Cookie.</p>
<p>The duo’s story begins in 2009, when sweetFrog opened its doors in Richmond, Va. after founder Derek Cha moved from the West Coast, bringing the valley’s frozen yogurt trend right along with him. It began franchising in 2012.</p>
<p>The F.R.O.G in the names stands for “Fully Rely On God,” which the franchise does in its effort to be a good neighbor and contribute to the community.  </p>
<p>The store offers a plethora of yogurt flavors, toppings, waffle cones and bowls, Belgian waffles, banana splits and parfaits.</p>

