	<p>Schultz is a CEO who cares <em>deeply</em> about social change and has been extremely public about his mission  to use the massive reach of Starbucks for social good.</p>
<p>In January 2012, he made his support for gay marriage clear when he publicly joined other big brands, such as Nike and Microsoft, in a backing Washington state bill to legalize same sex marriage. </p>
<p>Several months later, The National Organization for Marriage launched a boycott of the coffee chain leading to a decline in sales in the following quarter. During a March 2013 shareholder meeting, Schultz famously addressed a stockholder’s complaint that the company lost customers due to its support of gay marriage.</p>
<p>Here is an excerpt of what he said <a>in response</a>:</p>
<p>“Not every decision is an economic decision. We did provide a 38 percent shareholder return over the last year [Oct. 2011 - Sept. 2012]. I don't know how many things you invest in, but I would suspect not many things have returned 38 percent over the last 12 months. Having said that, it is not an economic decision to me. The lens in which we are making that decision is through the lens of our people. We employ over 200,000 people in this company, and we want to embrace diversity. Of all kinds."</p>
<p>In the same speech, he <a>invited that shareholder</a> in question to sell his shares and invest in another company. It is clear that Schultz believes that Starbuck’s accountability goes beyond merely financial.</p>
