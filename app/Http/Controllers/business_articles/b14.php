	<p>Schultz is well known for his desire to do good, even if some results are bungled.</p>
<p>For instance, his feel-good initiative where baristas in Washington, D.C. wrote “Come Together” on coffee cups to inspire the federal government to work together during the budgetary impasse -- or “<a>fiscal cliff</a>” -- of 2012 fell horribly flat.</p>
<p>But this campaign wasn’t as big a stinker as Schultz’s well-meaninged attempt for Starbucks to engage in the race conversation following racial tensions in Ferguson, Mo. by having baristas write the words “Race Together” on coffee cups in 2014. What ensued was confusion -- from the patrons -- and reluctance and embarrassment -- on the part of the employees. The campaign was pulled a week later.</p>
<p>While his execution of good intention may not always be fruitful, his concern about the country and politics have given rise to some successful and forward-thinking initiatives, some within his own company.</p>
<p>Starbucks rolled out a program to pay for college tuition for its U.S. employees and funds <a>70 percent of health care costs</a> for both full and part-time employees.</p>
