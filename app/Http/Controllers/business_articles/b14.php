	<p>Schultz has a tendency to micromanage -- from weighing in on what sort of beans to add to blends to waking up at 4 a.m. everyday to study the sales reports from the day before, <a><em>Business Insider</em></a> reports. And more recently when he attended a staff meeting where black postcards were presented to announce Starbucks’ partnership with Spotify, Schultz <a>instantly demanded</a> change to the cards.</p>
<p>“Black looks so dull,” he yelled. “We’re talking about music. This should be lively. Can we go with green instead?”</p>
<p>His strength is that he is aware of his control-freak tendencies, so he has countered this by shoring up his management team with leaders who can push back, including Kevin Johnson, his chief operating officer who came from Microsoft and Matt Ryan, a chief strategist with a history at Disney. </p>
<p>“Howard can always be convinced,” Luigi Bonini, Starbucks head of product development, told <a><em>Forbes</em></a>.</p>
<p>A story that illustrates Bonini’s point is when, in 2008, Schultz demanded that stores stop selling melted cheese breakfast sandwiches, saying that the strong cheddar scent was overpowering the scent of coffee.</p>
<p>However, <a>management pushed back</a> -- breakfast cheese sandwiches were popular -- and a compromise was reached. The cheese sandwiches were reinstated, and the newer version heated at a lower temperature with smaller slices of medium cheddar instead of big slices of sharp cheddar -- much to everyone’s satisfaction.</p>
