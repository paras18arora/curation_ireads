	<p>Schultz <a>stays connected to his past</a>, not forgetting his humble beginnings in the Brooklyn projects.</p>
<p>“I didn’t go to an Ivy League school. I didn’t go to business school,” <a>he says</a>, when relating to the sorts of youth he employs. “I was one of those kids. I could have today been one of those kids.”</p>
<p>The CEO also keeps on him a touchstone to keep him grounded. He carries with him the key to the original flagship Starbucks store that opened in 1971 in Seattle’s Pike’s Place, where he first saw the brand’s possibilities.</p>
<p>“I go there at 4:15 a.m. sometimes, just by myself,” he says. “It’s the right place whenever I need centering.”</p>
