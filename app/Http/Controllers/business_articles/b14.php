	<h2 class="slides">5. Earn the right to preach.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160511170626-5-starbucks-howard-schultz.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
