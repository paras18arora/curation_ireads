	<p><a>Starbucks</a> CEO <a>Howard Schultz</a> was 7 years old and living in the Brooklyn projects with his two siblings when his father, a truck driver, broke his ankle. Without health insurance or worker’s compensation, his family was left without an income or the ability to pay for medical bills.</p>
<p>The family somehow got by but that experience was a defining moment for Schultz. Later, as the CEO of Starbucks, he instituted an unheard of health insurance program that offers health insurance to both full and part-time employees.</p>
<p>A talented athlete, Schultz got into Northern Michigan University on a football scholarship but ultimately decided not to play and got through school with loans and odd jobs. After college, he went into sales, and it was at his second company, Hammarplast, which sold European coffee makers, that lead Schultz to Starbucks.</p>
<p>A compelling communicator, he’d risen within the company quickly. Starbucks, a tiny coffee bean selling operation back -- then owned by Jerry Baldwin, Zev Siegl and Gordon Bowker -- blipped onto his radar when Schultz, the director of sales, noticed an uptick of coffee maker sales to the company out in Seattle. Piqued, he flew out to the city to suss out the situation.</p>
<p>“When I walked in this store for the first time -- I know this sounds really hokey -- I knew I was home," <a>Schultz later remembered</a>.</p>
<p>A year later, he joined the company as Starbucks' director of sales, and during a trip to Italy, he had an epiphany. Up until then, Starbucks had just sold coffee beans. However, struck by Italy’s coffee bar culture, he became convinced that Starbucks should serve coffee drinks and foster community.</p>
<p>In 1984, the original owners of Starbucks gave him the greenlight to open up one coffee bar in Seattle, which became a huge success. However, it soon became evident that Schultz had a very different idea of where to take the company, so he left to open a coffee bar chain of his own: Il Giornale.</p>
<p>Il Giornale also was successful, and Schultz was able to buy Starbucks and merge it with Il Giornale in 1987. Today, no coffee chain sells more coffee drinks to more people in more places.  </p>
<p>The 62-year-old CEO -- who was first to graduate from college in his family -- is currently worth approximately $3 billion and heads the Schultz Family Foundation, which focuses on economic mobility for veterans and youth.</p>
<p>Schultz is a true radical and visionary for leadership and business acumen. Here are five lessons we can take away from this leader.</p>
