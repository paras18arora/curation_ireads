					</div>
	
	<p>At age 62, Schultz sometimes gets asked by younger business leaders, including John Zimmer, the president of Lyft, and Marvin Ellison, the CEO of J.C. Penney, how to advocate for social causes without risking job security.</p>
<p>His response? “You have to earn the right.”</p>
<p>What he means is that you have to demonstrate that you can deliver the results as a trusted business leader first, with financial growth and profits, before pushing your social goals. Which he has. Although Schultz left as CEO in 2000, he returned to the company in 2008 and has been delivering golden results.</p>
<p>In 2015, <a>stocks surged 48 percent</a>, and according to the <a>latest financial report</a> for the second quarter of 2016, worldwide Starbucks revenue has increased 9 percent -- equal to $5 billion -- and the company has opened 350 additional stores, bringing total store count to 23,921.</p>
				
				
