	<p>Appropriately,<a> Hooters </a>was originally founded on April Fool’s Day in 1983.</p><p>The group of six businessmen who started the venture had no prior restaurant experience, but they got a kick out of the idea. So, in October 1983, the doors opened for the first Hooters restaurant in Clearwater, Fla. Those six men are L.D. Stewart, a painting contractor; Gil DiGiannantonio, a liquor salesman; “Uncle Billy” Ranieri, a retired service station owner; Ed Droste, a real estate executive; Dennis Johnson, a brick mason by trade; and Ken Wimmer, a partner in the painting business with L.D. Strange group.</p><p>The first Hooters girl, Lynne Austin, was hired on a bet during a bikini contest.</p>The restaurant itself began franchising in 1986, specializing in food, sandwiches, burgers, salads, chicken wings, alcohol, and, of course, pretty ladies. Thirty years later, the franchise is ranked 462 on the Franchise 500 list.

	
				
				
