<p>The first time Dawn Costanzo became a nurse, she wasn't the person you'd want taking care of your grandmother. In the mornings, she'd slip on her scrubs, hang a stethoscope from her neck and cruise a beat-up Mercedes around the wrong side of Charleston, S.C., trying to score heroin. "It was great," she says. "You really learn how to read people and how they operate."</p>
<p>Costanzo's car was wired with a camera, the scrubs and stethoscope were loaners, and a badge, gun and radio were in the glove box. Costanzo was an undercover narcotics officer, and she loved her job.<br />
<br />
But in 2004, after nine years on the force and with a 5-year-old daughter and a 9-month-old son, she decided raiding meth labs and stinging drug dealers wasn't the best career choice. She applied to nursing school, thinking she'd work in a hospital. But during her studies, a friend's wife became seriously ill, and Costanzo saw firsthand the effect home healthcare can have on a patient's life. After graduation, she recruited her best friend and partner in the narco unit, Paula Tharp, and the crime-fighting duo bought out an 18-year-old Interim <a>HealthCare franchise</a> serving seven counties in coastal South Carolina.</p>
<p>Now, 4 1/2 years into the venture, Costanzo and Tharp's business is the only licensed pediatric home healthcare operation in the state, and they are considering doubling their territory. We caught up with Costanzo between calls to learn the difference between life in white and life in blue.</p>
<p><br />
When we started, home healthcare wasn't regulated in South Carolina. With our background, we wanted to find someone who would hold us to certain standards. So we took the difficult step of becoming accredited by CHAP, a national home healthcare organization. Police experience also helps with employees. If we say we're doing a background check, we do it. If we say we drug test, we do that, too.</p>
<p><br />
Yes. Before, they used an answering service after hours, but we wanted people familiar with the patients and able to answer questions. So for two years, Paula and I were on call 24/7; now we have a nurse on call. Then there was a need for pediatric home healthcare in our area, so we made it our niche and were certified last spring. We are the first and only pediatric-only home healthcare in the state.</p>
<p><br />
Let me tell you a story. One of our patients stopped getting services and ended up going into a bad depression. One day out of the blue she called. Paula and I went over to her house, which was in disarray. She couldn't walk, and she had lost 30 or 40 pounds. We cleaned her house up, took care of her and helped her get on the right medication. She's still one of our clients, and she feels like family. If ever there was a story I'm proud of, it's that one.</p>

	
				
				
