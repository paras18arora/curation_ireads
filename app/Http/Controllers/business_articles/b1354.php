<p><br />
SnapGoods connects people who need things--paintball helmets, high-end cameras--with product owners willing, for a fee, to rent out those items. The site has a verification program for borrowers and, when lenders agree to the program terms, the site fully backs the transaction and replaces lost, damaged or stolen goods.</p>
<p><br />
As Labor Day 2009 approached, SnapGoods co-founder Ron J. Williams wanted to take his girlfriend on a motorcycle tour of New York City. But he didn't own a bike and he couldn't find a place to rent one. Using an online classifieds site, Williams found a motorcycle owner willing to rent his ride, but it wasn't an easy transaction. "I had to take $2,000 cash out of the bank for a deposit, plus I didn't know the guy," Williams says. "It led to an aha moment: What if the entire scope of consumer options was available not just to buy or sell but to grab as I needed it?"</p>
<p><br />
Williams and co-founder John Goodwin launched the site in August 2010 with a focus on goods available for rent. A March 2011 redesign flip-flopped the focus to the needs of borrowers, since "want" posts generated more activity. Williams says they realized, "It's not really just about demand, it's about demand and also giving people the ability to assist. Even if I don't have what this person is looking for, I know somebody who does." They also added a rewards system, including credits toward future rentals for people who make transaction referrals.</p>
<p><br />
Borrowers pay a transaction fee of 50 cents plus 7 to 10 percent of the rental price. The site's relaunch is expected to boost rental volume significantly, from about 40 transactions a month to 1,000 a month by the end of the year.</p>
<p><br />
Most site activity has centered in New York City and other major metro areas. Williams wants to generate more activity nationwide with the launch of iPhone and Android mobile apps later this year. The co-founders also want to get more small businesses involved on the lending side.</p>

	
				
				
