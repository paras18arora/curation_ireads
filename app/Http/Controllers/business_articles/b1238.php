	<p>Dreaming of opening his own pizza chain while working at a parlor in high school, John Schnatter made the goal a reality in 1985 -- but it took a bit of creativity.</p><p>When Schnatter returned from college in 1984, his father’s restaurant, Mick’s Lounge, was facing financial difficulties and close to bankruptcy. Schnatter acted. He sold his beloved 1972 Camaro for $2,800 to bail his father out.</p><p>Related: <a>Papa John's Founder: 'I Am the American Dream'</a></p><p>He also decided to put in some time at the tavern. He purchased used restaurant equipment and began selling pizzas out of the back of the bar. The next year he opened up his first pizza store in Jeffersonville, Ind. called Papa John's. </p><p>The following year, Papa Johns started to franchise and has since gone worldwide with more than 4,700 locations in 37 countries. This helped it earn its ranking as <a>No. 30 in Entrepreneur’s Franchise 500 list </a>for 2016.</p><div> </div>

	
				
				
