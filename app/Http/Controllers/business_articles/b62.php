	<p>As a manager, you want to build an office environment that is creatively fulfilling for your employees but is also as productive as possible.</p>
<p>But who do you model yourself after to become a powerful and effective manager? If you refer back to the bosses of the big screen for your inspiration, most of their leadership styles leave something to be desired. However, even if these film bosses are a master class in what <em>not</em> to do, you can still learn a lot from their mistakes.</p>
<p>Read on for 7 lessons in leadership from the most memorable movie managers.</p><a></a>

		
	<h2 class="slides">1. Give credit where credit is due</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613174311-1.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			20th Century Fox

					</div>
	
	<p></p>
<p>Dabney Coleman's Franklin Hart Jr. truly checks all the boxes as far as terrible bosses go. Not only does he bully and harass his female employees like newbie Judy (Jane Fonda) and long suffering secretary Doralee (Dolly Parton), but he lies and pretends that great ideas from veteran office manager Violet (Lily Tomlin) are his own, and passes her over for a promotion to hand it to a male colleague.</p>
<p>If we're honest, the eternal lesson from this move is not to be a sexist jerk, but a good managers will champion their employees and help them rise to their full potential. </p>
<p></p>
		
	<h2 class="slides">2. Be direct</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613174358-2.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			20th Century Fox

					</div>
	
	<p></p>
<p>Bill Lumbergh, the iconic symbol of middle management portrayed by Gary Cole in <em>Office Space</em> wore terrible suspenders and spoke in a soul-sucking monotone.</p>
<p>He was wont to<a> say</a> things indirectly like, "What's happening… Oh, I almost forgot, uh, I'm also gonna need you to go ahead and come in on Sunday too, mmkay… We need to sort of play catch up."</p>
<p>Effective managers say what they mean, and don't fall into the linguistic trap of office jargon.</p>
<p></p>
		
	<h2 class="slides">3. Don't just look out for number one</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613174847-3.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			20th Century Fox

					</div>
	
	<p></p>
<p>Like Franklin Hart in <em>9 to 5, </em>Sigourney Weaver's high-powered Katharine Parker attempts to steal an idea from her down-to-earth secretary Tess McGill and get her fired for speaking up about it. Underestimating the unassuming Tess ultimately lands her in hot water, disgraced and out of work.</p>
<p>An effective manager understands he or she needs to be collaborative and that valuable ideas can come from everywhere. And yes, Tess did assume her boss’s identity to get ahead -- but come on, those are minor details.</p>
<p></p>
		
	<h2 class="slides">4. Stay above the fray</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613175239-6.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Columbia Pictures

					</div>
	
	<p></p>
<p>It's safe to say that director David Fincher and writer Aaron Sorkin took some creative liberties with the story of <a>Mark Zuckerberg</a> and <a>Facebook's</a> creation -- though there is truth to the contentious nature of the founding of social giant and the subsequent legal action from Eduardo Saverin and the Winklevoss twins.</p>
<p>But the lesson here is that as a strong manager, you should endeavor to build a professional rapport with everyone. If there is some sort of interpersonal issue, rise above it and don't let it affect the work.</p>
<p></p>

<div> </div>

		
	<h2 class="slides">5. Don't take advantage of your position</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613175421-7.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			MGM Studios

					</div>
	
	<p></p>
<p>Like a few other bosses on this list, Fred MacMurray's Mr. Sheldrake has a shaky relationship with general ethics. Jack Lemmon's hapless C.C. Baxter, an insurance agency worker with a crush on Fran (Shirley MacLaine), one of the company's elevator operators lets his superiors use his apartment to carry on their extramarital affairs.</p>
<p>When Baxter is put on the track to promotion, his boss Sheldrake finds out. Sheldrake then offers Baxter the promotion for use of his apartment.</p>
<p>The lesson here is don't be that guy, <em>obviously</em>, but also, don't use your status as the man or woman in charge to intimidate. Managers that lead by example are more effective than the ones that inspire fear and anxiety in their employees.</p>
<p></p>

<div> </div>

		
	<h2 class="slides">6. Never pass up a chance to educate</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613180316-5.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			20th Century Fox

					</div>
	
	<p></p>
<p>And speaking of bosses that inspire terror, Miranda Priestly (Meryl Streep) the powerful, withering editor of <em>Runway </em>magazine, never yells but is known to say things like "by all means move at a glacial pace. You know how that thrills me."</p>
<p>She can make or break a designer's career with a nod of her head. But in a memorable scene in the film, when her new assistant Andy (Anne Hathaway) laughs at the apparent similarity of two blue belts that are being tried out for a photo shoot, Miranda doesn't hesitate to break down why Andy isn't above the industry that pays her checks.</p>
<p>A good manager takes mistakes and if she can, turns them into learning opportunities for everyone to do better in the future.  </p>
<p></p>
		
	<h2 class="slides">7. Mentorship goes both ways</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160613175513-8.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Paramount Pictures

					</div>
	
	<p></p>
<p>At the start of the film, George Clooney's Ryan flies around the country as a specialist that helps companies downsize. Basically he fires people for a living and has next to no attachments in his personal life.</p>
<p>New hire Natalie (Anna Kendrick) wants to completely overhaul how the company fires people -- do it remotely with bloodless efficiency, and take Ryan off the road for good.</p>
<p>When she accompanies him on one of his trips to learn the ropes, they end up helping each other more than they realize. As a manager, encourage and find ways for different departments to work together, because your employees could find mentors and mentees in an unexpected way.</p>
