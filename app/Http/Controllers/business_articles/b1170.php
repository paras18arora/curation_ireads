	<p>If you want to lose weight, get in shape and have some fun doing it, <a>iLoveKickboxing.com</a> may be your solution.</p><p>The program was founded based on the question “What’s wrong with most workouts… and how can we fix it?” .</p><p>The answer was a high-power, intense, adrenaline-inducing kickboxing routine and gym to help people look and feel like fighters and have some fun while doing it. Founded by Michael Parrella in 2009, the idea was inspired by his martial arts studio, which he started in 1991.</p><p>By 2012, the gym began franchising and now has 137 locations in the U.S. Because of its high energy, the company earned the ranking as No. 388 on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
