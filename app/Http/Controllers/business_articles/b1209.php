	<p>With summer just around the corner, it’s time to start pulling out those sandals. Don’t have a pair? Don’t fret: the Flip Flop Shops franchise might have a few options worth your while.</p><p>Founded by Sarah Towne and Todd Giatrelis, the duo were inspired to start the company after Towne struggled to find a pair of comfortable shoes while at a convention in Las Vegas.</p><p>In 2004, the first location opened in Boston. In 2007, the company began franchising after teaming up with Brian Curin, Darin Kraetsch and Alan Woods, all of which are veterans of the franchise industry having worked with Cold Stone Creamery and Moe's Southwest Grill.</p>Today, the company holds the <a>285th spot </a>on Entrepreneur’s Franchise 500 list for 2016.

	
				
				
