	<p>Instagram now boasts more than 500 million users, more than 80 percent of whom are outside the United States, the company said on Tuesday, in a sign of the photo-sharing app's rapidly growing global reach.</p><p>The company owned by Facebook Inc.Â had 400 million users last September. It has seen its user base more than double over the past two years. Instagram added that 300 million people used the app every day.</p><p>Instagram has quickly eclipsed its rivals, notably Twitter Inc., which has seen its user base stall at just above 300 million. Facebook has about 1.6 billion users.</p><p>Facebook bought Instagram in 2012 for $1 billion, its first large acquisition, a deal initially met with skepticism.</p><p>Instagram has grown into one of the most popular social media apps worldwide and is expected to contribute significantly to Facebook's revenue, with research firm eMarketer predicting the company will generate $1.5 billion in ad revenue this year.</p><p>It is able to leverage Facebook's popular advertising technology and resources to target highly specific audiences.</p><p>Instagram said users shared on average more than 95 million photos and videos every day, with posts garnering 4.2 billion "likes" each day.</p><p>(Reporting by Yasmeen Abutaleb; Editing by Peter Cooney)</p>

	
				
