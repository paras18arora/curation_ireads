	<p>Triathletes Taylor Collins and Katie Forrest from Austin, Texas, were tired of grazing on fruit-and-nut bars while training. So the couple decided to develop a low-carb, grass-fed animal-protein snack: the EPIC bar, which they debuted in 2013 at Natural Products Expo West, a leading natural food and products conference. </p><p>It turned out that their timing was impeccable; gluten-free and paleo diets had become all the rage. Based on their prototype alone, they snagged a national distribution deal with Whole Foods Market the first day of the conference. Now all the couple had to do was scramble to fill the retailer’s order in 90 days.</p><p><img alt="EPIC Bars" src="https://assets.entrepreneur.com/images/misc/1465322994_epic-bars.jpg" /></p><div class="courtesy">Photo Courtesy of EPIC</div><p>Armed with $750,000 they’d scraped together from savings, credit and a generous angel investor, the pair got to work producing the bars in a rented warehouse. But there was a hitch: The perfect machine for packaging their preservative-free bars didn’t exist. To have one custom-built would take six months. And the $250,000 pharmaceutical-packaging machine they settled on could potentially create an air leak in one of every 10,000 packages, resulting in rancid meat. So the duo hired half a dozen people for the express purpose of inspecting the packaged meat bars 40 hours a week to make sure each one was airtight. </p><p>By the end of 2014, EPIC had made $6.8 million in sales. This past January, the company was acquired by General Mills. Collins and Forrest remain executives of EPIC, which is still headquartered in Austin. The biggest difference is that they’re now sitting on a hefty nest egg. </p><p>Without that imperfect, labor-intensive production process early on, “we couldn’t have gotten to where we are now,” Collins says. “There was no other option in our mind, and we were not going to not ship our product.”</p><h2>Read more success stories about turning a hobby into a 