	<p>Spills happen -- and usually when we least expect them. If you find yourself mopping merlot off your client's sleeve, fear not. A little know-how can fix both the spill and that client meeting. </p><h2></h2><p>“It’s the dissolved solids that remain that do the damage, and beer and wine have lots of them,” says Joseph Kouyoumjian, president of MicroReplay, which restores liquid-damaged electronics. When that IPA spills, turn your laptop off and upside down, and remove the battery.</p><p>If you can’t get to a repair shop, wait 24 hours (rice might speed up drying, but it won’t mitigate damage). If it restarts, even once, quickly back up your data first. “It could run for two years or two minutes,” he says. “So be ready.”</p><h2></h2><p>Phones and tablets are more tolerant of spills, and your data is likely backed up in the cloud or on your SIM card. Power down the device, pop the SIM tray and dry the card and inside the slot, Kouyoumjian says.</p><h2></h2><p>According to the American Chemical Society, red wine gets its color from anthocyanins, which are great for heart health -- but not for cashmere. If you’re the victim of a red wine spill, order a very dry vodka martini. Higher-proof alcohol will dissolve the wine stain and dry faster than water alone.</p><p></p>

	
				
				
