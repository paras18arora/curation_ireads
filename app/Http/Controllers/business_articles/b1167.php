	<p>Whether in love or not, nothing’s better on a hot summer day than the chance to cool off, especially when Italian Ice is involved.</p><p>So, in 1984, Bob Tumolo, a former firefighter, opened an Italian ice store and named it after his wife Rita. With help from his family -- his mom making the ice and his brother helping open a trio of stores in the following three years -- he eventually started franchising <a>Rita’s Italian Ice</a> in 1989.</p><p>In 2005, the family sold the franchise to McKnight Capital Partners, and it was later bought out by an investment firm, Falconhead Capital, in 2011. Today, Rita’s has 624 locations in the U.S and is ranked as No. 94 on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
