	<p>Buffett is notoriously frugal, but he isn’t miserly.</p>
<p>He has been a longtime philanthropist and has supported at-risk youths, education, prison education, homelessness and animals to tick off <a>some of his causes</a>.</p>
<p>However, his biggest cause is <a>The Giving Pledge</a>, a campaign he developed with his friends Bill and Melinda Gates in 2010, to get America’s wealthiest to commit donating the majority of their fortunes during their lifetimes. He’s a pledge himself and has decided to give the majority of his wealth to the <a>Bill & Melinda Gates Foundation</a>.</p>
<p>Some major names in business have <a>joined his pledge</a>: <a>Facebook</a> CEO <a>Mark Zuckerberg</a>, <a>George Lucas</a> and his wife <a>Mellody Hobson</a> of Ariel Investments, <a>Chobani</a> founder Hamdi Ulukaya and <a>Groupon</a> co-founder Brad Keywell to name a few.</p>
<p>At present, there are <a>137 signatories</a> who have joined the pledge.</p>
