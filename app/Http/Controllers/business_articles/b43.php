					</div>
	
	<p>Buffett has been a long-time advocate for a tax hike on the rich. Or more specifically, for the government to create two new tax brackets for those who make upwards of $1 million and $10 million, as he wrote in a 2011 op-ed in <a><em>The</em> <em>New York Times</em></a>:</p>
<p>“My friends and I have been coddled long enough by a billionaire-friendly Congress. It’s time for our government to get serious about shared sacrifice… while most Americans struggle to make ends meet, we mega-rich continue to get our extraordinary tax breaks.”</p>
<p>For the upcoming 2016 election, Buffett remains firm on taxing the country’s highest income earners and <a>endorses Hillary Clinton</a>, however he has hat tipped Bernie Sanders’s integrity and constancy of message.</p>
