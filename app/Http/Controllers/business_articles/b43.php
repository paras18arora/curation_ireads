	<h2 class="slides">3. Learn from your mistakes.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160427160519-3-warren-buffett.jpeg" />
			</div>
			<div class="credit">
