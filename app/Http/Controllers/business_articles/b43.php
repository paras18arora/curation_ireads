	<p>Known for his frumpy appearance, it’s not obvious at first glance that <a>Warren Buffett</a> is today worth more than <a>$65 billion</a>. Sure, he runs a multinational holding company Berkshire Hathaway and has a golden touch with stock picks, but Buffett prefers Cherry Cokes to booze, lives in <a>the same house</a> he’d bought in Omaha for $31,500 in 1958 and celebrated his second marriage by dining at a chain restaurant. He’s also pledged to give away the majority of his fortune during his lifetime.</p>
<p>At 85-years-old, the CEO is a true nonconformist among his leadership kind, and he continues to surprise us. His most recent splashy move that made news was his unexpected investment of <a>$1 billion in Apple stock</a>. Buffett has a well-known reputation for staying away from tech stocks. But for him, this is just par for the course.</p>
<p>His ticker tape of surprise moves include the highly unorthodox arrangement he had with his first wife, Susan Buffett and his mistress -- who eventually became his second wife. </p>
<p>While he and Susan had a traditional marriage for the first 25 years, raising three children with Susan as the stay-at-home mom, in 1977 Susan moved to San Francisco to pursue her dream of becoming a singer. Buffett was a mess, unable to look after himself, and Susan -- worried about her husband’s well-being -- asked local waitress, Astrid Menks, to look after Buffett.</p>
<p>What ensued was a unique symbiosis and respect between the trio, with the three sending out holiday cards including all of their names. When asked about his arrangement, Buffett put it like this: "Susie put me together, and Astrid keeps me together."</p>
<p>When Susan died from cancer in 2004, he was devastated. He married Astrid -- whom is like-minded in frugality -- two years later in a quiet civil ceremony at his daughter Susie’s home.</p>
<p>The finance titan’s life is the realization of living by his own rules and leadership wisdom. Buffett is a true radical and visionary.</p>
<p>Here are five leadership lessons we can glean from his life.   </p>
<p><a></a></p>

		
	<h2 class="slides">1. Mine your area of expertise.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160427161325-1-warren-buffett.jpeg" />
