<p> </p>
		
	<h2 class="slides">2. Fight for what you believe in.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160427160342-2-warren-buffett.jpeg" />
			</div>
			<div class="credit">
