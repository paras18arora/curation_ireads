					</div>
	
	<p>Buffett’s <a>first investment was at the age of 11</a>, when he visited his stockbroker father’s office and bought three shares of a stock at $38 a share. The young titan sold at $40 for a small profit. However, the stocks rose to nearly $200 a share, and he regretted selling so soon.</p>
<p>Later on, he referred to this experience as an early lesson in patience in investing.</p>
<p>He shared other lessons learned <a>in a 1989 letter he wrote</a> to shareholders, in which he recounts the time when he acquired the department store Hochschild Kohn for cheap, and then three years later, sold it for the same price.</p>
<p>It was an example for “bargain-purchase folly,” he wrote. “It’s far better to buy a wonderful company at a fair price than a fair company at a wonderful price."</p>
<p><a>  </a></p>

