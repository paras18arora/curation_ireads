	<h2 class="slides">5. Be transparent.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160427160707-5-warren-buffett.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Bloomberg | Getty Images

					</div>
	
	<p>When the 85-year-old Buffett found out he had Stage 1 prostate cancer in 2012, he shared the news <a>in a letter to shareholders</a> in April of 2012, even though his sickness would likely affect the value of the company’s stocks.</p>
<p>His letter went on to provide details about his condition.</p>
<p>"The good news is that I've been told by my doctors that my condition is not remotely life-threatening or even debilitating in any meaningful way. I received my diagnosis last Wednesday. I then had a CAT scan and a bone scan on Thursday, followed by an MRI today. These tests showed no incidence of cancer elsewhere in my body."</p>
<p>He went on to disclose that he would undergo treatments starting in July.</p>
<p>However, he was able to share good news with the public In September of 2012, when he <a>publicly announced</a> before a group of newspaper executives affiliated with Berkshire Hathaway:</p>
<p>“It’s a great day for me. Today I had my 44th and last day of radiation.”</p>
<p>Since then his health has been solid, but Buffett has cited his elder son, Howard, to be his successor as nonexecutive chairman of Berkshire Hathaway alongside two hedge fund managers who were hired to run his vast investment portfolio.</p>
