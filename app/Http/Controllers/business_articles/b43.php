	<p>Buffett is a longtime value investor, considered a somewhat stodgy, old-fashioned strategy versus the more new-fangled growth investing that is associated with tech investing.</p>
<p>The “Oracle from Omaha” invests in stocks that has been undervalued by the market but have good instrinsic value -- and then waits for the market to recognize the value. His approach sometimes takes years for a payoff, but his success motto is that he’ll take a certain payoff over a big one any day.  </p>
<p>And his investment philosophy has certainly <a>paid off</a>, with a 22 percent annual return for Berkshire Hathaway from 1964-2005.</p>
<p>Some of his best known investment successes have been with American companies, such as Coca Cola, Geico and Gilette, but Buffett also saw the future in China and did very well investing in China’s main oil company PetroChina in 2002 and 2003, which earned Berkshire Hathaway back $3.6 billion when he unloaded the shares four years later.  </p>
<p>Buffett gained a rep for being averse to tech stocks for the majority of his career, but it appears that this aversion has changed since Berkshire Hathaway invested <a>billions of dollars in IBM and Verizon stocks</a> in 2011 and more recently, when this month he surprised the investment world by buying $1 billion in Apple stocks. But is this really that much of a surprise?</p>
<p>Recent reports from <a><em>The New Times</em></a>, along with other publications, suggest that Berkshire Hathaway’s purchase of in Apple stock is a declaration that Apple has transitioned from a growth stock to a more mature “value stock,” which is in line with Buffett’s investment strategy.</p>
