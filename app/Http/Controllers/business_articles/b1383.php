	<p>If you feel the need for leads but don't have the dough to exhibit in far-flung trade shows, go virtual.</p>
<p>Virtual events -- online trade shows and webinars -- offer big cost savings while giving participants detailed information about attendees, says Patrick Egan, president of CMC Media, a Santa Barbara, Calif., media company that creates online communities.</p>
<p>It costs about $20,000 for a small business to take part in a trade show for a few days, Egan says, and about $1,000 to set up a virtual booth at one of his trade shows for a full year. Businesses can interact virtually with thousands of people who visit the online destination, and some remain active marketing centers. "We've had some customers who use their booths weekly to meet virtually with customers," Egan says.</p>
<p>Tim Clark, a partner at The FactPoint Group, a business-consulting firm in Los Altos, Calif., says virtual events can be excellent sources of lead generation. "Your ability to get information about the people who visited you and what they chose to view should be almost immediate," he says.</p>
<p>Clark suggests asking up front if you will have support in designing your booth and what the levels of participation cost. Paying a bit more could get you on a conference panel, giving you more visibility, he says, adding that it's also critical to staff the booth at all times with adequate personnel levels.</p>
<p>"Pay attention to times when your booth might get busy, and have a backup plan," Clark says. "You want to be sure you can answer questions and be able to help everyone who comes to your booth."</p>

	
				
				
