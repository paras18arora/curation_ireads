	<p>Making the right decisions about mobile service is a critical part of doing business, but deciphering the plans, available devices and tech jargon is enough to stop business owners in their tracks. We break down what's available from each of the major wireless carriers to help you figure out the best ways to mobilize your business.</p>
<p></p>
<p>Known best for its lock on the iPhone and iPad devices, AT&T has one of the best device portfolios on the market and is a strong choice for international business travelers. Its voice network, however, was ranked lowest by a smidgen in recent small-business satisfaction studies. AT&T's data network is perceived as weakest among small businesses, but the carrier's recent infrastructure investments helped it pull top performance marks in a study done by PCWorld this year. Plus, with the biggest Wi-Fi partner base of any carrier, AT&T ameliorates any remaining data weaknesses with free Wi-Fi coverage for subscribers.</p>
