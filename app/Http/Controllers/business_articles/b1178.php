	<p>At age 15, Molly Martin took a part-time job at a mobile-phone store in Greenville, Mich. She was a natural and quickly worked her way from intern to sales associate, then sales manager and, eventually, at the age of 21, division manager. By the time she was 22, she decided to go into business for herself. “I started looking at different franchises in the mobile space and eventually signed on with Wireless Zone,” says Martin, who took ownership at 23. “This industry is growing more into a tech industry -- there’s so many ways to expand.” Now 26, she has opened two successful units, in Fremont and Whitehall, north of Grand Rapids. </p><h2></h2><p>I’d been in the industry so long that I had good support from Verizon, the carrier Wireless Zone works with. I’d worked with account managers and district managers for years, and they had my back. I was fresh out of college, where I studied business and marketing and entrepreneurship. I learned how to manage the back end of a company and how to run the sales floor. In fact, with my experience, the training period was cut to 10 days from 21. </p><h2></h2><p>Within six months of opening my first store, in Fremont, a second location opened up, in Whitehall, 20 miles away. That area really needed a store, and I simplified operations by replicating everything I’d done with my first store.</p><h2></h2><p>I opened my third location, a kiosk, in February 2015 in the Grandville mall, but I didn’t really know anything about how malls work. The whole experience was outside my comfort zone. Staffing in the mall, for one, was difficult. The employees were really “mall employees” and not salespeople. The kiosk took up a lot of my attention, and my other stores suffered, so 11 months later I closed the kiosk. In two weeks, the numbers in my original two stores were back up. I learned that if you’re not comfortable, there’s no reason to waste your time and energy on it.  </p>

	
				
				
