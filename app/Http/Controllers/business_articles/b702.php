	<p>Tinder has recently been on a campaign to try and prove that its app is about more than just hook-ups.</p><p>"Tinder’s not one thing. It's about all different things -- hookups, long-term relationships, friendships," <a>CEO Sean Rad told <em>Fast Company</em></a>. Now add one more relationship to the mix: employer-employee.</p><p>Rad revealed that Tinder had actually recruited and hired "about six people" through its own app.</p><p>"Those conversations did not lead to dates," <a>Rad said</a>, laughing. "But to 'Hey, why don’t you come by and interview about this role?'"</p><p>Here's one example:</p><p>Tinder’s "in-house sociologist" Jessica Carbino <a>told<em> Los Angeles Magazine</em></a><em> </em>that she snagged her gig after matching with Rad on Tinder. In October 2013, she was a 27-year-old Ph.D. candidate at UCLA, and decided to try out Tinder to get a date.</p><p>Here’s what actually happened, <a>according to Los Angeles Magazine</a>:</p><p style="margin-left:40px">"One of the photos she swiped right on was of a twentysomething with short dark hair and a stare intense enough to knock down walls. He swiped right on her, too. The guy, it turned out, was the company CEO, Sean Rad."</p><p>He ended up offering her a job. It seems that Rad, <a>who has also found romantic partners on Tinder</a>, is quite the multi-tasker.</p>

	
				
