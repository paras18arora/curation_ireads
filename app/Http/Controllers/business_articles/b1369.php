<p> The location needs to be easily accessible to customers, especially in the morning. Then, think about the area's demographics. "It's easy to get emotionally attached to your concept or space," Milletto says, but if your customer base wants food and iced drinks, include them in the menu, even if it changes your square-footage needs. And finally, negotiate the hell out of the lease.</p>
<p> Take "extreme care" in building out the concept. Apply it to the layout, music, décor, furniture, customer flow and even the equipment behind the bar. Everything matters.</p>
<p>Since you're not opening a franchise, partner with vendors and roasters who have similar missions and are willing to provide support. Then train (and retain) the right employees, who can make or break your operation.</p>
<p> There's money to be made in specialty coffee, but in order to make a living, you have to invest serious time and energy into getting it and serving it. "It's very important," Milletto declares, "to have true passion for coffee."</p>
