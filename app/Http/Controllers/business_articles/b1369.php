	<p>Hard to believe, but achieving gold status at Starbucks does not an independent café owner make. A lot goes into a great cup of coffee, and it's the same for a great coffee shop, says Matt Milletto, vice president of Portland, Ore.'s Bellissimo Coffee InfoGroup, a company that's trained thousands on the art of opening and running a successful retail coffee business.</p>
<p>"The coffee bar is often that third place outside of work and home to relax, and creating the right ambiance is critical to success," Milletto says.</p>
<p>Aside from the patience to learn as much as you can before getting started, here's what else Milletto says you'll need:</p>
<p>Yes, you must differentiate yourself from the competition with a cool concept, but base it around what you can afford. Coffee retail is a high-volume, low-transaction business, and big coffeehouses can require a quarter of a million dollars in startup costs. Figure out if it's feasible--or hire someone who will.</p>
