	<p>The iPhone effect is finding its way into computer peripherals. Take the Touch Mouse. This Microsoft tool is the result of the Mouse 2.0 project, which was initiated in part to bring multifinger touch control devices to computers and in the process do away with discrete right- and left-clickable mouse buttons.</p>
<p>"The Touch Mouse runs from the same principles as a smartphone, but we applied those principles to open up a new realm of control over the PC," says Debbie Uttecht, product marketing manager for Microsoft Hardware.</p>
<p>Instead of merely clicking or using a scroll wheel, users can swipe on the mouse--right and left, up and down--to initiate commands. Drag one finger up to scroll a page up, for example, or drag two fingers up and a full window opens.</p>
<p>In practical use, the $80 Touch Mouse is intriguing. Windows open and close with ease and documents are simpler to control. But beware: Touch Mouse controls take some time to master. Still, if you are looking to make that clunky old PC of yours a touch-controlled wonder, give this device a try.</p>

	
				
				
