	<h3></h3>

<p></p>
<p></p>
<p>Introverts are great at listening to and empathizing with people. Author of <a><em>The Successful Introvert: How to Enhance Your Job Search and Advance Your Career</em></a><a> Wendy Gelberg</a> says introverts tend to have a calm demeanor, they think before they act and speak and they use analysis and thought to add an important dimension to any situation. Their ability to internalize events and closely listen to others make them great for a career in consulting.</p>
<p>Business and life coach<a> Val Nelson</a> began her self-employment journey in 2009 by coaching people to thrive in business as well improve their lives. As an introvert herself, her listening and empathy skills drove her decision to help others. Nelson chose to get life-coach training, which took her six months and cost her around $5,000 -- something she recommends -- although you are not required by law to get certification to call yourself a “life coach.”</p>
<p>“Good coaching training combined with strong business experience” is what it takes to excel as a successful life coach, explains Nelson, who adds that the “overhead for a coaching business can be fairly low.”</p>
<p>Another plus: most coaches work solely through the phone or online with their clients -- a setting that most introverts thrive in.</p>

<h3></h3>

<p></p>
<p></p>
<p>Rather than working in groups, introverts typically prefer to build deeper one-on-one connections with individuals. Often possessing an ability to provide thoughtful advice through planning and research, an introvert makes an excellent college application advisor.</p>
<p>Gael Casner has been an independent educational consultant for 14 years through her business,<a> College Find</a>. Helping students explore educational options, creating college lists for students, reviewing admissions essays and helping students narrow their options to make a final decision are only a few of the things Casner does on a daily basis.</p>
<p>She develops relationships with parents and students, and spends much of her time researching colleges and industry trends. Like Casner, if you do your research as well as get to know the strengths of your students, you’ll be able to successfully guide them toward their future while generating some income for yourself.</p>
<p>A quick way to find clients is to tap into some school networks in your area and get your name out there. It starts with a simple email. Setting up a time to meet with the local principal of some schools and faculty members to introduce yourself and your services will help build your credibility and rapport with parents, plus create partnerships with schools for referrals.</p>

<h3></h3>

<p></p>
<p></p>
<p>Creating your own online tutoring business is a great way to explore your intelligence and provide assistance to others.<a> Carl Arnold</a> is an online tutor helping middle and high school students develop composition skills, as well as assisting with application essays for aspiring college students.</p>
<p>You too could hone in on your academic specialties and use them to teach and guide students. Arnold communicates with students through phone, Skype or email, so for those introverts who thrive in a virtual medium, this career path could be a great option.</p>
<p>With fees ranging from $65 to $80 an hour depending on a student’s needs -- sought-after SAT and standardized testing tutors can charge up to $150 an hour -- you can charge per session or offer a package deal, which also covers the costs of materials.</p>
				
				
