	<h3>Graphic designer</h3>

<p></p>
<p></p>
<p>For highly creative and visual introverts, freelance graphic design can be a great way to make a living. With digital businesses on the rise, demand is higher than ever.</p>
<p>Jacob Cass is a graphic designer who started design business<a> Just Creative</a> in 2012. Solving clients’ business problems through visual communication such as creating logos, websites, stationery and marketing materials are only some of the many projects he undertakes on a daily basis.</p>
<p>“Web design can be self-taught -- that’s how I learned,” Cass says. “You need to know software to do this, but most importantly you need to understand the principles of design as well as understand clients needs, not wants.”</p>
<p>It’s simple to get started. Cass registered his company with the government as a sole proprietorship, then began reaching out to clients. Both tasks that can be done from your computer. Other than acquiring certain software such as Adobe Photoshop, Illustrator and InDesign, startup costs were minimal, he says.</p>

<h3></h3>

<p></p>
<p></p>
<p>Detail-oriented and meticulous, introverts make excellent coders. The combination of patience and focus makes coding a great option for an introvert seeking self-employment. Because coding is such a niche skillset, there is high demand for freelance coders, and much of the work can be done from the comfort of your home.</p>
<p>It gets better. There is an abundance of free resources online such as<a> Code Academy</a> and<a> Udemy</a> where you can educate yourself. Also<a> General Assembly</a> offers one-shot classes and intensive six to 12 week training sessions online and in-class for a cost ranging from $140 to $3,500 -- that’s what jumpstarted coder Yin Mei’s career.</p>
<p>Mei enrolled herself in a 12-week General Assembly Bootcamp where she developed the necessary skills to become a front-end developer. (Front-end development is the part of a website that you can see and interact with like fonts, drop-down menus, buttons, contact forms and other aesthetics of a site.) It requires fluency in HTML, CSS and JavaScript, plus coders should know front-end frameworks such as AngularJS and ReactJC.</p>
<p>To the contrary, back-end development refers to the “server-side” -- basically everything you can’t see on a website. It operates the site with updates and changes made on the front-end. If it’s back-end developing you’re looking for, Java, Scala and Python are the primary languages. And don’t let those technical words scare you off.</p>
<p>“If you are a functional human being, you will be able to learn to code,” Mei says.</p>

<h3></h3>

<p></p>
<p></p>
<p>Passion for fashion? Or just own way too many clothes you don’t need anymore? Rather than hoarding those unused belongings in your closet -- or trashing them -- sell them online. Online consignment offers introverts a great opportunity to make money through a completely virtual process. Today there are a number of<a> online platforms</a> -- such as <a>The RealReal</a>, <a>Tradesy</a> and <a>ThredUp</a> -- specific for selling your unwanted clothing, jewelry and accessories.</p>
<p>Linda Lightman, an <a>eBay</a> seller of 15 years, built an e-consignment empire,<a> Linda’s Stuff</a>, which now brings in almost $25 million a year. She began her online career by simply selling her son’s old video games, eventually moving to items in her closet and later selling items for friends.</p>
<p>It only took passion and a can-do attitude for Lightman to start her business, which now employs 20 people in a 5,000-square-foot office space in Hatboro, Pennsylvania. “I was always so passionate about fashion and for me it was a no-brainer,” she tells<a> <em>Daily Mail</em></a>.</p>
<p>Startup materials? You will need a computer and a camera to take photos of your clothing. The rest is easy. Do your research, pick a great user ID or name for your shop, using quality images and vivid descriptions of what you’re selling and providing online customer service. And once you get the hang of it, you’ll be able to start selling for others too!  </p>

<h3></h3>

<p></p>
<p></p>
<p>Social media doesn’t necessarily mean “social.” Many businesses today have abandoned traditional marketing methods and taken to social media networks such as the ever-popular <a>Instagram</a> to promote their products and services. A majority of companies may not be well-versed in this new marketing technique, which is why outsourcing an Instagram consultant is often a great option.</p>
<p>Instagram consultant<a> Emelina Spinelli</a> helps firms grow a sustainable Instagram following and influence. She’s passionate about the photo app and providing resources for others to learn about this unique and extremely popular social channel, which just <a>hit 500 million users</a>.</p>
<p>Like Spinelli, if you have a love of Instagram, knowledge of basic marketing and simply, motivation, Instagram consulting can be a great path for you. You’ll be able to monetize these skills by charging a flat rate per project or by the hour. Market and promote your services online, blog on other related sites providing free tips (don’t give all the secrets away) and, ironically, using social media channels, build a following and reach out to businesses or individuals. All of which will come with little to no startup costs.</p>
