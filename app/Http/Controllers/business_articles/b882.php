	<p>If you identify as an <a>introvert</a> and are interested in your own low-cost startup, you could be an ideal candidate for starting your own business.</p>
<p>Introverts are powerful in their own right. They are productive thinkers with strong opinions who can achieve great lengths. Despite what the media says -- often stereotyping them as “shy” or “socially awkward” -- introverts can make great business leaders and entrepreneurs. In fact, many successful business leaders are introverts, including Bill Gates, Mark Zuckerberg and Warren Buffett.</p>
<p>At least one-third of all Americans are introverts, says<a> Susan Cain</a>, author of <em><a>Quiet: The Power of Introverts in a World That Can’t Stop Talking</a> </em>and the leading voice today on lost opportunities when undervaluing introverts.</p>
<p>“They are the ones who prefer listening to speaking, reading to partying; who innovate and create but dislike self-promotion; who favor working on their own over brainstorming in teams,” she says.</p>
<p>Here’s a list of 12 <a>low-cost business ideas</a> under $1,000 to get you on your way.</p>
