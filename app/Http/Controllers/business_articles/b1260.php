	<p>Founded in 1988, Coffee News is a weekly newspaper franchise distributed to restaurants.</p><p>The franchise got its start with the old problem-solve formula. Canadian Jean Daum was waiting for her food to arrive at a restaurant and started to read the nutritional information on sugar packets. It was at that moment Daum came up with the idea that restaurants should give patrons something to read while they wait. Coffee News was born shortly after.  </p><p>It began franchising in 1994, and in 1995, Bill Buckley became the first U.S. franchise. When Daum passed away in 2007, Buckley bought the company in 2008 and relocated to Bangor, Maine.</p><p>It has now been around for the past 22 years and the publication keeps people informed and entertained with its ads for local businesses as well as news stories, jokes, trivia and even horoscopes.</p>Because of this, the company has earned the 158th spot on <a>Entrepreneur’s Franchise 500 list. </a>

	
				
				
