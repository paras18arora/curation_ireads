	<p>The National Football League officially kicks off its 2010 schedule Sept. 9, when the Minnesota Vikings take on defending Super Bowl champs the New Orleans Saints. But the season unofficially launches Labor Day weekend, when the growing ranks of the fantasy football faithful converge on bars, basements and barbecues across the nation for their annual league drafts. The latest Fantasy Sports Trade Association data pegs the number of fantasy gamers in the U.S. and Canada at 30 million, and the industry continues to expand, embracing "sports" from mixed martial arts to reality TV shows. There's even a sitcom devoted to the hobby, FX's raunchy The League.</p>
<p>So far, fantasy gaming remains as recession-proof as the professional sports it mirrors: 8 percent of gamers plan to devote more time and energy to their fantasy team this year than in previous years, an FSTA survey found, and 71 percent say they will be just as committed as in 2009.</p>
<p>What's the appeal? It's fun. It's social. It's an outlet for smack talk. Hell, if you know what you're doing--and catch a few lucky breaks--you might even win a few bucks.</p>
