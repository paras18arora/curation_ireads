	<p>After five years in business, Zalmi Duchman needed help. On top of trying to grow his food delivery service in the Surfside, Fla., area, he was handling all of the reconciliation, payroll, projections and other financial matters.</p>
<p>Overwhelmed, Duchman decided to hire a chief financial officer in early August. But the decision wasn't easy. "I still don't know when I'll give him the login to my bank information," he says.</p>
<p>Figuring out whether to hire a CFO can be tough, especially because it's not a position that generates revenue, says Ren Carlton, founder of Dynamic Advisory Solutions, a Troy, Mich.-based CFO and business consulting firm, and author of <em>Profitpreneurship--Creating a Business That Produces Outstanding Financial Results</em>. However, an entrepreneur's ineffectiveness at trying to do it all is where a CFO can return on investment.</p>
<p>Are you ready for a CFO? Carlton gives these telltale signs:</p>
<ul>
<li>You're regularly getting financial "surprises" in the form of bank notices, tax penalties, or unusual financial statements.</li>
<li>You regularly feel overwhelmed by the financial demands of your business.</li>
<li>You're not keeping good records and not tracking expenses because you don't have time to do so.</li>
<li>You're not looking at planning, regulatory or tax changes.</li>
</ul>
<p>As for Duchman, he is committed to working with his CFO for six months. After that, if he has passed off banking and payroll activities to his CFO and identified that he brings value (they have started addressing changes they will make when the healthcare reform bill goes into effect in 2014, for example), Duchman will consider his hire a success. </p>

	
				
				
