	<p>It’s no surprise a business that’s literally a room full of trampolines got its start in Las Vegas.</p><p>Much to the delight of parents everywhere, the company SkyZone opened in 2004 and began franchising five years later.</p><p>If you’ve never been inside this room of fun, you know that each location features a trampoline-lined playing court for “open jump,” dodgeball tournaments, fitness classes and other activities.  </p><p>Open for seven years total, this franchise has quickly made a name for itself earning the 308th spot on <em>Entrepreneur’s </em>2016 <a>Franchise 500 list. </a></p>

	
				
				
