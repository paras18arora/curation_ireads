<p> If you're serious about it, so to speak, be prepared to put in some effort. Most people aren't used to having fun at work, so you'll need to set an example -- even if it means sporting a peg leg, all by your lonesome, during the office's first "Talk Like a Pirate Day."</p>
<p>"Any workplace can be fun, but it needs to come from the top," says M.P. Mueller, a former standup comedian and the founder of Door Number 3, an indie ad agency in Austin, Texas, where a mariachi band and petting zoo have been known to turn up. "The first couple of times you may get stares, but if you give people permission to bring their personalities to work, they'll be happier. And happy people are more productive."</p>
<p>Here are Mueller's top five tips:</p>
<ol>
<li>. "An open-air, no-cubes environment allows staff's personalities to infect one another," she says. If a cube farm is a must, create opportunities for people to interact--such as monthly birthday celebrations with activities. (She likes "Draw the Boss While Blindfolded.")<br />
 </li>
<li> "Keep staff meetings light," she says. "There are times you have to be serious, but if you spend nine hours somewhere, you should make it fun when you can." At Door Number 3, employees bang on a big moose door knocker when they have an announcement, and recently, two people were awarded gas masks after they volunteered to clean out the fridge.<br />
 </li>
<li> "Too often, business owners create fabricated, forced 'fun,'" Mueller says. "When I brought in the petting zoo, everyone had deer-in-the-headlights eyes at first. But they laughed about this for days afterward."<br />
 </li>
<li> Just make sure you're engaging people. Renting a mariachi band for an afternoon and getting a few six-packs and some queso cost less than a couple of hundred dollars, Mueller recalls, but it totally changed the tenor of the day. This applies to companies of any size. Door Number 3 has 15 employees, but Mueller's previous employer had around 700, and the CEO there would dress up as Elvis (about $60) and encourage events that involved human lint rollers (6 bucks for 45 yards of duct tape).<br />
 </li>
<li> Expect some initial awkwardness. "When I did 'Talk Like a Pirate Day' the first time," Mueller says, "people were like, 'Really?' But I was so determined to be 'that' agency, and with the exception of one new hire, people really got into it."</li>
</ol>

	
				
				
