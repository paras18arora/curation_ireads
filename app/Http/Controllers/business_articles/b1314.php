	<p>In her book <em>Master Your Fears: How to Triumph Over Your Worries and Get On With Your Life</em>, psychologist Linda Sapadin presents an action plan for addressing your fears, including the following key points that can be applied to the business world.</p>
<p> Undertake an action you've been thinking about taking, without regard for the result. "For the entrepreneur, it could be that you want to contact someone who could be helpful. What if he blows you off? It doesn't matter. The goal will be to take the action, not the outcome," Sapadin says.</p>
<p> Use positive rather than negative language. "I tell people to say it even if [they] don't believe: 'I can do this' vs. 'I can't do this'--that's fear-talk rather than calm-talk. Develop a habit of using calm-talk even if your first reaction is fear-talk," she advises.</p>
<p>When you're not sure, fake it. "You don't have to feel confident on the inside to look confident on the outside," Sapadin says. "Many accomplished people feel shaky--terrified--but they go on to do what they do."</p>
<p> Sapadin likens the body to a circuit breaker--don't overload it. "As you're feeling fearful, your body is tightening up," she says. She recommends relaxation exercises such as deep breathing, yoga or meditation. Better yet, get away for a long weekend.</p>

	
				
				
