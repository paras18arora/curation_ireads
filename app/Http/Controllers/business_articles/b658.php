	<p>Facebook Inc. on Thursday launched a feature that allows users to upload photos with 360-degree views.</p><p>Apart from photos taken from 360-degree cameras, panoramic photos taken on a mobile phone can be converted to a 360-degree view on the newsfeed, the company said on Thursday.</p><p>The photos can also be seen in virtual reality compatible devices, Facebook said.</p><p>Facebook rolled out a similar service for video last September.</p><p>(Reporting by Rishika Sadam in Bengaluru; Editing by Saumyadeb Chakrabarty)</p>

	
				
