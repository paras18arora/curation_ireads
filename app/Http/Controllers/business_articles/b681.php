					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Snapchat Tops Twitter in Daily Users" src="https://assets.entrepreneur.com/content/16x9/822/20160603150135-snapchat.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Step aside, Twitter: 4-year-old Snapchat has surpassed Jack Dorsey's microblogging service in daily usage, according to a report from Bloomberg.</p><p>Citing unnamed people familiar with the matter, Bloomberg on Thursday <a>reported</a> that 150 million people are now using Snapchat every day, up from 110 million in December. Meanwhile, the site's sources estimate that Twitter has fewer than 140 million daily active users.</p><div class="row pcm-content" style="box-sizing: border-box; margin: 0px; padding: 0px; position: relative; max-width: none; width: auto; text-align: center; float: left; font-family: open_sansregular, arial, sans-serif; font-size: 18px;"> </div><p>Twitter does not disclose how many users interact with its service daily, but, in its most recent earnings report, said it now logs 310 million active users a month. About 44 percent of those users are active each day in Twitter's top 20 markets, implying it has around 136 million daily active users, according to the report.</p><p>As Bloomberg pointed out, Twitter, which was founded in 2006, was once the No. 2 social network behind Facebook, but has since been eclipsed by Instagram, Facebook Messenger, and WhatsApp. Snapchat, meanwhile, "has been growing quickly, boosted by its popularity among young people."</p><p>At this week's Recode technology conference, Dorsey, who took over for Dick Costolo as Twitter's CEO last year, said Snapchat's messaging experience is "very modern," Bloomberg reported. Dorsey also acknowledged that Twitter can be confusing and boring for new users, but said he's working to fix that.</p><p>The news about Snapchat's rise isn't too shocking. Last month, Bloomberg reported that the app now sees <a>10 billion daily video views</a> -- up from 8 billion in February. Meanwhile, Snapchat recently confirmed that daily active users spend an average of <a>25 to 30 minutes each day</a> using the app. Around 60 percent or so of these daily users are actively creating new content.</p>

	
				
