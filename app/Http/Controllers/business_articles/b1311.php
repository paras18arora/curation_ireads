	<h3>Beverages</h3>

<p>When Bill Owens held the inaugural meeting of the American Distilling Institute in 2003, 86 people showed up, most of them old faces from the tiny world of craft distilling. For the organization's 10th annual conference in April, more than 900 folks packed the Sheraton in downtown Denver. "There were kids with tattoos, doctors, lawyers, veterinarians," Owens says. "There were people with master's degrees and Ph.D.s. These are people who know how to make business happen."</p>
<p>Craft distilling, like craft brewing before it, is in the midst of a boom. Less than a decade ago, there were 70 distilleries in the U.S. Now there are 623, producing everything from whiskey, gin, vodka and rum to less-common spirits such as absinthe--and all the schnapps in between. Owens believes the number will rise to 750 by the end of 2014 and that the market will be able to support 1,000 independent booze-makers within a few years.</p>
<p>So why such rapid growth? Owens says distilling is the last piece in the artisan renaissance that has reshaped consumerism. "Every town in America has a coffee roaster, little bakeries and a brewery. Look at all the vegetable growers and cheese-makers," he says. "There's a huge amount of pride in physical work. There's no pride in going to an office building and sitting in a cubicle. Distilling is part of that."</p>
<p>But people who start distilleries aren't the same as those who make beer in the basement or raise chickens in the backyard. Craft distillers tend to be well-capitalized professionals with strong business plans and patience.</p>

