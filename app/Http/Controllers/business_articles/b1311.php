<p>After years working in completely different industries, in 2012 Mary and Pat Scanlan and Mark Kleckner started producing vodka from heritage varieties of potatoes in the Woody Creek Valley outside Aspen, Colo. Unlike commercial producers that use commercial ethanol, grain or discarded potatoes, the team behind <a>Woody Creek Distillers</a> picks, cleans, mashes and distills its potatoes within 24 hours for an ultra-premium product made from 100 percent local ingredients. After producing 10,000 cases last year, they hope to scale up to 50,000 or 100,000 cases in the next five years, and they've also started producing brandy and whiskey.</p>
<p>"People are getting smart. They're asking how their liquor was made, what it's made of and what's up with the stuff they've been drinking all their life," Kleckner says. "People are starting to look beyond all the marketing and hype. Because of that, craft distilling is going to benefit."</p>
<p>Scott Bush, who has revived Iowa's famed Templeton Rye--a popular whiskey during Prohibition and Al Capone's drink of choice--thinks craft distilling is filling a vacuum that the big distillers failed to capitalize on. "There was a need for innovation in the spirits industry for a long time," he says. "They hadn't done anything new or different for so long that people naturally gravitated to craft brands."</p>
<p>But craft distilling isn't just about expanding market share behind the bar. Many distilleries are opening tasting rooms and gift shops, and their impact on local economies can be huge. "The future for these places is in becoming tourist destinations," says Owens, who knows of craft distillers bringing in $30,000 per month from their gift shops alone. "People don't realize how much life they bring to their communities with breweries and distilleries."</p>

