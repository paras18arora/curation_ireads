	<p>Some may argue that to understand the history of L&L Hawaiian Barbecue, you must first try it's Hawaiian lunch plate.</p><p>As one of its original menu items, and still one of the more popular options, the dish features two servings of rice, a serving of macaroni salad, and a generous serving of a hot entrée. The combination of Asian and American elements in the dish helped it stand out from the competition and grown to include more than 175 franchises across the U.S. But it wasn’t easy.</p><p>Before it expanded into franchising, the company’s founders Johnson Kam and Eddie Flores, Jr., started the business in 1976 after purchasing a drive-in in Honolulu. Initially called L&L Drive-Inn, the entrepreneurs expanded the concept into a franchise in 1988.</p><p>By 1999, the business reached the mainland, launching in California. Flores decided to rename it L&L Hawaiian Barbecue and has continued to grow, earning the No. 407 spot on <em>Entrepreneur’s 2016</em> <a>Franchise 500 list. </a></p><div> </div>

	
				
				
