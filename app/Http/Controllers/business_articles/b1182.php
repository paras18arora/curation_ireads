	<p>Nothing is more American than a good ole sugar rush, and <a>Great American Cookies</a> is more than happy to provide one.</p><p>The company’s got the zeal of proud tradition, too, founded with a generations-old family recipe for gourmet cookies. Launched in 1977 in Atlanta, it started franchising the same year and has offered fresh brownies, cookies and cakes ever since.</p><p>Currently, the business is a member of the Global Franchise Group, which includes franchises such as Pretzelmaker, Marble Slab Creamery and MaggieMoo's. With more than 300 locations in the United States, Great American Cookies is ranked as <a>No. 128</a> on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
