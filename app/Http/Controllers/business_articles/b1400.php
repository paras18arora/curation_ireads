<p><br />
Are you buried under a landslide of e-mail, status updates, blog entries and tweets? Help is on the way. <a>Gist.com</a> creates customized social networks for business users, aggregating contacts from across multiple inboxes, address books and social media websites to build integrated profiles that keep you abreast of all that's going on with your colleagues, clients and collaborators. And Gist does it all automatically, sans invitations or friend requests. "So many people complain about their inboxes--the amount of messages they receive is overwhelming, and it's difficult to get the information they want," says T.A. McCann, Gist CEO and a former Microsoft exec, who founded the Seattle-based startup in 2008. "We built a system to help you understand who you're communicating with."</p>
<p><br />
Gist integrates with each user's preferred messaging platforms (including Outlook and Gmail) and business software tools (e.g., Lotus Notes and Salesforce.com), and even extends from the desktop to smartphones like Apple's iPhone and devices running Google's Android. Once all relevant information is aggregated into a single access point, Gist leverages more than 60,000 news sources, 20 million blogs and content feeds from social networks like Twitter and Facebook to constantly update users on their contacts' professional and personal progress. "We connect you to all the places where your contacts live," McCann says, adding that Gist also ranks and organizes relationships, applying multiple metrics and user behaviors to establish the relative strength and importance of each connection.</p>
<p><br />
Gist now boasts more than 100 million "social business" profiles--but it only assembles and supplies information. It's up to you to decide what you do with it. Savvy users can catch up on the latest company news before a meeting and track subsequent moves to identify follow-up opportunities. Gist also uncovers points of interest, targeting potential icebreakers and conversation starters. The service is free for now, but future upgrades will introduce premium features that bring in information from paid databases like Hoovers. "You can see exactly what your contacts care about at this moment and contextualize that information to help secure their business," McCann says. "It's about building relationships--and isn't that what business is all about?"</p>

	
				
				
