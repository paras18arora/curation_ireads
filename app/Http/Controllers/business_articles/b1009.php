	<p>Los Angeles screenwriter Meridith Baer loved to rearrange furniture and furnish her home with plants during lulls between writing projects. In 1998, after the home she’d helped a friend spruce up sold within a week for $500,000 over the asking price, brokers started calling to enlist Baer’s staging services.</p><p>So began Meridith Baer Home, a luxury home staging company that today makes more than $3 million per month, maintains five warehouses of inventory and employs some 200 people. Last year the company staged more than 1,500 properties in Los Angeles, San Francisco, Florida, greater New York City and Mexico. </p><p><img alt="Meridith Baer Home" src="https://assets.entrepreneur.com/images/misc/1465323236_meridith-baer.jpg" /></p><div class="courtesy">Photo Courtesy of Berlyn Photographyer</div><p>Baer certainly didn’t make her first $1 million one year into the business by shying away from unfamiliar territory. “It started that I did maybe a $2 million house and then they said, ‘Could you do a $7 million house?’ and I said, ‘Sure.’ And then I had to scramble like mad to find enough furniture, art and decorations to fill it,” Baer says. “But I really believe that the growth came from my saying yes and not worrying about how I was going to do it. People want to hear yes. And then they’ll write you a check.”</p><h2>Read more success stories about turning a hobby into a paying job <a>here</a>.</h2>

	
				
				
