					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Snapchat Acquires 3-D Photo StartUp" src="https://assets.entrepreneur.com/content/16x9/822/20160606152119-snapchat.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Snapchat in 3-D? The popular social network has reportedly acquired 3-D photo app service <a>Seene</a>.</p><p><a>According to <em>TechCrunch</em></a>, the low-priced purchase was made "a couple of months ago," though neither company has officially confirmed the deal.</p><p>Seene <a>makes</a> "computer vision technology" for mobile devices, including 3-D scene reconstruction, object tracking, augmented reality and 3-D face capture -- the latter of which is a very Snapchat-friendly quality. The system (see video below) launched in September within the Seene app, and promises real-time, high-resolution 3-D facial capture on iOS and Android.</p><p><iframe allowfullscreen="" frameborder="0" height="360" src="https://www.youtube.com/embed/gLMOpt5invA" width="640"></iframe></p><p>Neither Snapchat nor Seene immediately responded to <em>PCMag</em>'s request for comment. The startup's website shows no signs of a change, still listing San Francisco and London as its shared home; <em>TechCrunch</em> says the team will relocate to Los Angeles.</p><p>In April, Snapchat added the ability to <a>face swap with photos</a> from your phone's camera roll. Ever wonder what you'd look like as your cat? Or with your best friend's flawless skin? Hold the screen over your face to launch the app's selfie features, then swipe to the "Face-Swap From Camera Roll" lens. The program automatically scans your photos for faces, surfacing all applicable options.</p><p>It's functions like that which attract an estimated <a>150 million people</a> every day to the application, which recently bested Twitter's estimated 140 million daily users. The micro-blogging site -- once the number-two social network behind Facebook -- has also been eclipsed by Instagram, Facebook Messenger and WhatsApp.</p>

	
				
