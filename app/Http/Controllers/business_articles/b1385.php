<p>Social media is no flash-in-the-pan marketing trend--Facebook, Twitter and the like have the potential to amp up branding and drive demand like nothing before them. A study from Knowledge Networks and MediaPost Communications last November shows that these tools are as effective as TV for getting the word out for advertisers. So <em>Entrepreneur </em>put social media's marketing prowess to the test with an experiment that would measure exactly how much social media can help a business grow. Our subject? <a>Big Papa's BBQ</a>, a Denver-based restaurant chain and social media newbie. With the help of a team of creative thinkers and experts in branding and digital marketing, <a>we set out to take Big Papa's from social media zero to social media hero</a>--in 60 days.</p>

<p><br />
Big Papa's is a fast-casual restaurant founded in 2003 by Bill Cossoff, a serial entrepreneur and former commercial real-estate broker, and Frank Alfonso, a corporate restaurant manager for 25 years. It has two other locations--in Littleton, Colo. (one of which is a sit-down restaurant)--and it runs a catering business and employs 60 people total. Big Papa's notched $2.1 million in revenue in 2010. It's never spent more than $40,000 a year on advertising, and it had no social media presence.</p>

<p><br />
The goal was simple: Give Big Papa's a marketing makeover. But the stakes were high: We aimed to boost revenues by at least 50 percent. Our team, led by LeeReedy/Xylem Digital of Denver, came up with four steps to maximize followers and create a community of loyal (read: returning) customers.</p>

<ul>
	<li>Pop-up events where Big Papa's gives away ribs.</li>
	<li>A baby-back throwdown that challenges other area barbecue joints to the ultimate taste test.</li>
	<li>An exclusive Super-Secret Supper Society of followers that meets regularly for all-you-can-eat ribs and beer.</li>
	<li>A contest to win a catered party for 20 friends.</li>
</ul>

<p>For the first 30 days, LeeReedy/Xylem Digital took the lead in pushing promotions on Twitter and Facebook. At the halfway point, they handed the reins to Big Papa's and watched as the restaurant owners went social on their own.</p>

<p><br />
Heading into the effort, Cossoff and Alfonso wanted to get existing customers to buy more ribs, to recruit and inspire new fans and to shore up their brand as the best barbecue joint in Denver--all without breaking the bank. Did they succeed? Smoking ribs takes time, so in the spirit of true barbecue, you'll have to wait until our April issue to see how Big Papa's fared.</p>

<p>Follow #socialsauce on Twitter to keep an eye on Big Papa's social media immersion.</p>

	

