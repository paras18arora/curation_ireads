	<p>The gourmet-burger chain founded by the Wahlberg brothers is rapidly expanding.</p>
<p>The Boston-based chain, called Wahlburgers, has signed agreements with five franchise groups to open 30 new restaurants in seven states: Massachusetts, California, Georgia, Michigan, Ohio, North Carolina, and South Carolina, the company told <em>Business Insider.</em></p>
<p>Today the chain has only seven open restaurants.</p>
<p>The new agreements bring the total number of restaurants set to open over the five to seven years to 118, Wahlburgers CEO Rick Vanzura told Business Insider in an interview.</p>
<p>The cities that will get Wahlburger locations include: Atlanta and Savannah, Georgia; Los Angeles; Charlotte, North Carolina; Cleveland; Detroit; Charleston and Myrtle Beach, South Carolina; and greater San Francisco. The company has also made previous agreements to open in Manhattan, Orlando, Philadelphia, Long Island, and Washington, D.C.</p>

		
