	<p>The average ticket at Wahlburgers restaurants is roughly $14 to $15 a person for a drink, side dish, and burger, with tax.</p>
<p>The restaurants feature a full bar with craft beer, wine, and liquor, and menu items such as the O.F.D -- a half-pound beef burger with house-made tomato jam, bacon, Swiss cheese, and sautéed mushrooms -- and sandwiches like the crispy haddock, which features seasoned panko-breaded haddock fillets topped with lettuce, tomato, and balsamic tartar sauce.</p>
		
