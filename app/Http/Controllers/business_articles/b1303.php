	<p>The menu also has turkey burgers, chicken sandwiches, and a range of made-to-order salads. Sides include classic or sweet-potato fries, tater tots, onion rings, chili, and macaroni salad.</p>
<p>As at Five Guys and Shake Shack, Wahlburgers customers can order their food at a register and pick it up at the counter. But Wahlburgers also offers full table service, and it puts a strong emphasis on dining in.</p>
		
