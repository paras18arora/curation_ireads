	<p>Opening a restaurant might seem like an odd choice for Mark and Donnie Wahlberg, who are best known for their acting and singing.</p>
<p>But the lesser-known Wahlberg brother -- Paul -- is a classically trained chef who has been in the restaurant industry for more than 30 years.</p>
		
