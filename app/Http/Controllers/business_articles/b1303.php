	<p>The privately held company doesn't release many details about its financials. Vanzura said the original Wahlburgers location generated about $5 million in sales annually, but he noted that he would not expect that volume for every restaurant going forward.</p>
<p>By comparison, Shake Shack's 41 company-owned restaurants generate about $5.4 million annually, company filings show. Shake Shack, which went public last year, has a market value of about $1.5 billion.</p>
		
