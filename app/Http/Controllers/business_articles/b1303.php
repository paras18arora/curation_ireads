	<p>Vanzura says the company is still looking for franchisee groups to open additional locations.</p>
<p>The company, however, isn't accepting any individual franchisees; it's looking only for franchisee groups that can open multiple restaurants in any given territory.</p>
<p>"We're looking for people who have well-established experience in the restaurant business," Vanzura said.</p>
				
