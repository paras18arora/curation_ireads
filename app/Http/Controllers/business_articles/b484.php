	<p>Another SaaS-based product gets funded, and this time its Delhi-based FarEye. SaaS-based logistics management startup, which helps in managing logistics network, completed its Series A round of funding and has raised $3.5 million as growth funds from Saif Partners.</p><p><a>FarEye</a>, a SaaS-based mobile enabled solution for field workforce management. By integrating with the existing ERP’s & CRM’s the company digitizes the operations by connecting everything on a single platform.</p><p>The startup plans to integrate with IoT platforms & drones to enable automated delivery. Kushal Nahata, co-founder said “We are profitable B2B startup and believe in organic growth. Our vision is to create a global technology company which stays for decades. We might take time to understand but would like to build things to last long.”</p><p>The company has over 75 clients ranging from the exclusive e-commerce solution providers like Ecom express to the traditional behemoths like DTDC, Blue Dart, Safe Express, Bajaj Capital, Hitachi etc. </p><p>The company currently has a team of 30 and they plan to expand the team for sales, delivery & product and would reach a number of 60 in next few months.</p>

	
				
				
