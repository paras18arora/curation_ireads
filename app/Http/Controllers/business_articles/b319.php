	<p>Bartenders have seen it all. And when it comes to business meetings at a bar, they often know from a glance if things are going well -- or when they're heading for a spill. Let the insights from these three barkeeps guide you -- and make your next meeting a success.</p><h2>Always be on</h2><blockquote><p>“When you’re bartending, someone is always watching you. You may have had a terrible day, but you never let it show. And when conducting business, you're also on a stage and need to put everything into your performance and maintain the positive energy.”</p><h6><em>—Simon Ford, cofounder of spirits brand The 86 Co.</em></h6><p> </p></blockquote><h2>Stay ahead</h2><blockquote><p>“You have to be prepared. If you don’t come in early and do your prep work, then you’re going to be toast. Most people think that entrepreneurs are big risk-takers, but a more important trait is to be detail-oriented. Bartenders thrive on that.” </p><h6><em>—Henry Sidel, founder and president of Joto Sake</em></h6><p> </p></blockquote><h2>Love your audience</h2><blockquote><p>“It’s important to relate to everybody on some level. Just like in a bar, it’s the people who believe in you—the regulars—who become the most valuable supporters.”</p><h6><em>—Doc Hendley, founder and president of Wine To Water, a nonprofit dedicated to bringing clean water and sanitation to the world</em></h6></blockquote><p></p>

	
				
				
