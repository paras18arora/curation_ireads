	<p>Got a craving for tacos on the go? <a>Del Taco’s</a> got the goods.</p><p>The first Del Taco opened in 1964 by Ed Hackbarth and David Jameson in Yermo, Calif. The menu consisted of tacos, tostada, fries and cheeseburgers for just cents on the dollar. In its first day of business, it made $169 in sales, the equivalent of 900 tacos.</p><p>By 1970, the restaurant became a franchise and later merged with Naugles restaurant chain. In 1988 Del Taco adopted the 24/7 restaurant format.</p>Today, it has more than 550 restaurants in 16 states and is ranked as No. 482 on Entrepreneur’s Franchise 500 list.

	
				
				
