	<p>Suman Kanuganti and Yuja Chang knew zero VCs but needed funding for their startup, Aira. It creates internet-connected, camera-equipped glasses for the blind; if a person wearing them needs help navigating somewhere, an on-call sighted person can watch the camera feed and verbally direct the blind person. </p><p>Rather than beg for meetings, the cofounders became active in the blind community, where they hoped to find support. At a fund-raiser for the Foundation Fighting Blindness (FFB) in 2014, they chatted up the evening’s speaker, Lux Capital partner Larry Bock. He’d lost most of his vision to macular degeneration. “I was so in awe of the low-profile, inconspicuous but extremely powerful nature of the technology,” he says. Soon he provided seed capital and joined as Aira’s executive chairman. </p><p>Aira launched a prototype at a local FFB charity walk, where one tester successfully navigated several miles. A year of product tweaks followed, and then Bock helped again: His fund, along with ARCH Venture Partners (which tends to back companies he seeds), provided nearly $800,000 in new seed money in October 2015 -- and contributed to Aira’s $3.5 million Series A in March 2016. “We love funding founders who tackle big, hard problems unconventionally,” says Josh Wolfe, Lux’s managing director. The cash will help Aira launch officially later this year and hopefully scale up to 200 users by early 2017.</p>

	
				
				
