<p>2010. What a big, beautiful, round, even number for such a defining, ironic, iconic and odd year. We've witnessed the rise of the rebel, the crash of the titans, the birth of boundless thinking, the uprising of customer as brand manager and the emergence of the social network.</p>
<p>We've watched as the recession gives way to the "not a double dip, but not yet out of the recession non-recession, where-the-hell-is-the-bottom" economy. And this year, the very naming of the economy became sport: In short order, the economy was called the entrepreneur economy (OK, we started that one), the engagement economy, the attention economy, the social economy, the network economy, the halo economy, the iEconomy, the whatever-you-want-to-call-it economy.</p>
<p>Everyone's got a name for it, because everything--even the name of the economy--is up for grabs in the powerful new community economy. (See what I mean?)</p>
<p>In this issue, we take a look at what awaits business in the new year and what to expect as we get deeper into the new--still unnamed--economy. We tapped brilliant minds like Dennis Crowley, Jack Dorsey, Tony Hsieh and Seth Godin to get a sense of what's next (and in the case of <a>Godin's Back Page editorial</a>, what's gone and never coming back). And "<a>The Disrupters</a>" looks at powerful trends that will shape 2011. Social media, not surprisingly, weaves its way through each one.</p>
<p>The biggest trend for next year? Entrepreneurs continue to defy the recession and create innovative ways of tackling impossible challenges. And in the process, define the economy.</p>
