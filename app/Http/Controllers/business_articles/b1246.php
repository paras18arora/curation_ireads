	<p>Franchising since 1995, Color Me Mine provide wanna-be artists with the supplies and the studio to make their own ceramic pieces. Each franchise carries approximately 300 pieces and 60 colors to pick from, and staff provides assistance.</p><p>While you may think the store’s services are aimed for the younger bunch, this is certainly not the case. With a host of events for all ages -- from pajama nights to crash courses -- there’s something for everyone.</p>If you want to get your creative juices flowing, the franchise hosts parties, showers, team building activities, workshops and fundraisers and sits comfortably in the 323rd spot on Entrepreneur’s 2016 <a>Franchise 500 list.</a>

	
				
				
