	<p>When it comes to style at a low cost, Clothes Mentor has got ya covered.</p><p>The chain isn’t the first resale franchise founders Lynn and Dennis Blum opened. Previously working on two others, Once Upon a Child and Plato's Closet, the duo sold them to Grow Biz International (now Winmark Corporation).</p><p>Clothes Mentor opened its doors in 2001 offering a place for women to buy and sell shoes, purses, clothing and other delightful duds. It caught the eye of Grow Biz co-founder Ronald Olson, who had left the company in 2000. He began franchising Clothes Mentor in 2007.</p>Today, the franchise is ranked as <a>No. 260</a> on Entrepreneur’s Franchise 500 list for 2016.  

	
				
				
