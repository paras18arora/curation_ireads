	<p>Entrepreneurship has a lot in common with chess. To win, you have to anticipate where you might get clobbered down the line. “It’s staying a few steps ahead of everybody else, foreseeing what will happen and having the solution before the problem even occurs,” says Joseph Ferlito, CEO of Trrtlz a bracelet company that boasts $65 million in sales.</p><p>Consider Trrtlz’s approach to inventory. Conventional wisdom says not to order too much product when you’re just getting started and instead to test the market with small batches of merchandise. But from the jump the Ferlitos increased their production exponentially to meet the growing consumer and retailer demands they expected. </p><p>“It was a very calculated risk,” Joseph says. “But judging from our initial sales -- selling 20,000 bracelets in a week -- it made sense to think that those numbers would double quickly.”</p><p>Planning ahead also helped Trrtlz avoid competing with knockoffs when the product was still brand-new and the company still vulnerable. The Ferlitos placed two seasoned team members on the ground in China to oversee manufacturing and to carefully nurture local ties with key businesspeople. That way, Joseph says, “If there is a knockoff brand coming out, you hear about things before they even become an issue.”</p><h2>Read more success stories about turning a hobby into a paying job <a>here</a>.</h2>

	
				
				
