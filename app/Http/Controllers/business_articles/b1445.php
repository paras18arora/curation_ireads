	<p>For dudes, the downturn has been nothing but bad news. For the first time in economic history, the male unemployment rate surpassed the female unemployment rate--and it just kept getting worse. By December, 10.2 percent of men were out of work, versus 8.2 percent of women, During the worst of the job losses, male workers were handed 82 percent of the pink slips.</p><p>But while the world's been busy boohooing the "mancession," it's just starting to look at the other half of the story. And the news is good.</p><p>First, women are on the brink of becoming the majority of the workforce--and they start businesses at twice the rate of their male counterparts. In fact, the Center for Women's Business Research puts the current economic impact of women-owned firms at $3 trillion and reports they employ 16 percent of all U.S. workers--a number that's certain to rise when the economy rebounds.</p><p>It gets better. According to a recent study by Guardian Life Small Business Research Institute, women-led companies will account for as many as 5.5 million new U.S. jobs by 2018.</p><p>Good jobs, too. Women business owners care more about creating opportunities for the people who work for them, says Mark Wolf, institute director: "They're more intense about paying employees well and providing better healthcare." The future workplace, he adds, will be a more "inclusive, horizontally managed environment."</p><p>In other words, give it a few years and women are going to save the world--well, the U.S. economy at least.</p>

	
				
				
