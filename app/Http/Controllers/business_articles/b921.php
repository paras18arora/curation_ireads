	<p>We close deals in bars to relax, get away from our routines and make toasting our next success a little easier. But if you are meeting at a bar, choose your location carefully. Research shows control is key and where you sit could make or break a deal. </p><h2></h2><p>Researchers find that when we’re warmer, we’re more likely to judge people as trustworthy and to be generous. Don’t order a round of hot mulled wine, but do sit far from drafty doorways or windows.</p><h2></h2><p>For a study in the journal <em>Social Cognitive and Affective Neuroscience,</em> participants touched rough or smooth surfaces before judging social interactions. Those who’d touched rough ones viewed others as more difficult and adversarial. </p><h2></h2><p>When researchers from Harvard, MIT and Yale asked people to haggle over the price of a car, participants who  were parked in a wooden chair held out for more money than those seated in cushioned chairs. Take the hard seat and put its rigidity in your favor. </p><h2></h2><p>It takes the average brain just 200 milliseconds to determine a person’s emotional state based on facial expressions, according to research from the University of Glasgow’s Centre for Cognitive Neuroimaging. While waiting, make small talk with a server so you’re not caught slack-jawed on Candy Crush.</p><h2></h2><p>People with strong posture are more likely than their hunchbacked counterparts to feel good about themselves, according to an Ohio State University study. So sit up straight (and avoid a low table), and have the confidence to seal that deal.</p><p></p>

	
				
				
