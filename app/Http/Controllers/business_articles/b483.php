					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Mark Zuckerberg Uses a Very Low-Tech Solution to Thwart Spies" src="https://assets.entrepreneur.com/content/16x9/822/20160623140141-mark-zuckerberg-tapes-computer.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Celebrities -- they're just like us: Facebook founder and CEO Mark Zuckerberg tapes his laptop webcam and microphone to avoid being spied on.</p><p>The paranoid tactic was <a>spotted by developer Chris Olson</a> Tuesday, when the young entrepreneur <a>shared a photo</a> of himself at his desk, celebrating Instagram's 500 million monthly users.</p><p>Look closely -- past the corny picture frame and goofy grin -- and you can spot a piece of black masking tape on the left side of Zuck's laptop, as well as clear tape blocking the built-in camera at the top.</p><p>The strategy is not uncommon, especially among high-profile people who may be targeted by hackers. FBI director James Comey, for example, <a>recently admitted</a> to covering his computer's webcam, arguing that "absolute privacy" has never existed in America.</p><p>In late 2013, researchers <a>uncovered a loophole</a> in Apple's iSight system that allowed them to hack into some versions of MacBook laptops and iMac desktops to disable the webcam indicator LED.</p><p>Nearly a year later, a UK privacy watchdog <a>issued a warning</a> about a Russian website that streams live footage from insecure webcams -- from CCTV networks to <a>baby monitor cameras</a> -- around the world.</p><p>So, really, Zuckerberg is entirely justified in taping up his machine in an effort to keep anyone from extortionist hackers to the National Security Agency out of his life.</p><p>(And yes, that's his laptop: It's sitting on the same desk he showed off during a <a>Facebook Live video tour</a> last fall --complete with personalized graffiti, satellite model, and sunscreen).</p><p>Facebook did not immediately respond to <em>PCMag's</em> request for comment.</p>

	
				
