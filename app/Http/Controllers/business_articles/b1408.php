	<p><br />
The Daily Bark is <a>Milo</a>'s robust blog that keeps visitors abreast of retail news, shopping trends, local deals and company happenings. The blog is updated regularly with useful posts like "How Are Americans Spending Their Money?" and "The Origins of America's Largest Retailers." This page also serves as an aggregate of Milo's most recent activity on Facebook and Twitter, and the most popular items on the site.</p>
<p><br />
Jack Abraham launched Milo.com last year to help brick-and-mortar retailers convert online browsers into in-store shoppers.</p>
<p>"Actual stores offer what the internet never can: instant gratification and the tangible benefits of seeing a product before you buy it," says Abraham, Milo's CEO. "With Milo, brick-and-mortar can more effectively compete with e-commerce retailers like Amazon and drive intent-based foot traffic into their local stores."</p>
<p>Shoppers enter their ZIP code and product name, and Milo searches the shelves of local stores in real time to find the best prices and availability. Currently, the site tracks 50,000 stores and 2.8 million products.</p>
<p>About a million people use Milo every month, and the site's traffic grew 70 percent each month in 2009. Abraham has raised $5 million in funding--the first bit came from his college professor at University of Pennsylvania. Investors now include a co-founder of YouTube and the founder of Mint.com.</p>
<p><br />
"The beauty of the site is that it's basically a giant ad in disguise," Abraham says. Milo gets a commission from the merchant when a visitor buys a product online and picks it up at the store, and it also gets a cut when users click from Milo to the merchant's website. Last holiday season, site traffic spiked nearly 300 percent from September of the same year.</p>
<p>Through strategic partnerships with brands and retailers, Milo will introduce before Black Friday coupons that will appear only when it's relevant to the shopper's search. "This should really encourage people to actually go into stores after visiting our site," Abraham says.</p>

	
				
				
