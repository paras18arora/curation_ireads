	<p>It’s easy to have a goal in mind. It’s harder to follow through. But, getting your motivation in gear, even when you're not feeling it, can make the difference between those who <em>wish</em> they were successful versus those who <em>are</em> successful. </p>
<p><a>Productivity</a> is about staying focused and on track, and CEOs and founders use technology, meditation, relationships and other tools to squeeze the most out of everyday. </p>
<p>These 10 entrepreneurs share the productivity hacks they use to take care of business.</p>

<div> </div>


		
	<h2 class="slides">1. Melissa Bushnell</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617182042-MelissaTyingBow.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Baked by Melissa

					</div>
	
	<p>Melissa Bushnell is the founder for <a>Baked by Melissa,</a> a cupcake shop offering the dessert in bite-sized flavors. As someone who works with baked goods, she says she spends her time creating products as opposed to spending time on technology.</p>
<p>“I really just focus on creating with my hands so much,” she says. “I don’t spend too much time on my phone.”</p>

<div> </div>

		
	<h2 class="slides">2. Dave Rusenko</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617181406-2016-01-23-Weebly-45251.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Weebly

					</div>
	
	<p>Rusenko is the founder and CEO of <a>Weebly</a>, a hosting service for people to make and manage their own websites. The most successful and experienced people he’s ever worked with rely on on pen and paper, says Rusenko.</p>
<p>I don’t find many people otherwise doing that, and it’s something I’ve taken to,” he tells. “In every meeting I go to, I carry  a notebook around and take notes. It helps me remember to follow up and I don’t forget what was discussed when I refer back later.”</p>
		
	<h2 class="slides">3. Jeff Chapin</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617183524-JeffChapin-CPO.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Casper

					</div>
	
	<p>Chapin is the co-founder and chief product officer for <a>Casper</a>, a company that specializes in sleeping by offering related products, including a “one size fits all” deliverable mattress. Chapin keeps it easy and low-tech, using notes around the office. </p>
<p>I still use sticky notes to make daily to-do lists,” he says. “I make a list of what I need to do and check it off. Pencil and paper works for me better than anything I’ve seen.”</p> 

