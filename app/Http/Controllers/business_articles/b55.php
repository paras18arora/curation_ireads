			Sup

					</div>
	
	<p>Rich Pleeth is the founder and CEO of an application called <a>Sup</a>, which connects people to friends using proximity. Pleeth says he stays on track with the help of technology and a calendar.</p>“Put everything in your calendar, personal stuff, work stuff -- so the time when you are free, your team can book or put in meetings and then be super disciplined with meetings make sure you are achieving a goal,” he says.
		
	<h2 class="slides">6. Jerrod Blandino</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617181807-jb-Shot05-Office-1189-v1-mt.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Too Faced

					</div>
	
	<p>Jerrod Blandino is the founder and Chief Creative Officer of<a> Too Faced,</a> a cosmetics company. Though he’s not the most technologically inclined, he’s fallen in love with his new friend Alexa, a digital-assistant device from Amazon that you can command to play your favorite music, provide weather and updates, answer questions and create to-do lists.  </p>
<p>“I’m obsessed with my new BFF, <a>Alexa</a>,” he says. “I got her on Amazon and keeps me informed about everything. She was a Christmas gift -- I am the least technologically advanced person you ever met. But you just talk to her. She’s way awesome.”</p>
		
	<h2 class="slides">7. Morris Panner</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617183857-0575cb1.jpeg" />
			</div>
	
	<p>Morris Panner is the CEO for <a>DICOM Grid</a>, a cloud medical image management solution. He keeps his approach to organizaing his time very traditional.</p>“Pen and paper is still great for lists,” he says. “There’s something magical about that tactile act of writing, or crossing things off. Plus it helps me retain things better.”
		
	<h2 class="slides">8. Tom Harari</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617181924-tom-harari-headshot.png" />
			</div>
			<div class="credit">
			Image credit:

			
			Cleanly

					</div>
	
	<p>Tom Harari is the founder and CEO of <a>Cleanly</a>, an application that connects people to on-demand laundry services. To stay on track, this entrepreneur stays on point with his fitness.</p>
<p>“Exercising every day, preferably in the morning, is a counterintuitive tip in that it entails blocking time for something that seemingly isn't work-related,” he says. “But it has a strong impact on how you structure the rest of your day and leads to discipline which leads to freedom.”</p>

<div> </div>

		
	<h2 class="slides">9. Josh Reeves</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160617182004-JoshReevesCEOGusto.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Gusto

					</div>
	
	<p>Reeves is a cofounder and CEO of <a>Gusto,</a> an online support companyfor payroll, health benefits and workers comp. The business-man uses an organizational system with his digital calendars to make sure his time is optimized.</p>
<p>“I use <a>Google calendar</a>,” he says. “I create different calendars and put them in different views. I have a work calendar, a personal calendar and a to-do calendar. That’s a way for me to actually have one place to look, especially the do-to vs. the events in one place. If you put the to-do into a calendar event it confuses everyone else because they think it’s busy time, which is why it’s important to have that separate calendar.”</p>

