	<p>Jessica Dilullo Herrin is the founder and CEO of <a>Stella and Dot</a>, an online store for fashion merchandise. With a firm belief that self care enables her to be her best self, the entrepreneur uses an app to help her get rest.</p>
<p>“I use the <a>Sleep Well App</a>, an app that helps sleep and insomnia through hypnosis, all the time to help me sleep and meditate before I go to bed,” she says.</p>

