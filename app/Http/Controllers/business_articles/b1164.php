	<p>“I opened Unplug for selfish reasons,” says Suze Yalof Schwartz of her modern meditation studio inLos Angeles. In 2012, she was stressed -- a mother of three shuttling for work between New York City and L.A. She was curious about meditation, but classes required weeks of commitment. What she really wanted was “a quick fix of calm” -- so she built it. Now in its third year, Unplug has amassed a cult following, and Schwartz “has a folder of a thousand people who want to franchise it.” Here’s how she did it:</p><h2> </h2><p>Schwartz took Deepak Chopra’s class, visited Buddhist temples and enrolled in a Vedic meditation class “in some guy’s apartment for four days.” Aside from peace of mind, she found an industry stuck in the 1960s -- right down to the incense  and Birkenstocks. “Not that that’s bad,” she says, “but for a person who has never meditated, it would be great to have someone who’s modern and treats it like a life hack.” </p><h2></h2><p>Schwartz has called Unplug "the SoulCycle, DryBar or Calvin Klein  of meditation." Like those brands, Unplug is clean, efficient and minimally designed. The studio is “white on white on white. No Buddhas, no tapestries. I want it to feel fresh, like you’re checking into heaven.”  </p><h2></h2><p>Unplug’s classes last for roughly 30 to 45 minutes, and instructors talk only for the first five. Schwartz edited out what she calls “meditation-y voices.” (“<em>Siiiiink intoooo your breeeaaath</em>,” she intones.) To bypass lengthy in-class Q and A’s -- as are common in meditation studios -- instructors wait outside afterward.</p><h3>Check out more companies on the <a>2016 Brilliant 100 list</a>. </h3>

	
				
				
