	<p>You need to pick your key performance indicators, or KPIs. They’re data used to chart a business on its way to success and profits, and are often used when revenue-starved startups need to identify ways to measure progress in the absence of cash flow. But there’s a big risk: If you choose the wrong KPIs, you may drive your company to financial ruin.</p><p>So, how to pick? Identify your business goals and the activities that lead directly to achieving them. Keep in mind that more often than not, bad KPIs are the result of upper management and the board deciding what to track. In my experience, you’re better off listening to line managers and frontline employees; they’ll give you more granular KPIs, to truly show how your company is doing. (See chart below for some starter ideas.)</p><p>Once you have your KPIs picked out, lock in the time period for each one to be measured. The goal is to monitor change as close to real time as possible (I suggest weekly) and hold people accountable for improvements.  </p><p><img alt="" src="https://assets.entrepreneur.com/images/misc/1459794214_indicators.jpg" style="height:725px; width:610px" /></p>

	
				
				
