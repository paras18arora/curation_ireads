<p>First up, to a co-worker: "Ugh, you suck." A box popped up informing us we'd exceeded our tone tolerance. We clicked through for more details--no revision suggestions, but we were scolded. The phrase was deemed "humiliating" and "angry," and we were given the option to edit or ignore. Next, to a boss: "Why are you doing this to me?" This one made it through until we switched to the Mild setting, where it was flagged as "fearful." Overall, ToneCheck performed consistently well, as long as we didn't get too creative. ("He's such a bosshole!" passed under all settings.)</p>
<p>Even with a few kinks in the free beta version, there've been plenty of takers: more than 12,000 just weeks after its July release, says Lymbix founder and CEO Matt Eldridge. "And we've been flooded by requests from companies."</p>
<p>He's working hard on a final version that gets smarter the more you use it. But for now, it's already doing some good as a reminder to give messages a read-through before hitting send. </p>

	
				
				
