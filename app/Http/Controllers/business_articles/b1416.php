	<p>Oh, the testy e-mail. We get them, we send them, we almost always regret them.</p>
<p>And since it looks like we'll never learn, the next best thing could be <a>ToneCheck</a>--a judge-y new e-mail plug-in from Lymbix that flags emotionally charged words and phrases, giving you a second chance to sound like a nice person.</p>
<p>Intrigued, Entrepreneur gave it a whirl.</p>
<p>After a quick download at tonecheck.com, we had to choose our settings--a sort of obnoxiousness scale, in ascending order of censorship: Extreme, Moderate and Mild. We clicked Extreme, which flags only extreme words, just to see what we could get away with.</p>
