	<p>When Australian expat Donna Katz set up her wine exchange company, Tradeagrape, in Napa Valley back in 2013, she faced what every international business does: U.S. incorporation documents, and a long list of regulations and compliance issues. “I had to figure out every step on my own,” she says. She still is, to the tune of more than $50,000. </p><p>Had she started today, she might have appreciated a new service called Atlas: It offers to take care of all this fuss and more for just $500.</p><p>Atlas is the creation of Stripe, the electronic payment processing company, and was launched in early 2016. It helps entrepreneurs incorporate a U.S. company with a Delaware address, set up a bank account with a U.S.–based bank, obtain a federal tax ID number and start accepting digital and credit card payments (through Stripe, of course). It also connects founders with local tax and legal professionals. The service is available only to foreign businesses, but Stripe says it would like to expand domestically at a future date.</p><p>Stripe says that, in its first months, Atlas processed handfuls of companies and reduced their setup time from months to a matter of days.</p><p>No early client was ready to speak up, but there’s no shortage of entrepreneurs who wish they’d had help like this. Christian Van Der Henst is one: He’s Colombian, but wanted to launch his online education company Platzi in the States -- and it took two months. “I could have focused my energy on starting the business,” he says, “not getting bogged down by red tape.” </p>

	
				
				
