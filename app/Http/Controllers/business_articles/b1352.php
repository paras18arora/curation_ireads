<p> Andrea Zabinski</p>
<p>Online business owner</p>
<p>76</p>
<p>Nearly $5,000</p>
<p> Zabinski went to Quirky as an inventor but quickly became dedicated to influencing and social selling. She achieved significant influence by providing the popular idea-turned-invention Cloak with its tagline. "I like the collaboration of inventing on Quirky. It becomes addicting to help other people. It's very rewarding. Anyone can post their ideas and have their dreams met."</p>
<p>Ventu, a bowl for straining, serving and storing food (her invention)</p>
<p>"Stick with it. Be on Quirky.com at least once a week. The key is to influence the names and designs of ideas. You can't just vote and take surveys and expect to make money."</p>
<hr />
<br />
<p> Matthew Fleming</p>
<p>Clinical psychologist</p>
<p> 58</p>
<p>About $4,000</p>
<p> Fleming was an influencer from day one. "I came to the site and was inspired by Judi Sigler's MugStir submission, so I began to make video comments and suggestions about her idea. I don't think I earned much influence for that product, but I was hooked by the process and the collaboration and was eventually moved to pitch my own idea [Wrapster, a headphone cord organizer]."</p>
<p> An influencer at heart, Fleming says he will continue helping the Quirky community perfect their ideas.</p>
<p> "Think of it as trying to gather micro-royalties on products. Keep swinging that bat every day by submitting ideas, suggestions, voting, filling out the surveys and ordering products in pre-sale. Try to earn some small influence on each and every product. Success will be cumulative, and over the long haul, all those daily pennies and dollars you earn will add up." </p>

	
				
				
