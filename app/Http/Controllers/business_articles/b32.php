			monkeybusinessimages | Getty Images

					</div>
	
	<p>Dave Rusenko, the founder and CEO of <a>Weebly</a>, a hosting service for people to create and manage websites, says he starts his day a bit later than most with some quiet time.</p>
<p>“I usually start a bit later because I work a bit later. I start at 9 a.m. and start working around then and really just have some quiet time for an hour or two and work from home. Then I get into the office and work until 8 p.m.," he says. "The office is great. Most of my best work is done at the office. A lot of times I get to talk with people who have good ideas. But at home it's about catching up on emails, getting started with and laying the framework for what I hope to accomplish that day. Being at the office can be busier. Working at home is nice and quiet and a nice way to focus on in the morning to prepare for the day ahead.”</p>
		
	<h2 class="slides">2. Meditate.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629191928-GettyImages-594173143.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Mark Andersen | Getty Images

					</div>
	
	<p>Jerrod Blandino is the founder and CCO of <a>Too Faced,</a> which creates beauty products. Though he isn’t great with tech, he uses one app to help clear the mind.</p>
<p>“I just recently learned to start my day meditating with this app -- <a>1 Giant Mind</a>," Blandino says. "It’s helped me set myself off in a more focused way in the morning.… When it comes time to start taking care of everything, I’m more organized and clear.”</p>
		
	<h2 class="slides">3. Kick off your day with a small pleasure -- and make it a ritual.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192036-GettyImages-518018332.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			MarijaRadovic | Getty Images

					</div>
	
	<p>Melissa Bushnell, the founder of <a>Baked by Melissa,</a> a cupcake shop offering the dessert in bite-sized flavors, starts off her day with a tasty ritual.</p>
<p>“I drink a cup of Turkish coffee. I married an Israeli and when I started dating him, he introduced me to Turkish coffee," she says. "After that, I go to the gym. I wake up when it’s still dark out and I take pride in that because I’m a morning person. I do that to take care of myself and be the best version of myself that I can be. The way you start your day dictates the way the rest of your day is going to be. It's important for me to do things that make me feel positive, motivated and confident when I start.”</p>
		
	<h2 class="slides">4. Get connected from the moment you wake up.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192304-GettyImages-483422710.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			monkeybusinessimages | Getty Images

					</div>
	
	<p>Unlike those who meditate, Rich Pleeth does the opposite. As the founder and CEO of an application called <a>Sup</a>, which connects people to friends who are nearby, he gets going by checking his phone as soon as he wakes up and making sure he knows what’s going on online.</p>
<p>“I check all the news sites, my social feeds and do as many emails as possible while in bed," he says. "Then it’s up to the shower before heading to my first meetings.”</p>
<p></p>
		
	<h2 class="slides">5. Get a bird’s eye view of your schedule and day first thing.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192420-GettyImages-520224354.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			vgajic | Getty Images

					</div>
	
	<p>Damien Patton is the founder and CEO of <a>Banjo</a>, a news and social media content curation application. He starts the day by going into his personal “command center,” a separate room in his house with multiple screens and monitors for him to work before going into the office.</p>
<p>“I start the day by talking to the different teams and seeing how things are going, the metrics, etc.,” he says. “That way, when I start my meetings for the day, I have a full live view of where the day is beginning.”</p>
		
	<h2 class="slides">6. Play with your loved one.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192716-GettyImages-476715304.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Alija | Getty Images

					</div>
	
	<p>Jeff Chapin is the co-founder and chief product officer for <a>Casper</a>, a company that specializes in sleep by offering products including a “one-size-fits-all” deliverable mattress. He starts the day with his dog, Alta.</p>
<p>“I start by running with my dog -- around 6:30 or 7 in the morning," Chapin says. "I try best not to check my phone or email until I at least do that and have breakfast.”</p>
<p>He says the reason why he takes this time to unplug is that his default is to do many things at once and rush on to the next thing.</p>
<p>“Real life rarely allows us to get a full eight hours of sleep or an entire hour to run with the dog in the morning,” he says, “but that doesn't mean we can't use those six hours of sleep or 30 minutes to run and clear the head, right?”</p>
		
	<h2 class="slides">7. Drink lots of coffee.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192759-GettyImages-135862826.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Andy Teo aka Photocillin | Getty Images

					</div>
	
	<p>Jessica Dilullo Herrin is the founder and CEO of <a>Stella and Dot</a>, an online store for fashion merchandise. Herrin gets her day started by caring for her mind, body and soul.</p>
<p>But not without some coffee, <em>first.</em></p>
<p>“The very first thing I do is go to the kitchen to grab some coffee. But after that, I start my day with two habits: meditation and exercise,” she says. “I need strength in mind and body to be able to go do everything else my day requires, so I focus on self care. It’s like putting on my own oxygen mask. I know if I take care of myself then I’ll be able to take better care of everyone else.”</p>
		
	<h2 class="slides">8. Ignore the phone.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629192849-GettyImages-595910717.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Flavien Haas/EyeEm | Getty Images

					</div>
	
	<p>As an entrepreneur, consultant and author, Peter Gasca serves as an advisor for <a>Startup.SC</a>. He is also the founder of <a>Naked Cask</a>, a startup in the craft beer industry. Rather than go right to his device when he wakes up, Gasca gives his brain a chance to get the wheels turning.</p>
<p>“I am guilty of turning off the alarm on my phone and getting drawn into the notifications on my lock screen,” he writes in <a><em>Entrepreneur</em></a>. “All this does is immediately raise my stress level. Instead of grabbing your phone to check your email, calendar or updates first thing in the morning, opt to spend the first five minutes to wake up with your own thoughts." </p>
<p>He writes: </p>
<p>"Nothing that happened in the past few hours while you slept cannot wait just a few more minutes until you are mentally awake and prepared to deal with it. Now, I understand that most of us use your phones for our alarms. If you are unable to resist the urge to check your phone after you have turned off the alarm, do the smart thing and invest in a cheap alarm clock and move your phone to another room.”</p>
		
	<h2 class="slides">9. Become a morning person.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629194237-GettyImages-163053447.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			AdamRadosavljevic | Getty Images

					</div>
	
	<p>Tom Harari is the founder and CEO of <a>Cleanly</a>, an application that connects people to on-demand laundry services. He changed his ways and became a morning person.</p>
<p>“Each day follows the same pattern -- wake up at 6 a.m., review the previous day's KPIs over a strong cup of coffee, gym at 7 a.m., then office," he says. "I wasn't always a morning person, but getting up early definitely enforces a certain discipline which carries over into the rest of the day.”</p>
<p> </p>
		
	<h2 class="slides">10. Garden -- or do something that allows you to be in touch with nature.</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160629193034-GettyImages-459405181.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			monkeybusinessimages | Getty Images

					</div>
	
	<p>Josh Reeves is a co-founder and CEO of <a>Gusto,</a> an online support companyfor payroll and benefits. With a passion for introspection and wildlife, this entrepreneur wakes up early and heads to the garden.</p>
<p>“My schedule is waking up for sunrise. I usually spend the first 30 minutes in the garden," he says. "I find a lot of joy and simplicity in life from nature. It constantly grows without any of my input or contribution. It’s a reminder that the world is a lot bigger than anyone really envisions it," says Reeves.</p>
<p>"It also gives me a chance to clear my head. It’s very easy to become reactionary in startup life -- to become tactical and overcome whatever obstacle. But there’s always a million puzzles to figure out, so I need the opportunity to just detach and think -- to have my mind daydream. I like starting my day with that mindset. “</p>
				
				
