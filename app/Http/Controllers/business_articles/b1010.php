	<p>During summer 2014, New York City yoga studio owners Raquel Vamos and Dov Vargas created a rudimentary website for $35 to sell a plastic wheel they invented to help with deep back stretches. At most, they expected to get a few dozen orders for the $99 yoga prop from friends and students. But after a few months, they were selling up to 100 Dharma Yoga Wheels a day. After a year, they’d raked in $1.3 million and garnered Instagram raves from popstress Britney Spears and a number of NFL players. </p><p>But the couple knew this was hardly cause to rest on their laurels. They understood that one product does not make a thriving company. So Vamos quit her full-time nursing job and threw herself into the business. The team then hired a seasoned marketer to help keep the company in the spotlight, and got to work creating various yoga wheel upgrades and spin-offs, including an international yoga-teacher-training program based on their invention.</p><p>“You can’t get too stale with the business,” Vamos says. “You’ve got to constantly keep creating.” In fact, she says, the best time to research how to improve a product, create accessories for it and otherwise expand your business is the moment you launch it. Vamos hopes that mindset keeps the company moving forward, with the potential to bring in $1.7 million by the end of this year. </p><h2>Read more success stories about turning a hobby into a paying job <a>here</a>.</h2>

	
				
				
