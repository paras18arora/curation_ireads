	<p>At first, Rob and Renee Israel were simply creating popcorn concoctions in their small New York City apartment. As they mixed and matched popcorn combinations, they not only produced yummier flavors but healthier ones. So, in 2003, the duo started their own business, Doc Popcorn, and moved to the heartland of natural foodies: Colorado.</p><p>They began selling the snack in malls, stadiums and event centers and eventually started franchising in 2009. Today, it has 93 franchises in the U.S. and seven outside of the country.  </p><p>This has helped Doc Popcorn get ranked on Entrepreneur’s Franchise 500 list for 2016, holding solid at <a>No. 269</a>. on Entrepreneur’s Franchise 500 list for 2016, Doc Popcorn has establishments across the U.S. and around the world.</p><div> </div>

	
				
				
