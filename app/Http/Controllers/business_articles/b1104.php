	<p>Brothers and lifelong inventors Brett and Sven Newman didn’t want to aggressively grow their product design firm just for growth’s sake. Nor did they want their firm to get acquired. Instead, the pair grew Daylight Design carefully, focusing on the clients and projects that most appealed to them and maintaining a corporate culture with a family vibe. </p><p>For the Newmans, prioritizing growth above all else would have meant being less selective about the work they did and taking on more projects to feed the overhead beast. “Grow with intent,” Brett explains, “and you have more freedom to focus on the projects and clients that truly speak to you.”</p><p>That slow-growth strategy paid off. The Bay Area business hit the million-dollar annual revenue mark a year after its 2007 launch, and it now clears between $3 million and $5 million in annual billings. Clients include both mission-driven organizations like UNICEF, HopeLab and Back to the Roots, and household brands like Bosch, Instagram and Audi.</p><p><img alt="Daylight Design" src="https://assets.entrepreneur.com/images/misc/1465323820_brett-sven-newman.jpg" /></p><div class="courtesy">Photo Courtesy of Patrik Peterson</div><p>Growing mindfully is just good business, the brothers argue. In their company’s infancy, they had a significant six-figure opportunity to design disposable water bottles. After a long, hard think, they decided that helping add millions of plastic bottles to landfills wasn’t how they wanted to make their mark, and they turned the gig down.    </p><p>“Turning that down helped establish our values even more strongly,” Sven says. “Our reputation was made early on.” For the Newmans, that reputation has helped win over the kinds of customers they want to work with, and create a company they can be proud of. </p><h2>Read more success stories about turning a hobby into a paying job <a>here</a>.</h2>

	
				
				
