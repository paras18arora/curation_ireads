	<p>How can I start an effective mentorship program at my company?</p><p>As the longtime director of the Small Business Development Center at Kilgore College in Kilgore, Tex., Brad Bunt has watched plenty of entrepreneurs put mentorship programs to work -- with varying degrees of success. His three steps for getting it right:</p><h2>1. Identify your goals.</h2><p>What, exactly, do you aim to achieve through mentorship? “It generally comes down to retaining employees, improving employee satisfaction or increasing sales and productivity,” Bunt says.</p><h2>2. Be a careful matchmaker.</h2><p>This might seem like a no-brainer, but it’s not as simple as asking supervisors to mentor their subordinates. Figure out how prospective mentees learn, Bunt says, then find the right mentors for their learning styles. </p><h2>3. Keep a close watch.</h2><p>Set a timetable that allows things to really get going before you review the program. Bunt says six months is usually the sweet spot. “Once it’s established,” he says, “mentors get over some of the insecurities they had and see the benefits of the program.”   </p>

	
				
				
