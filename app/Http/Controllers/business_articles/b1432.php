<p>That's the concept for the Cordina Mar-GO-rita, a portable (and very drinkable) cocktail from 
<a>Big Easy Blends</a>
, a New Orleans company started in 2009 by Sal LaMartina and partners Antonio LaMartina and Craig Cordes. With distribution in only 13 states, the cocktail's monthly sales have already exceeded $100,000, and Sal projects annual revenue of $60 million in less than five years.</p>
<p>"We've got plans for GO-jitos, pi�a-GO-ladas and strawberry daiq-GO-ris," he says. "We're going to change the way people enjoy frozen cocktails."</p>
<p>That's right. Straight out of the freezer or pulled out of the cooler like a beer. And you'll avoid getting scolded at for having a glass poolside or on the beach, which is how LaMartina & Co. hit on the idea in the first place.</p>

	
				
				
