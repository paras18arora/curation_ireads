	<p>If a refreshment is what you’re after, try and stop by <a>Surf City Squeeze.</a></p><p>The smoothie and juice bar got its in 1988 start thanks to tri-athlete Kevin Blackwell and his wife Kathryn. It would soon after become known as Surf City Squeeze and in 1994 began franchising.</p><p>Expanding on the Arizona-based company, Blackwell went on to develop multiple other quick-serve franchises under the parent company Kahala, inspired by his favorite beach in Hawaii. Brands include Stone Creamery, Blimpie, TacoTime, Samurai Sam's, The Great Steak & Potato Co., Rollerz, Frullati, Ranch1, NrGize and Cereality.</p><p>Today, the company serves beach babes and surfer dudes with more than 100 locations with 90 percent of its menu consisting of smoothies. The franchise’s dedication to quality service and its wide variety of flavors from chocolate, blueberry, strawberry, banana and more, is most likely why it’s ranked as No. 488 on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
