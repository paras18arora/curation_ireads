	<p>Business owners ask me this question all the time, and with good reason. Outside audits are intense examinations of your company’s financial systems and controls, including sample testing and outside verification of many transactions on your ledgers. An outside auditor’s opinion will include assurance that your financial statements are in accordance with generally accepted accounting principles (GAAP) and that your business is viable and sustainable. They can cost tens of thousands of dollars and suck up an accounting department’s time for weeks at a stretch. Here’s when you’ll need one:</p><h2> </h2><p>If you’re courting investment or a buyout from major corporations and private equity firms, they’ll want to review three years’ worth of audited financial statements before they commit. Same goes for a bank looking to make a loan to you in excess of $1 million.</p><h2></h2><p>If you’ve sold your company to a private equity firm or taken out a sizable loan (congrats on that!), you’ll need to conduct an outside audit every year to reassure financial partners that your company is buttoned up and their investment is safe. That’s also true if you take your company public.</p><h2></h2><p>Depending upon your company’s credit history, a loan or a line of credit under $1 million may only require what’s called a review, where an outside CPA firm looks through your accounting systems to confirm everything is legit.  </p>

	
				
				
