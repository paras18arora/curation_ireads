	<p>It’s always exciting to try something new during your lunch break. With a new breakfast menu and a revolving door of new items -- including the Quesalupa -- the Taco Bell continues to stay relevant.</p><p>The founder, Glen Bell, originally came home from the Marine Corps when he was 23 and started a hot dog stand. But his real interest was in more alternative options, which is why he started selling tacos for 19 cents. Growing in popularity, he opened Taco Tia stands and in 1962, sold the brand and opened the first Taco Bell. Two years later, it became a franchise. Since then, the brand’s continued to evolve worldwide.</p><p>Its fight to stay current might be why it ranked No. 18 in Entrepreneur’s most recent <a>Franchise 500 list.  </a></p><p>Meanwhile, it might be time you go grab a Cheetos Crunchwrap Slider or two.</p> <div> </div>

	
				
				
