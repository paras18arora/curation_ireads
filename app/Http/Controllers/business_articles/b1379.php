	<p>Daily-deal sites, including instant powerhouses like Groupon.com, LivingSocial.com and Woot.com, were fast favorites of savings-minded shoppers. But the guys behind Washington, D.C.-based <a>Deals For Deeds</a> wanted to offer something more than savings, so their company puts a giving spin on all the buying: A minimum of 5 percent of every purchase is donated to a community nonprofit of the customer's choosing.</p>
<p></p>
<p>Inspired by the wild growth of the deal-site category, co-founders Harrison Miller and Josh Hoffman launched <a>DealsForDeeds.com</a> in April 2010. The D.C.-area natives are supportive of their community and wanted to help local organizations.</p>
<p></p>
<p>According to Miller, the outpouring of response from local businesses was immediate. The site promotes one deal a day and four causes each month. Deals For Deeds typically retains 30 percent of the purchase price; donations are made from that cut.</p>
<p></p>
<p>Miller calls Deals For Deeds a "cause-marketing campaign in a box." He says businesses like being affiliated with various charities and that the relationships formed remain long after the daily deal is done since company logos are featured on Deals For Deeds' Social Giving page. Customers are also active participants; they can vote for nonprofits and other organizations they'd like to see featured on the site.</p>
<p>Deals For Deeds currently works only with D.C.-based businesses and national companies with D.C. locations, but plans to roll out across the Mid-Atlantic this year.</p>

	
				
				
