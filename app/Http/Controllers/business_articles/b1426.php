	<p>Nichelle Shaw isn't shy about promoting her hand-crafted jewelry business online. The owner of Necessities by Nichelle, in Hyattsville, Md., has a website, a blog and a presence on two online marketplaces. But she avoids <a>Facebook</a> because of the privacy concerns she's heard about. "I didn't want some random thing or person to be able to affect my business," Shaw says. "It's too wide open."<br />
<br />
<img alt="" src="//assets.entrepreneur.com/article/complete-idiots-guide-to-facebook.jpg" style="margin-left: 5px; margin-right: 5px; float: right; width: 76px; height: 114px; " />That's a common misconception, says online marketing consultant and Entrepreneur columnist Mikal Belicove, author of <a><em>The Complete Idiot's Guide to Facebook</em></a>. Everyone should be concerned with privacy, he says, but business pages on Facebook have controls that let owners call the shots. "The business profile is not connected to the individual's profile unless the individual chooses to do so," Belicove says.</p>
<p>Consumers who are worried about sharing personal information with a business when they connect with it can change their privacy settings so that the business is not able to access that information. However, when users click the "Like" button on a page, everyone else who has done so (and all of their connections) will be able to see that. But Belicove says that is not a concern for the majority of people who connect with their favorite brands on Facebook.</p>
<p>"As a consumer on Facebook, I'm responsible for what comes out of my ‘mouth,' " he says. "Most of my information is within my power to share or not."</p>

	
				
				
