	<p>Embrace that sweet tooth with a scoop of Baskin-Robbins ice cream. With its parent company being Dunkin’ Brands Group, it’s no wonder this franchise earned the 29th spot in Entrepreneur’s <a>Franchise 500 listing. </a></p><p>The franchise itself has been around since the 1930s after Irvine “Irv” Robbins, a teenager at the time, started experimenting with different flavors while bored working at an ice cream shop.</p><p>Eventually, he and his brother-in-law, Burton “Burt” Baskin, got in on the business together after they pursued different ventures. For example, the number 31 was used at one of the first shops to encourage people to enjoy a different flavor each day of the month. After three years in business, there were six stores between them.</p><p>Once they joined forces, they reportedly flipped a coin to determine whose name would come first on the sign, which is how the name Baskin-Robbins came to be. By 1949, the brothers had more than 40 stores in Southern California.</p><p>Now the franchise has more than <a>1,000 ice cream flavors in 7,300 retail shops</a> across nearly 50 countries.</p> <div> </div>

	
				
				
