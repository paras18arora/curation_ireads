	<p>Even forward-looking tech companies find themselves getting nostalgic for the past every now and then. Because the industry moves so rapidly, items and logos from just a few years back can appear hopelessly outdated.</p>
<p>Below are 9 priceless relics that offer a peek into tech titans such as Amazon, Twitter and Google’s early days.</p>
<p></p>

		
	<h2 class="slides">1. The email that launched Airbnb</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223172754-1.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			Bill Gross | Twitter

							</a>
					</div>
	
	<p>Today, Airbnb is valued at more than $25 Billion. But back in 2007, it was just an idea. In a recent <a>Ted Talk</a>, cofounder Joe Gebbia shared the email he sent now-CEO Brian Chesky, which launched it all.</p>
		
	<h2 class="slides">2. An early Google server</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223151035-3.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Richard Strauss, Smithsonian's National Museum of American History

					</div>
	
	<p>Still on display at the Smithsonian, folks can go check out this early Google server for themselves. Lets just say there’s a reason this piece of technology sits in a museum: Back in 1998 co-founders Larry Page and Sergey Brin built 30 racks of servers with corkboard as a way to inexpensively maximize search capacity.</p>
		
	<h2 class="slides">3. Uber’s original logo</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223151145-4.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			Uber

							</a>
					</div>
	
	<p>In this case “back in the day” really just means 2011, but a lot has changed at Uber since then. For example, did you know the ride-hailing service used to be called Uber Cab? Back then, its logo wasn’t nearly as flashy and modern as it is now.</p>
		
	<h2 class="slides">4. The first book ordered on Amazon</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223151451-5.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			John Wainwright via Quora

					</div>
	
	<p>Computer scientist John Wainwright is perhaps best known for developing computer languages, but he has another claim to fame: In 1995, Wainwright ordered the first book ever sold on Amazon, Douglas Hofstadter's <a><em>Fluid Concepts And Creative Analogies: Computer Models Of The Fundamental Mechanisms Of Thought</em>.</a></p>
		
	<h2 class="slides">5. The first tweet</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223151714-6.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			Jack Dorsey | Twitter

							</a>
					</div>
	
	<p>It’s hard to remember what the world was like before Twitter. How did people get the news? Still, even Twitter cofounder Jack Dorsey had no clue what he was getting himself into when he sent out the first tweet in 2006.</p>
<p></p>
		
	<h2 class="slides">6. The first AOL instant message</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223152118-7.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			AOL

					</div>
	
	<p>Romance will never get old. It’s too bad AOL instant messaging did. But at least Ted Leonsis used the first message sent on the platform to express a sweet sentiment to his wife in 1993.  It read, "Don't be scared ... it is me. Love you and miss you." Her reply?  "Wow ... this is so cool!" Leonsis would eventually become AOL's vice chairman.</p>
		
	<h2 class="slides">7. The first item purchased on eBay</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223152244-8.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			eBay Newsroom | Vimeo

							</a>
					</div>
	
	<p>Online shopping sites like eBay repeatedly teach us that one man’s trash is another man’s treasure. This was true since the company’s earliest days in the mid-’90s. The first item sold on eBay, which was known as AuctionWeb at the time, was a broken laser pointer that went for $14.83. Apparently, the guy who bought it collected them.</p>
		
	<h2 class="slides">8. The first YouTube video</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160223152419-9.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			jawed | Youtube

							</a>
					</div>
	
	<p>For many of us, YouTube is how we consume a majority of online content. It’s crazy to think, then, that the first video was uploaded in 2005. Taken at the San Diego Zoo and featuring YouTube cofounder Jawed Karim talking about elephants, the video has been watched nearly 10 million times.</p>
<p> </p>
		
	<h2 class="slides">Portable MAC</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160303141651-portable-mac-computer.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			Rama | Wikimedia Commons

							</a>
					</div>
	
	<p>The ninth mission for Space Shuttle Atlantis was supposed to be tracking a satellite, but <a>sending emails</a> from one of the first portable MAC computers is way cooler. </p>
<p><a>According to IDG Connect</a>, the email read: </p>
<p><em>“Hello Earth! Greetings from the STS-43 Crew. This is the first AppleLink from space. Having a GREAT time, wish you were here,…send cryo and RCS! Hasta la vista, baby,…we’ll be back!”</em></p>
				
				
