	<p>An ever increasing number of independent contractors are making at least six figures.</p><p>There are going to be 3.8 million independent contractors by 2020 making more than $100,000 a year, <a>according to estimates from the self-employed professional service provider, MBO Partners</a>.</p><p>In 2015, 2.9 million full-time independent contractors were making more than $100,000. That is more than 16 percent of the 17.8 million full-time independent contractors in the U.S. in 2015, according to research from Herndon, Va.-based MBO Partners.</p><p>That means almost one in five full-time freelancers were turning six-figure salaries last year.  </p><p>The research was highlighted in <a>MBO’s recent report</a>, published in April, calling for a new federal worker classification for the burgeoning category of worker. It proposed the CSE, or Certified Self-Employed.</p><p><br />Taken together, the independent contractor workforce in the U.S. includes 42.1 million people, including both part-time and full-time freelance workers. The freelance workforce generates $1.15 trillion in economic activity, according to MBO.</p><p><a>The rise of the freelancer economy</a> has been a response in part to the proliferation of on-demand companies, such as Uber and Airbnb, which serve as a broker between people providing a service with people and companies demanding that service and the increasing preference of employees to be able to determine their own schedule.</p>

	
				
				
