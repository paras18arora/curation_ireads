	<p>Facebook Inc. said its customers' ads would now be visible on third-party apps and websites to everyone who has ever visited its website, and not just to users logged into its social networking service. </p><p>However, people can opt out of seeing ads on apps and websites not offered by Facebook, based on their ad preferences, the company said late on Thursday. </p><p>Facebook, like other online ad service providers, uses cookies to collect data on users' browsing habits to show them relevant ads.</p><p>The company, which has more than 1.6 billion users, offers online advertising services under its "Audience Network" business. </p><p>In the first quarter, Facebook generated more than 80 percent of its $5.20 billion ad revenue from mobile ads. </p><p>The company has been rolling out new features to ramp up mobile advertising and to encourage customers to experiment with video advertising.</p><p>(Reporting by Rishika Sadam in Bengaluru; Editing by Kirti Pandey)</p>

	
				
