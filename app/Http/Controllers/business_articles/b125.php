	<p>“I’ve never spoken about this publicly before. It’s hard,” <a>Sheryl Sandberg</a> said about her husband’s unexpected death one year and 13 days after the fact to the audience of graduates at this year’s commencement ceremony at University of California, Berkeley.</p>
<p>She went on to say, “I am not here to tell you all the things I’ve learned in life. Today I will try to tell you what I learned in death.”</p>
<p>The author of <a><em>Lean In: Women, Work and the Will to Lead</em></a> and COO of <a>Facebook</a> spoke with fierce clarity about the “<a>seeds of resilience</a>” that can become planted when hardship and devastation happens.</p>
<p>Critical to her ongoing recovery and perseverance are the findings of <a>Martin Seligman</a>, the founding father of the positive psychology movement and author of <em><a>Learned Optimism</a>, </em>whose insights into how people process setbacks helped Sandberg during her darkest days.  <em> </em></p>
<p>“I learned about the depths of sadness and the brutality of loss. But I also learned that when life sucks you under, you can kick against the bottom, break the surface, and breathe again,” she said. “I learned that in the face of the void -- or in the face of any challenge -- you can choose joy and meaning.”</p>
<p>According to Seligman, our ability to bounce back from life’s setbacks is mostly determined by the three P’s: personalization, pervasiveness and permanence.</p>
<p>Sandberg disclosed her own process using the three P’s -- explaining what they meant -- and how they impacted her healing.</p>
<p>Here are the powerful insights the 46-year-old <a>billionaire </a>shared during the commencement speech.</p>

