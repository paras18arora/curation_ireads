	<p><a>Personalization</a> is when you blame yourself for something negative that happens that is outside your power. Sandberg said she blamed herself for her husband’s death. She studied his medical records, wondering what she could have done to prevent his death and fell into a self-described “deep fog of grief” for months.</p>
<p>It wasn’t until she learned about Seligman’s three P’s that she realized what she was doing and was able to release herself from the blame.</p>
<p>“His doctors had not identified his coronary artery disease,” she stated. “I was an economics major -- how could I have?”</p>
<p>Sandberg went on to say getting over the hump of personalization can allow you not only to recover but thrive.</p>
<p><a></a></p>
		
