	<p>With pervasiveness, the Harvard business graduate <a>spoke</a> about the belief that an event will affect all areas of your life.</p>
<p>“You know that song ‘Everything is awesome?’” she said. “This is the flip: ‘Everything is awful.’ There’s no place to run or hide from the all-consuming sadness.”</p>
<p>She spoke of how the child psychologists she spoke to recommended she get her two young children back to their normal routine: going to school and her going back to work. She recalls going to her first meeting at Facebook “in a deep, deep haze.”</p>
<p>“All I could think was, ‘What is everyone talking about and how could this possibly matter?’” she said.</p>
<p>However, she spoke of a moment where she forgot her grief and getting drawn into the discussion.</p>
<p>“That brief second helped me see that there were other things in my life that were not awful. My children and I were healthy. My friends and family were so loving and they carried us -- quite literally at times.”</p>
<p>By focusing on her blessings, she was able to see that she was luckier than most mothers, who didn’t possess her financial resources and support network.</p>
<p>“That gratitude overtook some of the grief.”</p>
