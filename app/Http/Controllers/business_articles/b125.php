	<p>Permanence is the belief that the sad feelings or grief will last indefinitely.  </p>
<p>“For months, no matter what I did, it felt like the crushing grief would always be there,” she said.</p>
<p>She also spoke of how feelings can compound and her experience of what she described as “the second derivative of those feelings” -- feeling sad about feeling sad or anxious about feeling anxious.</p>
<p>“Instead,” she said, “we should accept our feelings -- but recognize that they will not last forever.”  </p>
<p>She was advised by her rabbi to “Lean into the suck.”</p>
<p>“It was good advice,” she said, “but not really what I meant by ‘lean in.’”</p>
<p>Resilience is learned, not a born trait, said Sandberg.</p>
<p>“Like a muscle, you can build it up, draw on it when you need it. In that process you will figure out who you really are -- and you just might become the very best version of yourself.”</p>
