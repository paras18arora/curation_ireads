<p> Nah. If you have the means, now is the time to hire or expand, says Kevin O'Connor, founder and CEO of <a>FindTheBest.com</a> and a serial entrepreneur who has successfully brought companies through three recessions, including DoubleClick.com (that one he eventually sold to Google for $3.1 billion). "People go into holding patterns because they're scared of running out of capital," he says. "And standards are much higher for VC money and at banks."</p>
<p>Where does that leave you? In one of two scenarios:</p>
<p>The nature of a business cycle means recessions come and go, so if you're flush with cash, you now have the opportunity to buy things cheaply. The companies in the best positions are the ones that have raised money all along--not just when they needed it--and stockpiled reserves. Lucky them.</p>
<p>Most likely, you're in Scenario 2: low on cash, and scraping. "I was a VC for a while, and most of the good stuff was by people who scraped," O'Connor says, adding that many startups are still launched with an initial investment of $25,000 to $50,000. "If you can get your product to a prototype state and hook some customers, you're still on the right track, no matter how gloomy the projections are."</p>

<p>O'Connor is practicing cash-flow savvy at FindTheBest.com, a comparison engine, by working out of a garage in Santa Barbara, Calif., looking for workers who will accept equity over a high salary, hiring interns (he has 10, plus 15 full-timers), asking employees to supply their own computers and cell phones and using cloud computing services to scale. "Be flexible," he advises, "and don't lock into anything right now."</p>
<p>Times may suck, but here's the thing: "You've done stuff to be smarter. You've been forced to be really productive and cut back on what you don't need. You are now an incredibly lean fighting group that knows how to handle the rough spots," he says. "And when you make it through, you'll have your best days ever."</p>
<p>And sitting on your hands? That may really drive you crazy.</p>

	
				
				
