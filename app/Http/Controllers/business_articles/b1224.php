	<p>Before you even see this franchise and its delectable treats, you will surely smell it.</p><p>Cinnabon is famous for its delightful aroma, a marketing ploy that gets us every time we are near one of their locations in malls across the country.</p><p>Founded in 1985, the company began franchising one year later, offering fresh and prepackaged baked goods including cinnamon rolls and “Pecanbons,” as well as coffee and other drinks.</p>With a high level of brand recognition Cinnabon earned the 55th spot on <em>Entrepreneur’s </em><a>Franchise 500 list</a> for 2016.

	
				
				
