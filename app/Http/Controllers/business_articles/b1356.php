	<p>Facebook and Twitter have lulled many businesses into thinking that blogging and posting content on social venues are the only means of stimulating dialogue on the web. But many businesses have substantial amounts of content on static web pages--from product and service descriptions to "about the staff" pages and photographs--that can be turned into customer talking points. The fix? Simply comment- or share-enable the pages.</p>
<p>Here are four good reasons you should consider opening your website content to comments:</p>
<ul>
<li>As users post comments, they generate fresh content for you--and that can help boost your ranking on search engines.</li>
<li>Comments give you a better understanding of your customers and site visitors. They can also inspire new or improved products and services.</li>
<li>Make it easy for users to recommend your site via Facebook, Twitter and LinkedIn and your site traffic could get a boost.</li>
<li>People like to feel that their opinion matters and that your business listens. They also like to see what others are saying about your business and what it offers. And, of course, they like to see what comments their comments attract, which makes your site open to more repeat visits.</li>
</ul>
<p>Comment-enabling static web content has never been easier, thanks to a number of powerful commenting tools:</p>

<div class="artsidebar" style="width:180px;">
<div class="head">Send it Instead</div>
If you're wary about opening your static website content to comments, at least let visitors share your content. Several tools enable you to add share features to static web pages, including:<br />
<br />
<a>Facebook's Send Button</a> lets users send a private message to friends on the site.<br />
<br />
<a>Facebook's Like Button</a> posts a link on the user's news feed.<br />
<br />
<a>ShareThis</a> posts a link on selected platforms, including LinkedIn, StumbleUpon, Digg, Facebook, Twitter, Google and Yahoo, as well as by e-mail.</div>
<p><a>Facebook's Comment Box plug-in for websites</a> makes it possible for people to post the comments they make on your website to their friends' news feeds on Facebook; the discussion thread remains synced across Facebook and your site.</p>
<p><a>Disqus</a> is a real-time comment system that notifies commenters when they receive a response and lets them carry on the conversation via e-mail or smartphone. Disqus is fully integrated with Facebook, Twitter, YouTube and even Flickr, allowing commenters to continue the discussion across platforms.</p>
<p><a>IntenseDebate</a>, another real-time commenting platform, notifies commenters when they receive a response and lets them carry on the conversation via e-mail. It also gives commenters the option to send a simultaneous tweet when posting a comment and share their comments with their Facebook friends.</p>
<p>All three tools are free and relatively easy for a skilled webmaster to install and configure. Each can be configured to notify key company personnel the instant someone posts a comment, so you can moderate and contribute to discussions in rapid fashion. In fact, you can even configure each one to hold comments for approval, thus avoiding potentially nasty dialogue from ever making it onto your site.</p>

	
				
				
