	<p>If you've ever freaked out because the boss walked by just as you were updating your OKCupid profile, consider yourself lucky.</p><p>Richard Branson, founder of the Virgin group, recently visited the Virgin Australia offices and found one employee napping on the couch. See below:</p><p><img alt="virgin blog" data-link="https://www.facebook.com/virginaustralia/" data-mce-source="Virgin.com and Virgin Australia" src="http://static6.businessinsider.com/image/575842b852bcd063018c7c50-2000/virgin-blog.jpg" /><a>Virgin.com and Virgin Australia</a></p><p><a>Boredpanda</a> originally spotted the photo, and Branson tells the story <a>on his blog</a> over at Virgin.com:</p><p style="margin-left:40px">"I popped into the office and the airport to say hello and check in to see what the team are up to. This guy wasn’t up to much at all – I caught him sleeping on the job! Wow, did he get a shock when I woke him up. He must have thought he was dreaming because he went straight back to sleep. To be fair, he was on standby, getting some much needed rest."</p><p>Perhaps snoozing in full view of the rest of the office when the company founder was due for a visit wasn't the best idea. But some psychologists and high-powered execs <a>support napping on the job</a>, provided you do it a bit more discreetly.</p><p>Arianna Huffington, cofounder and editor of <em>The Huffington Post</em> and author of <em><a>The Sleep Revolution</a></em>, said that if you don't have designated nap rooms in your office, you can ask an office manager to make sure that couches are placed in a relatively private spot.</p><p>"Then have your kit with your earplugs and your eye mask and you can lie on that couch," she <a>told <em>Business Insider</em>'s Rachel Gillett</a>. You can even bring a yoga mat to sleep on if you can find a private area, Huffington said.</p><p>Of course, regardless of whether management endorses napping at work, you should always try to get a solid night's rest, since <a>recent research</a> suggests skimping on sleep can sabotage your productivity.</p>

	
				
