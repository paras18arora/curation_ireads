<a><font size="4">Evernote</font></a>
<br />
In the old days (let's call them the '90s), jotting down a note required, well, actual jotting. Now there's Evernote, a free service that saves digital messages, notes and photos and indexes them for easy searching. Archive documents and texts for projects, snap photos at conferences, record meetings and, as the site says, "remember everything." Even if you don't want to.</p>
