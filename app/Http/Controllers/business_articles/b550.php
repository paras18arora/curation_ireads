					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Microsoft to Buy LinkedIn for $26.2 Billion" src="https://assets.entrepreneur.com/content/16x9/822/20160613134326-linkedin-microsoft.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Microsoft Corp. agreed to buy LinkedIn Corp. for $26.2 billion in its biggest-ever deal, combining the software giant's fast-growing cloud services business with the world's largest online network for professionals.</p><p>The offer of $196 per share represents a premium of 49.5 percent to LinkedIn's Friday closing price.</p><p>LinkedIn's shares were jumped 48 percent to $194.28 before the opening bell on Monday. Microsoft's shares were down 3.3 percent.</p><p>"Today is a re-founding moment for LinkedIn," Reid Hoffman, chairman of LinkedIn's board, said in a statement.</p><p>Jeff Weiner will remain chief executive of LinkedIn, reporting to Microsoft CEO Satya Nadella.</p><p>"I have always had a great admiration for LinkedIn," Nadella said in a video on Microsoft's website. "I have been talking with Reid and Jeff for a while. ... I have been thinking about this for a long time." </p><p>The deal is expected to close in 2016, the companies said in a joint statement.</p><p>Microsoft said it would issue new debt to fund the deal.</p><p>After the deal, LinkedIn will become part of Microsoft's productivity and business processes unit.</p><p>(Reporting by Supantha Mukherjee and Anya George Tharakan in Bengaluru; Editing by Saumyadeb Chakrabarty)</p>

	
				
