	<p>Those rays of sunshine are a waste if they can’t make their way through your windows. That’s where the <a>Window Genie</a> comes in, a franchise specializing in window-care services.</p><p>Though Richard Nonelle had no experience in the window-washing industry, he did have a degree in marketing and finance with a background in sales and management. So, in 1994, Nonelle founded Window Genie in Cincinnati, Ohio after he received advice from an old high school friend to go into residential window cleaning while members of his family pursued businesses of their own.</p><p>In 1998, the company began franchising and has since grown to offer full-service pressure washing, deck cleaning, house washing and concrete cleaning to the roster of services.</p><p>With more than 190 locations across the U.S., it’s no wonder Window Genie earned the ranking as <a>No. 170</a> on Entrepreneur’s Franchise 500 list.</p><div> </div>

	
				
				
