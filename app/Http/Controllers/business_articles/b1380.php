	<p>2011 could be called the year of the young franchise--the year when more than 10 percent of the top franchises in the nation, as determined in <a>Entrepreneur's Franchise 500®</a>, were less than 5 years old. And you'll find them all here in our <a>Top New Franchises list</a>. The list isn't intended to endorse any particular franchise--only your own research can determine whether a franchise, new or old, is right for you. Always investigate opportunities thoroughly by talking to franchisees, reading the company's legal documents and consulting with attorneys and accountants before investing.</p>
<p>The franchises in this list, all of which started since 2006, are listed in order of their Franchise 500® ranking. They're a varied lot, offering everything from chiropractic services to frozen yogurt, pet care to flip-flops. But they've all weathered in their formative years one of the toughest periods franchising has ever faced and proved that they can compete with the big boys.</p>
