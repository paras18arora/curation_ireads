	<p>As you can imagine, <em>Shark Tank</em> star <a>Kevin O’Leary</a> is beyond busy. The multi-millionaire mutual funds mogul says his phone rings “just about every 30 seconds” and his email inbox is overflowing, but he doesn't care. </p><p>“Not until I achieve the three daily goals that I set for myself the night before,” he recently told <em>Entrepreneur</em> from the Culver City, Calif. set of <a><em>Shark Tank</em></a>. “Each has to be accomplished before I take any inbound calls or send any emails, and I strictly follow that simple process daily, without fail.”</p><p>And without exception, barring a catastrophic emergency, as the 61-year-old entrepreneur put it. “Whether it’s family or business or something personal, I work on nothing else, whatever it may be,” he says. “My attitude is, until those three goals are done, nobody gets my time or attention.”</p><p></p><p>There’s a reason O’Leary, , a.k.a. “Mr Wonderful,” strips his morning to-dos down to three preset priorities: Like a fresh jolt of caffeine, it energizes him and supercharges his productivity. “I find it makes me very, very productive," he says. "I just avoid all the noise, check off the three tasks and move on with my day.”</p><p>He cautions that while it’s tempting to take on more than a trio of must-do tasks each morning, doing so often sets people up for failure. “Be realistic. Don’t overwhelm yourself," he says. "Three is enough.”</p><p><em>For more of O’Leary’s success tips, check out Shark Tank’s Season Eight premiere on your local ABC station. It airs on Sept. 23 at 9 p.m. ET. </em></p>

	
				
				
