<p><br />
A location-based mobile application and website, Broadcastr.com gives people the chance to share audio stories about the places that are important to them.</p>
<p>"We are creating a system where everybody together can create [a] living history that spans the entire world," says Andy Hunter, who launched the site in March 2011 with co-founder Scott Lindenbaum. Hunter says Broadcastr offers everything from "anecdotes, information [and] hilarious stories" to accounts of "very serious things," like the 9/11 memorial.</p>
<p><br />
Story submissions are growing by about 25 percent month-over-month, Hunter says. Pinning them to the places they're about makes it easy for listeners to access local tales--and has made the site a hit. Stories are served up to iPhone and Android app users based on the listener's location. "As you move through the world, that movement becomes your search query," Hunter says. "It's like an audio tour of the world that is generated by everyone."</p>
<p><br />
Hunter and Lindenbaum wanted to tie location-based mobile technology with compelling stories. Hunter says his "aha" moment came while watching a guy pin flyers to telephone poles in his hometown.</p>
<p>"I realized at that moment that what we really should do is give anybody the ability to create content [about the] place that's significant in their lives," Hunter says, "so you can tell the story about the place where you proposed, or the house you grew up in, or if you're a historian, revitalize a place lost to history."</p>
<p><br />
Hunter says the Broadcastr crew is looking at several means of generating revenue, starting with content sponsorships. The program will allow partners to pay for featured placement on the Broadcastr system. He says the company also offers paid production services to storytellers.</p>
<p><br />
Users will be able to purchase premium content from Broadcastr's partners. One partnership will eventually offer listeners location-based audio travel guides.</p>

	
				
				
