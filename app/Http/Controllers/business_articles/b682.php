					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Twitter Met With Yahoo to Discuss Merger" src="https://assets.entrepreneur.com/content/16x9/822/20150806182738-twitter-social-media-tweet-bird.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>The list of companies looking at a possible deal with <a data-gdsid="37243" data-inline-quote-symbol="YHOO">Yahoo</a> included the social media company <a data-gdsid="145284" data-inline-quote-symbol="TWTR">Twitter</a>, according to a new report from <em>The New York Post</em>, which claims the management teams of both discussed a possible merger but the talks ended without an agreement.</p><p>The report, citing sources close to the talks, said execs at the two firms had met several weeks ago and had spent several hours looking at whether a strategic combination might make sense.</p><p>"Twitter is the destination for instant news, and Yahoo has a lot of eyeballs on its site," one source told the newspaper. "The idea isn't as crazy as you might think."</p><p><em>The New York Post</em> also reported that Twitter CEO Jack Dorsey was not present and the sources said Twitter appeared "mainly interested in sucking information out of Yahoo, as it bowed out of the bidding process soon thereafter," sources said. </p><p>A Yahoo spokesperson told CNBC that it would not comment on details of the ongoing process, with the company currently looking at possible transactions which are in the best interests of the company and its shareholders. A Twitter spokesperson told CNBC that it does not comment on "rumor and speculation."</p>

	
				
