	<p>After a rough day, sometimes it’s acceptable to simply veg out on the couch in your sweats with a container of ice cream. Companies like Ben & Jerry’s understand -- and we thank them for it.</p><p>The Ben & Jerry’s franchise got its start in 1978 with just a $5 course on how to make ice cream, a stint at Penn State and a $12,000 investment.</p><p>The pair opened their first parlor in a renovated gas station in Burlington, Vt. To celebrate their first anniversary, the duo hosted the company’s first Free Cone Day, where customers were given a free scoop of ice cream. After Ben & Jerry’s began franchising in 1981, the pair continued to carry on this traditional, holding an annual Free Cone Day event at its shops around the world.</p>Today, the franchise is owned by Unilever and ranked as<a> No. 299 </a>on Entrepreneur’s Franchise 500 list for 2016.

	
				
				
