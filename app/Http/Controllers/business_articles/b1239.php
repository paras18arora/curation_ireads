	<p>In the early 1990s, Gordon Logan, the founder of Sport Clips, got the idea to provide discount haircuts to an underserved market: men and young boys. Opening his first store in 1993, Gordon’s goal was always to offer “championship haircut experience in an exciting sports-themed environment.” Two years later, Sport Clips became a franchise, opening up a shop in Houston, Texas.</p><p>It took five years to open up the first 50 stores, but Sport Clips has gained momentum over time. It now has more than 1,300 locations across the U.S. and Canada. This has helped the franchise snag the 16th spot on Entrepreneur’s<a> Franchise 500 list</a> for 2016.</p><div> </div>

	
				
				
