	<p>Let your garden bloom with the flowers and plants from <a>Flower Tent.</a></p><p>Founded by Tom Ansilio alongside his father, the business got its start in 1992 in Wyoming, Pa. Offering a variety of flowers and plants for the outdoors, it didn’t take long for Flower Tent to pursue a second location in Kingston, Pa.  -- especially after the first one had an Easter for the record books.</p>Over time, the franchise grew to 11 locations and his brother Tim eventually joined the team, making it a family affair. Today, Flower Tent has more than 80 locations across the United States and is ranked as <a>No. 319 </a>on Entrepreneur’s Franchise 500 list.

	
				
				
