					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="McDonald's Plans to Roll Out a Rewards Program For Customers" src="https://assets.entrepreneur.com/content/16x9/822/20150515174523-mcmuffin-mcdonalds-fast-food.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Buy five Egg McMuffins, get the sixth free?</p><p>Something similar may soon be coming to McDonald’s. The franchise announced it will debut a rewards program, which will be featured in McDonald’s existing smartphone app, later this year or in early 2017, according to Nation’s Restaurant News. In some markets, McDonald's already offers a reward system for its McCafe beverages.</p><p></p><p>The new, expanded rewards program will encourage customers to buy breakfast food items, which McDonald’s has been pushing since adding to selections such as the Chicken McGriddle and offering the menu all day.</p><p>It’s possible that the update serves as a strategy for McDonald's to keep up with competitors, including Dunkin Donuts and Starbucks. Both already offer loyalty programs.</p><p>“We’re working on a customer-designed loyalty program that we think will be as good as there is out there in the marketplace,” McDonald’s USA president Mike Andres says, according to <a>TIME. </a></p>Andrews said rewards will more than likely be earned based on number of visits to the restaurant each month. Once enough points are racked up, customers will have a limited time to claim the reward.

	
				
				
