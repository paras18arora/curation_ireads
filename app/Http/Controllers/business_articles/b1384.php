	<p>Choosing a desktop printer for a smaller business used to be an easy call. For heavy users, a laser printer, which cost thousands, made sense because cost per page was just pennies. Those with lighter printing demands usually chose an inkjet imaging device, which today costs less than a good business lunch. But watch out: Some inkjet prints can run as much as a dollar per page, a recipe for losing money fast.</p>
<p>Well, no more. New technologies and dramatic changes in ink pricing are blurring the line between laser and inkjet printing. Here are some nuggets of value in desktop inks and imaging:</p>
<p><span style="font-size:16px;"></span><br />
<br />
For the price of a decent-quality laser printer, Xerox brings a new idea: <a>solid ink-based printing</a>. Instead of spraying ink on a page like inkjets do or burning in the image with a laser, solid ink works like a giant printing press, by rolling a microscopic layer of color onto the page. The result is a deep, rich, highly professional printing look. Costs are reasonable by industry estimates: 3 cents per page for black and white, and 13 cents for color. One catch: No cartridges. This means less environmental impact, but no low-cost inking alternatives. A Xerox solid ink printer means Xerox solid inks.</p>
<p><span style="font-size:16px;"></span><br />
<br />
The rush to low-cost, refillable printer cartridges has led to one great benefit: Even giants like HP have had to figure out how to get cheap, high-quality inks on the market. Take the HP 60 Black Ink Cartridge. Standard versions sell for $15, which prints about 200 sheets for a not-cheap 8 cents per page. But HP quietly sells a higher-capacity version of the same cartridge, the <a>HP 60XL</a>. This cartridge prints 600 pages and sells for $35, which lowers the price to about 6 cents per page. My Print Rewards, HP's rewards program, can lower prices even more. Considering that a good inkjet small-business printer now costs less than $100, there is real ROI in HP Inks and printers.</p>
<p><span style="font-size:16px;"></span><br />
<br />
Refillable cartridges offer solid image quality, don't jam most machines and major retailers like <a>Walgreens</a> and TigerDirect.com are now in the business. Simply head to a participating Walgreens and, for $13, you can have your cartridges refilled, black or color. You might not want to use these inks for your best-quality jobs (or your résumé). But for internal printing and imaging, what's not to love?.</p>

	
				
				
