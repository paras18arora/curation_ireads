	<div><div><div><div><p>According to<em> <a>Bloomberg</a></em>, Twitter is prepping an update to its service that will keep photos and links from counting against the 140-character limit of a post. Currently the URL that points to images and sites counts against the space available in tweets.</p></div></div></div></div><div><div><div><div><div><p>The new feature is expected to launch within two weeks <a>according to <em>Bloomberg</em>'s</a> anonymous source. Part of the attraction of Twitter is that it limits the amount of information shared in each post. An early version of the service relied on SMS so limiting the characters to 140 was more of a technical issue than anything else.</p><p>But, long-time users have asked that links, photos and usernames not count against the character limit. It looks like at least two of those items will no longer suck up valuable tweet real estate.</p><p>Twitter has been on <a>feature-adding spree lately</a> as it tries to <a>attract new users</a>. But it has stopped short of <a>creating a purely algorithmic timeline</a> like that found on Facebook. Instead, it's <a>enhanced DMs</a> and surfaced what it believes are the important tweets to the top of users' feeds.</p><p>We have reached out to Twitter about this upcoming feature and will update this article when it replies.</p></div></div></div></div></div>

	
				
				
