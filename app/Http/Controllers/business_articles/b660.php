	<p>The man who created the technology behind status updates and user check-ins on social networks told CNBC he was tinkering around for the next big idea that could change the world.</p><p>No, it's not Mark Zuckerberg. </p><p>Neeraj Jhanji created a mobile social network called ImaHima in 1999 that could send users status and location updates from their friends via mobile phones. Back then, social networks were in their nascent stage and Zuckerberg was still in high school.</p><p>Jhanji got the idea on a Saturday morning in <a>Tokyo</a>, walking towards a crowded neighborhood train station. He wondered if any of his friends were nearby to join him for lunch. </p><p>"I remember taking the phone out of my pocket, looking at it, and thinking: the phone knows where I am... it also knows where my friends are. So why doesn't it tell me [if anyone's nearby]?" he told CNBC's "<a>Capital Connection</a>" on Thursday.</p><p>ImaHima gained popularity after its release, but never conquered the U.S. market, and as social networks and the mobile ecosystem evolved with the emergence of key players including Facebook, Apple, Google and Twitter, ImaHima fell out of the picture.</p><p>But Jhanji's foresight saw him patent the technology used to create ImaHima as early as 1999. When <a data-gdsid="139619" data-inline-quote-symbol="FB">Facebook</a> turned its sights onto the mobile platform, Zuckerberg's company "came knocking, interested in the patents," said Jhanji.</p><p>Facebook acquired the patents from Jhanji in 2013 for an undisclosed sum. Jhanji said he was not allowed to speak about the details but added, "[Facebook] got a very good deal." </p><p>Now, Jhanji said, he was looking around for a new project that could create an entirely new industry. For that, he created a company called Tinker that has offices in Silicon Valley and Singapore. </p><p>"We're trying to come up with new innovations that could be the start of something."</p><p>The company already has several products out. One of them -- Pasteasy -- lets users copy and paste documents, files photos and videos from one mobile device to another over WiFi.</p>

	
				
