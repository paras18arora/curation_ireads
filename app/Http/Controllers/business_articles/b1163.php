	<p>There are no cashiers, servers or meat products at Eatsa -- but there are crowds eager to experience the future of fast food. Cofounder and CEO Tim Young launched two outposts -- one in San Francisco, one in L.A. -- of his fully automated eatery last year, with more locations on the horizon. He gave us a tour (by phone, natch).</p><p> “We want to provide as many cues as possible that the food is custom -- this isn’t a vending machine,” Young says. “So the menu skews toward personalization: Do you want to build your own? Do you want to add avocado or leave anything off?” </p><p> Don’t know where to find the napkins? Forgot to order your meal without cilantro? Look to the “Eatsa concierge,” the lone human tasked with interacting with customers. “Some people want to be reassured, and the concierge is there for that.”  </p><p> While some fast-food spots move toward an open kitchen, Eatsa’s is entirely hidden. “We’re trying to keep the back of the house a mystery.” At any time, roughly half a dozen humans are prepping quinoa bowls.</p><p> Food is served through a wall of cubes. “There’s a short animation and then the food is unveiled, with the diner’s name sort of floating over it. It drives home the idea that the order is personalized to them.”</p><p> Young and his cofounder, Scott Drummond, spent two years collecting and analyzing data on consumer taste preferences and engineering dishes that would best the usual fast-food staples. The results: eight quinoa-based bowls, with flavor profiles ranging from Mexican to Mediterranean to Indian, for $7 a pop.</p><h3>Check out more companies on the <a>2016 Brilliant 100 list</a>. </h3>

	
				
				
