	<p>There’s only one way to describe the grand opening of a new McDonald’s location in Kazakhstan: great success! </p><p>On Tuesday, the fast-food franchise added the Central Asian nation to the growing list of countries it serves.</p><p>Many people had never even heard of Kazakhstan until the 2006 release of the satire film <em><a>Borat,</a></em> which featured comedian Sacha Baron Cohen as the titular character.</p><p><a></a></p><p>Kazakhstan originally banned the “Borat” until it discovered the film led to a major increase in tourism. The oil-rich country is currently in need of an economic boost, thanks to sinking oil prices. To a bid to combat this slump, it has actively courted franchises, <a><em>The New York Post</em> reports</a>.</p><p>Which helps explain why the opening was so well received by locals. A huge crowd, including President Nursultan Nazarbayev, gathered to check out the 9,000 square-foot, $3.5 million restaurant that features more than 200 seats, self-order kiosks and a drive-thru.</p><p><a></a></p><p>In December, Starbucks and French supermarket Carrefour also opened locations in Kazakhstan.  </p>

	
				
				
