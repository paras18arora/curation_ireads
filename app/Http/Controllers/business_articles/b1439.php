	<p><img align="left" alt="" border="0" height="300" hspace="5" src="/i/images/mg/amy-cosper.jpg" title="" width="200" />Admit it: Business happens in bars. You've done it. And if you haven't, you should. You do business with people, and bars let people be people.</p>
<p>Actually, a lot of business happens in bars. Not just the Gordon Gekko-sized transactions--though, it turns out, bars are where the producers of the upcoming Wall Street sequel got some of their juiciest material. 'We got the best stuff for the film over drinks," Eric Kopeloff says about drinking with actual Wall Street honchos in 
<a>'Where are you drinking?</a>
". That is, 'after breaking the ice with a personal question."</p>
<p>And it's true: The comfort factor can't be overestimated. Bars set the stage for relaxed, open conversations. The kind of conversations that lead to a brilliant new idea, a real partnership, a deal. The idea for our 
<a>special report on business in bars</a>
was conceived, you guessed it, in a bar.</p>
<p>Yet the comfort factor also makes bars dangerous. You're far from the clear rules and confines of the office. Plus, you're drinking. The same things that make bars great for business can also make them a disaster for business.</p>
<p>So we decided to draft 
<a>our own rules</a>
, and for that we consulted the Macdaddy of Bar-dom at Esquire: Ross McCammon. Throughout the report, you'll find his strategies for doing business in a bar--how to make a powerful impression, and stay sober while you're at it.</p>
<p>With the help of experts across the country, we put together the ultimate, coast-to-coast guide to the best bars for doing business. Not the generic hotel bars that any out-of-towner could find, but the places where the city's entrepreneurial elite hang--whether it's a quirky bar near MIT crawling with biotech geniuses or a palatial enclave in Midtown Manhattan, well removed from the tourist throngs.</p>
<p>Finally, we asked cocktail guru David Wondrich to name the 
<a>country's worst bar for doing business</a>
--and curiously enough, a really bad business bar is sometimes what you need to gauge the character of the guy across the table.</p>
<p>Because in a bar, you can tell, really tell, who you're dealing with. And you can more easily detect the character of a deal, too. So forget navel shooters and Jaeger. It's a long-standing tradition to do business in a bar.</p>
<p>At Entrepreneur, we love business. And we love bars. When the two collide, it is serious business, indeed.</p>
