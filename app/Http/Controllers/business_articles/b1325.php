	<p>What makes entrepreneurs tick? LinkedIn combed through more than 120 million public profiles to pull stats on entrepreneurs and compare them to the average member. Some common threads from the study released in September include:</p>
<p></p>
<ul>
<li>Top five business schools: Stanford, Harvard, MIT, Berkeley and Dartmouth.</li>
<li>Companies that breed entrepreneurs: Adobe, Apple, eBay, Electronic Arts, Google, Microsoft, SGI and Yahoo.</li>
</ul>
<p></p>
<ul>
<li>Top majors: entrepreneurship (duh), computer engineering, computer science, physics and electrical engineering.</li>
<li>Technical majors (except civil engineering) are overrepresented among founders. Nursing and administration are underrepresented.</li>
</ul>
<p></p>
<ul>
<li>65 percent of entrepreneurs are 30 and older at their first startup</li>
<li>Only 2 percent are serial entrepreneurs</li>
</ul>
<p></p>
<ul>
<li>Most heavily populated entrepreneur cities: San Francisco, Boston and New York.</li>
<li>Most startup founders who list higher education as their previous industry are in nanotech, biotech and medical devices.</li>
</ul>

	
				
				
