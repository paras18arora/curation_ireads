	<p>If you’re looking for a new way to give back, you share the same goal as <a>Touching Hearts at Home.</a></p><p>Launched in 1996, the organization was founded by Renae Olafson, who grew up in the family responsible for starting franchise Merry Maids. As a franchisee herself for 10 years, Olafson was more than ready to start the non-medical home care company in Minnesota.</p><p>Starting to franchise in 2007, the business offers personal and companionship care, meal prep, transportation and help with medication or Alzheimer's and other dementia care services.</p><p>It’s positive and selfless mission is most likely why is now has more than 50 locations and is ranked as No. 363 on Entrepreneur’s Franchise 500 list.</p>

	
				
				
