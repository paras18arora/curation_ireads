	<p>There are many subscription-delivery success stories, like BarkBox (a monthly shipment of dog treats) and BirchBox (for makeup samples). Dallas couple Blake and Taylor Davis tried to follow with The Jerky Box, promising handcrafted beef jerky from three vendors each month. Eight months later, it was dead. Time to debrief.</p><h2></h2><p><br />The business relied heavily on the jerky companies -- seeking them out, explaining why it was smart to sell us x amount of their jerky at wholesale. There were not enough craft jerky companies to make it work.  </p><p><br />We had a couple months where we had to send three bags of jerky from the same company to our subscribers. We could’ve gotten 10,000 subscribers, but without having enough jerky companies to consistently supply us, it was no good.</p><h2></h2><p><br />We thought about adding nuts or different specialties from other artisanal companies, but that would have added to the shipping, cutting into our profits.</p><h2></h2><p><br />Our margins were pretty low. The cost of shipping was never consistent. If we had a customer in Maine versus somebody local, the shipping ranged from $3 to $8. We were pretty much just breaking even. We couldn’t scale it. So in summer of 2014, we realized the business was way too much work for what we got out of it.</p><h2></h2><p><br />We didn’t want somebody else’s input or product to define our success. That’s why we moved into the service model, so we could be in full control of our success.</p><p><br />Now everything we do to make a profit comes down to us.  </p>

	
				
				
