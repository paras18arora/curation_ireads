<link rel="stylesheet" type="text/css" href="abc.css"><meta http-equiv="Content-Type" content="text/html;charset=utf-8" /><h1>Majority of U.S. Adults Get News From Social Media</h1>
				
					<img itemprop="image" itemscope itemtype="https://schema.org/ImageObject" alt="Majority of U.S. Adults Get News From Social Media" src="https://assets.entrepreneur.com/content/16x9/822/20160118173745-newspaper-route-delivery-job.jpeg" rel="share" />

					
							
								
				
				
										
					
	
	<p>Sixty-two percent of U.S. adults now get news from social media sources, according to a Pew Research <a>study</a> published yesterday. Reddit has the largest number of users who say they get their news on the site at 70 percent, followed by Facebook users at 66 percent and Twitter at 59 percent.</p><p>The numbers drop off from there, with just 31 percent of Tumblr users and 19 percent of LinkedIn users saying they get news from those sites. Pew also found that a majority of people (64 percent) get news on just one social media site -- most commonly Facebook.</p><p>The study was conducted from Jan. 12 to Feb. 8, 2016, and surveyed 4,654 participants about their online news habits. Many of its questions were related to specific platforms and news discovery methods.</p><p>"YouTube, Facebook, and Instagram news users are more likely to get their news online mostly by chance, when they are online doing other things," the survey found. Reddit, Twitter, and LinkedIn users, however, are more likely to seek out news online.</p><p>Facebook has rapidly solidified its status as a news source. Three years ago, <a>another Pew study</a> found that 47 percent of its users got news from the social networking site, representing approximately 30 percent of the U.S. population.</p><p>At its F8 developers conference last month, Facebook introduced an <a>expansion of its Instant Articles feature</a>, which reduces the load time of articles posted by participating publishers. Facebook also curates its own "Trending" section for breaking news, though that has been <a>criticized</a> for prioritizing politically liberal topics.</p><p>Twitter has also made efforts to boost its news content. It now classifies its mobile app in the "<a>news</a>" category of the iOS App Store, instead of its previous home in the social media category. Since publishers self-identify the category in which their app is listed, the move was likely intended to boost Twitter's App Store rankings.</p>

	
				
