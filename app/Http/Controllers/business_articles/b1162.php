	<p>Storm-water runoff is theleading cause of water contamination in urban areas, but tracking it has been a clunky, pen-and-paper process. Erin Rothman and Anya Stettler built a better way: Their platform tracks water quality in real time with waterproof hardware and cloud-based software. “We want to help cities spot and fix problems before they get out of hand,” Rothman says. Prior to getting funding from the 9Mile Labs accelerator in Seattle, the duo designed and built the Stormsensor prototype for $2,000. Here, their secrets to seriously bootstrapping a business.</p><p> Rothman has an environmental background; Stettler’s is software. They knew they needed electrical expertise, so “I googled ‘electrical engineer University of Washington water,’” Rothman says with a laugh. “I asked one of the professors for 30 minutes, and we wound up talking for three hours.”</p><p> “When I talked to people in marketing, manufacturing, design -- basically everything -- I was completely honest,” says Rothman. “‘This is our crazy, cool idea and we’d love for you to be involved, but we don’t have any money, and we won’t have any money for a long time.’” Some people bailed, but plenty stayed. Even the team’s law firm deferred payment until Stormsensor makes its first half million. </p><p> For nearly a year, Stettler and Rothman worked full-time elsewhere while building Stormsensor. “Focusing on really short-term goals helped me get through those periods of feeling like I can’t do any more,” Stettler says.</p><p>“Whatever we did have to pay for, I made sure we negotiated down to the lowest possible number,” says Rothman.</p><p> “We can have six meetings a day for two days straight where everyone says it’s the stupidest idea ever,” says Rothman. “But we still know we’re right. My job is to make sure everyone sees the beauty in what we’re doing -- even if later I might be curled up in a ball, crying.”</p><h3>Check out more companies on the <a>2016 Brilliant 100 list</a>. </h3>

	
				
				
