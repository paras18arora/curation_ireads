	<p>We’ve all been there. Walked miles to check out different tourist destinations, sported dressy shoes to a high-end function or gone too hard in a backyard game -- all of which have made our feet howl in pain. Fortunately, <a>Good Feet Store</a> has you covered.</p><p>Founded in Solana Beach, Calif. in 1995, the franchise provides solution for those arch aches and pains as a shoe store with different shoe options, support, inserts and other accessories.</p><p>Having begun franchising in 2003, each location’s employees work to help customers find the right support based on foot size and lifestyle, with 25 customizeable options (shoe or insert type) to choose from based on your type of foot. It’s dedication to service might be why the company is ranked as No. 475 on Entrepreneur's Franchise 500 list.</p><div> </div>

	
				
				
