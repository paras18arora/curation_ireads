	<p>Sometimes all you need is a nice hearty sandwich. When the hunger strikes, try out <a>Which Wich Superior Sandwiches</a>.</p><p>Founded in 2003 by Jeff Sinelli, the first location opened in Dallas. It’s specialty sandwiches not only caught the attention of hungry customers but also the media, being named “The Best Sandwiches” in Dallas by <em>D Magazine </em>a year later. By 2005, the business started to franchise with 50 locations by 2008.</p><p>The service allows consumers to make their own sandwiches or pick from already existing delectables. Either way, the business makes sure each experience is customized for each customer.</p><p>Now with more than 300 locations, Which Wich Superior Sandwiches is ranked as <a>No. 262 </a>on Entrepreneur's Franchise 500 list.</p><div> </div>

	
				
				
