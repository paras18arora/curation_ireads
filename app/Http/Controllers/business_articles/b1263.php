	<p>Have you ever heard of Taco Bueno?</p><p>Apparently the food chain with its “Muchachos,” the franchise’s signature burrito, is the most popular Tex-Mex chain in America, beating out Chipotle and Taco Bell, according to <em><a>Vice's Munchies.</a></em></p><p></p><p>The claim is based on a quick service restaurant survey of 10,000 people launched by industry research group<a> Market Force Information</a>.</p><p>According to <a><em>People</em></a><em>, T</em>aco Bueno has 178 locations in seven states, all of which are in the south or west: Louisiana, Texas, Arkansas, Missouri, Kansas, Colorado and Oklahoma. Its menu features the Wholotta platter, its largest option with a beef chilada covered with chili and queso, a beef taco and Muchacho, a side of queso in a tortilla chip cup, rice, refried beans with sour cream, guacamole and chips. If you’re able to take on the monstrous platter, you’d consume a whopping 2,500 calories.</p><p></p><p>Founded in 1967, its fans call themselves the “Buenoheads.” </p><p>Taco Bueno's popularity could also be a result of some of the recent struggles big burrito competitor Chipotle has had in the past year. Chipotle suffered a number of E. coli outbreaks and has struggled to regain consumers' trust.</p>

	
				
				
