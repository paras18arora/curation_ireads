	<p>In securing the Democratic presidential nomination this week, Hillary Clinton made history, becoming the first women to ever achieve that feat in the United States. It is the latest in a long line of watershed moments for women. It now seems appropriate to take some time to acknowledge a few of the other fearless females who broke down barriers.</p>

		
	<h2 class="slides">1. Jeannette Rankin</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609180952-1.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			New York Times Co. | Getty Images

					</div>
	
	<p>One could say that Rankin paved the way for Clinton. After all, it was Rankin who was the first woman to ever be <a>elected to the United States Congress</a> in 1916, years before women won the right to vote.</p>
		
	<h2 class="slides">2. Frances Perkins</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609181056-2.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Keystone-France | Getty Images

					</div>
	
	<p>In 1933, Perkins moved the goalpost for women in politics by becoming the<a> first woman appointed to the presidential cabinet</a>. President Franklin Roosevelt appointed her as his secretary of labor.</p>
		
	<h2 class="slides">3. Katharine Graham</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609181213-3.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			The Washington Post | Getty Images

					</div>
	
	<p>Graham became the <a>first female CEO on the Fortune 500 list</a> in 1972, when she was at the helm of the Washington Post Company.</p>
<p></p>
		
	<h2 class="slides">4. Sandra Day O'Connor</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609181420-4.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Bill Clark | Getty Images

					</div>
	
	<p>O’Connor made her mark in the third branch of government when she was sworn in as the <a>first female Supreme Court justice</a>. In 1981, President Ronald Reagan gave her a lifetime spot on the bench of the highest court in the country, and she served until retiring in 2006.</p>
		
	<h2 class="slides">5. Sally Ride</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609181522-5.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Space Frontiers/Stringer | Getty Images

					</div>
	
	<p>Ride aimed for the stars and then secured her place among them when she became the <a>first woman from the United States to go into space</a> in 1983.</p>
		
	<h2 class="slides">6. Aretha Franklin</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609181813-6.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Pool/Pool | Getty Images

					</div>
	
	<p>Franklin got the "respect" she deserved in 1987 when she was the <a>first woman inducted</a> into the Rock and Roll Hall of Fame.</p> 
<p></p>
		
	<h2 class="slides">7. Oprah Winfrey</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609182239-7.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Jim Spellman | Getty Images

					</div>
	
	<p>Winfrey is a household name, and the recognition is well deserved for her accomplishments. Not only is she known as the first woman to <a>own and produce her own syndicated talk show</a>, but she also became the first African-American celebrity to be <a>featured on the cover of <em>Vogue</em></a> in 1998.</p>
		
	<h2 class="slides">8. Mona Van Duyn</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609191144-8.jpeg" />
			</div>
			<div class="credit">
			Image credit:

							<a>
			
			Library of Congress

							</a>
					</div>
	
	<p>Van Duyn became the <a>first female poet laureate</a> of the United States in 1992, proving that the pen is mightier than gender barriers.</p>
		
	<h2 class="slides">9. Kathryn Bigelow</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609182532-9a.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Jason LaVeris | Getty Images

					</div>
	
	<p>When she won her Academy Award in 2010, Bigelow became the first woman to take home <a>an Oscar in the Best Director category</a> for her work on <em>The Hurt Locker</em>.</p>
<p></p>
		
	<h2 class="slides">10. Jen Welter</h2>
	<div class="hero">
		<img class="slide" src="https://assets.entrepreneur.com/slideshow/20160609183218-10a.jpeg" />
			</div>
			<div class="credit">
			Image credit:

			
			Ralph Freso/Stringer | Getty Images

					</div>
	
	<p>When Welter was hired by the Arizona Cardinals in 2015, she became the <a>first female coach</a> in the NFL.</p>
				
				
