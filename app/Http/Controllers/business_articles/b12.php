	<p>“What’s dangerous is not to evolve,” <a>Jeff Bezos</a> once <a>said in an interview</a>. This declaration pretty much sums up why the CEO and founder of <a>Amazon</a> is a powerhouse of a leader and innovator.</p>
<p>But, let’s not gloss over the fact that alongside his phenomenal riseBezos, who also owns a private rocket ship company Blue Origin and <em>The Washington Post</em>, has acquired a reputation for being ruthless and having “ice water in his veins,” according to a less-than-flattering article describing a punishing work culture that ran in <a><em>The New York Times</em></a> last August,</p>
<p>The tech boss worth upwards of <a>$60 billion</a>, wrote a company memo in response to the story, reports <a><em>Geekwire</em></a>, part of which reads:</p>
<p>“The article doesn’t describe the Amazon I know or the caring Amazonians I work with every day. But if you know of any stories like those reported, I want you to escalate to HR. I strongly believe that anyone working in a company that really is like the one described in the NYT would be crazy to stay. I know I would leave such a company.”</p>
<p>In addition, his senior vice-president of global corporate affairs, Jay Carney published a blistering response on <em>Medium</em>, where he cited journalistic bias and questioned the credibility of some of the story's sources, including one who was fired for an attempt to defraud vendors.</p>
<p>"'Why weren't readers given that information?'" <a>he wrote</a>.</p>
<p>While the 52-year-old may have some tough qualities for some to swallow, he clearly embodies courage, innovation and big-picture thinking, all of which forged Amazon into the massive global online one-stop shop, <a>reportedly worth $250 billion</a> as of 2015.</p>
<p><a></a></p>
<p>And it’s not just his businesses that evolve. Bezos is changing, too. It’s been through his acquisition of the flailing <a><em>Washington Post</em></a>, which he purchased for $250 million in 2013, Bezos has been able to show a different side.</p>
<p>When one of the newspaper’s reporters <a>Jason Rezaian</a> was released after being imprisoned in Iran for 18 months on charges of espionage, Bezos flew in his private jet to greet him. He then flew both Rezaian and his family to the Florida Keys at the freed reporter’s behest.</p>
<p>And Bezos hasn’t been afraid of employing his consistent business model of new ideas and expansion with the <em>Post</em>. Since his acquisition of the publication, it surpassed <em>The New York Times</em> in unique traffic last year for the first time and traffic has tripled<em> </em>since his takeover -- a fact largely attributed to his aggressive approach using technology and social media along with Bezos’s mandate to experiment, says <a><em>The</em> <em>Wall Street Journal</em></a>.</p>
<p>As Bezos continues to change and push boundaries, here are four other success tips that have put him at the top.</p>

