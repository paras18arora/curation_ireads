	<p>Bezos does not do PowerPoint. Instead, he has a more unconventional approach of how to pitch ideas at meetings and present them.</p>
<p>He requests that his employees write a four-to-six-page narrative before the meeting and distribute it to everyone who is attending for them to read for the first 20 minutes of the meeting, according to <a><em>Business Insider</em></a>. Afterwards, whoever is presenting fields questions. While it’s definitely a unique method of showcasing new ideas, Bezos believes it forces careful consideration of one before bringing it to his attention -- something a PowerPoint presentation just doesn’t accomplish.</p>
<p>“‘PowerPoint-style presentations somehow give permission to gloss over ideas, flatten out any sense of relative importance, and ignore the interconnectedness of ideas,’” <a>he wrote in a 2004 memo</a> explaining his decision to purge PowerPoint from Amazon.</p>
<p>Blue Origin president Meyerson was reluctant to engage in this practice. However, he relented. And now he admits to being a “<a>complete convert</a>.”</p>

