	<p>Bezos took his mantra, “What’s dangerous is not to evolve” and applied it to his leadership when he bought <em>The Washington Post</em> in 2013. The publication, while old school and respected, had long been failing in terms of readership and expansion.</p>
<p>Two points of evolution for this endeavor? He’s already increased web traffic growth and brought new ideas.</p>
<p>The first is in part due to him integrating aggregation content into the site. Bezos did so after seeing that aggregator sites were getting more traffic by summing up articles from the <em>Post </em>than the publication got writing them.</p>
<p>As for ideas, Bezos gave funding to<em> </em><a><em>PostEverything</em></a>, a millennial-focused site for experts to publish their opinions. He is also trying an experiment where in lieu of running costly global news bureaus, he’s instituted an automated web hub that connects <em>The Post</em> to hundreds of freelancers in the U.S. who can be assigned articles.</p>
<p>Will it work? Let’s be like Bezos and see.</p><br />
