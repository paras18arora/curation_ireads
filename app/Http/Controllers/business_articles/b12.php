	<p>Bezos is a big picture guy. You have to be to invest in private space exploration.</p>
<p>He’s been an investor in Blue Origin for 16 years -- before space-exploration rival Elon Musk got into the mix with his SpaceX company -- and he funds it out of his own pocket to the cool tune of <a>$500 million a year</a>.</p>
<p>In June, Blue Origin successfully sent a reusable rocket booster to space and according to the company’s president, there are plans to begin taking reservations for passengers to travel into the earth’s orbit (something Elon Musk’s SpaceX had already done).</p>
<p>“We think we’ll start flying passengers in 2017,” says <a>Rob Meyerson</a>, the president of his space exploration company Blue Origin. “These will be test engineers. Then we’ll sell tickets. I imagine Jeff and I will fly in the 2018-ish time frame.</p>
