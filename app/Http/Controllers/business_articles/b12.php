	<p>Bezos is a data-driven guy and a risk taker -- a powerful combination for decision making.</p>
<p>Bezos is a data-driven guy and a risk taker -- a powerful combination for decision making.</p>
<p>He did a hard pivot when he left his cushy job in 1994 as senior vice president of D.E. Shaw & Co., a successful hedge fund, to take a gamble and get into the space of e-commerce and books.</p>
<p>The Princeton grad first got the idea to start an internet enterprise in 1994, “after surfing the internet in search of new ventures for D.E. Shaw & Co. to invest in, he came across the statistic that World Wide Web usage was growing by 2,300 percent a month,” according to <a><em>Entrepreneur</em></a><em>.</em></p>
<p>Not one to shy away from opportunity, he recognized the possibilities of online sales and compiled a list of 20 possible products that might sell well on the internet; books were on the list, along with CDs and software.</p>
<p>Suffice it to say, he ran with books. It was so successful that at one point the growth of sales was outpacing his book supplies. Bezos had to purchase books from Barnes & Noble to keep up with orders.</p>

