	<p>If there's such thing as the default setting for a business drink, it's a hotel bar and a glass of Macallan 12-year-old scotch, the single malt that's a natural for soothing anxious partners or convincing uncertain investors. But depending where in the country you're sipping it, Macallan can seem like a bargain or a hideously expensive luxury. Not surprisingly, it's priciest with a view of Central Park. But more than twice what it goes for in Portland?<br />
<br />
How do they price a pour, anyway? "We base it on the cost price of the bottle," says Pradeep Raman, director of food and beverage at The Peninsula, Chicago. "Depending on our equipment cost and the cost of the labor, we have a percentage markup." (Equipment=Glass. Labor=Pour from bottle to glass.)<br />
<br />
Raman says the hotel pays $42 for the bottle. At 1 1/2 ounces per standard pour, you get 17 pours per bottle at a cost of $2.47 each. "On this our margin is 500 percent," he says.<br />
<br />
Only 500 percent! If the Oak Bar is also paying $42 per fifth, its margin is 890 percent. Wait--why aren't we in the bar business?</p>
